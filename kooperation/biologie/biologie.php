    <b>Blumberger Mühle<br></b>
    <br> Es besteht seit mehreren Jahren eine gute Kooperation / Partnerschaft zwischen der <b>Blumberger Mühle</b> (bei Angemünde), der <b>Naturwacht
        Brandenburg</b> und dem <b>Dathe Gymnasium</b> aus Berlin Fr.hain-Kreuzberg. Kern der Vereinbarung ist eine jeweils dreitägige Exkursion der 5./6. Klassen in das inmitten der Schorfheide gelegene NABU-Zentrum "Blumberger Mühle". Unter der fachkundigen Anleitung von Rangern der Naturschutzwacht beschäftigen sich die Kinder mit dem Thema "Nachhaltige Waldnutzung".
    <br> Am 25. Januar 2007 wurdel diese Kooperation mit einem Vertrag zwischen den drei Partnern hier im Hause besiegelt.
    <br>
    <br>

    <div class="subtitel">Die Blumberger Mühle - Das Kräuterprojekt
        <br>
    </div>
    <br> Natur erlebnisreiches Programm Lagerfeuer, Zelte Wanderungen, selbst kochen, Kräuter erkennen Freizeit.
    <br> Ob Lagerfeuer, Wanderungen, Kräuter erkennen oder selbst kochen, für jeden ist etwas dabei. Es gibt jede Menge Kreatives und Originelles zu entdecken. Kräutersalz und Tee herstellen, Stockbrot am Lagerfeuer und ein Besuch auf dem Gut Kerkow machten uns viel Spaß. In der Kochgruppe bereiteten wir zusammen mit dem Koch das Essen vor, die Aktionsgruppler streiften durch die Landschaft und unternahmen Experimente bei denen ihre Sinne zum Einsatz kamen, die Kräuterbestimmer sammelten Pflanzen, um Steckbriefe zu schreiben und später wurden die Gruppen gewechselt. Die Freizeit konnten wir füllen wie wir wollten. Alles in allem war es ein tolles Erlebnis!!
