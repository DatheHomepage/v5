    <div class="subtitel">- Die Eltern-Kooperationsbörse (EKB) -</div>
    <br> Das Wissen und die Fähigkeiten der Elternschaft einer Schule stellen ein enormes Potential dar, das im Unterricht, in Arbeitsgemeinschaften oder bei der Elternarbeit hilfreich sein kann. Dieses Know-how ist nur zu nutzen, wenn man weiß, wer welche Angebote und Talente einbringen kann. Um dies zu erfahren, bieten wir unseren Schülerinnen und Schülern, Lehrerinnen, Lehrern und Eltern die <a href="http://www.fadg.de/cms/forum">Eltern-Kooperationsbörse</a> als Plattform an. Über die Eltern-Kooperationsbörse sichern sich Schule und Eltern eine effektive und langfristige Zusammenarbeit, die insbesondere unseren Schülerinnen und Schülern zugute kommt.
