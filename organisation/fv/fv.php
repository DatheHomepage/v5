<div class="titel">- Der Förderverein -</div>
<br>
<div class="teaser">
    Der Förderverein am Dathe Gymnasium ist ein gemeinnütziger Verein, der sich aus Mitgliedsbeiträgen und Spenden finanziert und ideell und materiell die Aufgaben des Gymnasiums zu lösen hilft. Er engagiert sich für die Ausgestaltung der Einrichtung der Schule, unterstützt Schulprojekte und finanziert Auszeichnungen von Schülern für besondere Leistungen oder besonderes soziales Engagement. Der Förderverein ist maßgeblich am Erhalt der Biologiestation beteiligt. Aktuelle Informationen über die Projekte finden Sie auf der Seite des Fördervereins.
