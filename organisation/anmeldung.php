        <br><br>
		<div class="subtitel">Aufnahme in die bestehenden Klassen eines Schuljahres</div><br>
		Die Aufnahme ist u.U. noch möglich. Bitte kontaktieren Sie uns.<br>
		<br> Über Neuaufnahmen in die bestehenden Jahrgangsstufen kann nach Maßgabe 
		freier Plätze etwa Ende Mai entschieden werden. Bitte legen Sie zur Bewerbung ein 
		formloses Anschreiben und Kopien der letzten beiden Zeugnisse vor.
        <br>
        <br> Die Aufnahme in eine unserer Schnell-Lerner-Klassen ist möglich, 
		wenn Schüler/innen mit einer hohen kognitiven Begabung auf dem letzten, 
		der Aufnahme vorausgehenden Zeugnis einen Notendurchschnitt von 1,7 oder 
		besser in den Fächern Deutsch, Erste Fremdsprache, Mathematik und den jeweils unterrichteten 
		naturwissenschaftlichen Fächern nachweisen. Bitte vereinbaren Sie bei Interesse an einem 
		solchen Quereinstieg mit der Schulleiterin ein Aufnahmegespräch.<br><br>
