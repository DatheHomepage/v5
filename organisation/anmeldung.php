<div class="titel">- Anmeldung -</div>

<div class="artikel">
    <img class="artikel-bild" src="./teaser/schule/schule1.jpg">
    Sie möchten Ihr Kind am Dathe-Gymnasium in eine bestehende Klasse des laufenden Schuljahres anmelden?<br>
        Die Aufnahme ist u.U. noch möglich. Bitte kontaktieren Sie uns.
</div>
   
<p>
    Die Aufnahme in eine unserer Schnell-Lerner-Klassen ist möglich, wenn Schüler/innen mit einer hohen kognitiven Begabung auf dem letzten, der Aufnahme vorausgehenden Zeugnis einen Notendurchschnitt von 1,7 oder besser in den Fächern Deutsch, Erste Fremdsprache, Mathematik und den jeweils unterrichteten naturwissenschaftlichen Fächern nachweisen. Bitte vereinbaren Sie bei Interesse an einem solchen Quereinstieg mit der Schulleiterin ein Aufnahmegespräch.
</p>

<div class="artikel">
    Die Anmeldezeiträume für die neuen 5. und 7. Klassen sind abgeschlossen. Wir bedanken uns bei allen Familien der angemeldeten Kinder für das Vertrauen.
Alle Eltern erhalten am <b>25.05.2018 vom Schulamt </b>eine schriftliche Information über die Aufnahme, ggf. Nichtaufnahme Ihres Kindes an unserer Schule.<br>
    <br>
</div>

