    <b>Der dritte Tag der Skifahrt (09.01.2015)</b>
    <br> Am zweiten Skitag wurden wir schon früh von den Sonnenstrahlen geweckt.
    <br> Die Skigruppen waren nun endgültig festgelegt und die Arbeit an den Skitechniken konnte beginnen.
    <br> Mittags trafen die einzelnen Gruppen wieder aufeinander, um zusammen zu essen.
    <br> Viele bestellten das 5€-Menü, welches aus einem Hauptgericht und einem Skiwasser bestand.
    <br> Auch im weiteren Verlauf des Tages arbeiteten wir an unseren Skikünsten, die sich weiterhin verbesserten.
    <br> Den Abend beendeten wir unterschiedlich, zum Beispiel mit einem Kartenspiel.
    <br> (Von Lara und Flora)
    <br>
    <br>
    <b>Der vierte Tag der Skifahrt (10.01.2015)</b>
    <br> Es sollte der dritte Tag auf Skiern werden…
    <br> Jedoch waren die Witterungsbedingungen nicht optimal, sodass die Gondeln nicht fuhren.
    <br> Somit hatten wir einen „unerwarteten“ freien Tag, den wir uns jeweils selbstständig gestalteten.
    <br> Einige blieben in Lienz, während andere sich per Taxi zum Hotel fahren ließen.
    <br> Es wurde viel Billard und Karten gespielt. Abends, nach dem Essen gingen wir wieder zur Dorfkirche,
    <br> um den klaren Sternenhimmel und den Ausblick über Lavant zu genießen.
    <br> (Von Richard und Bruno)
    <br>
    <br>

    <b>Der fünfte Tag der Skifahrt (11.01.15)</b>
    <br> Heute ging es trotz der geringen Schneemenge wieder auf die teilweise angetaute Piste.
    <br> Dort fand auch die erste Leistungskontrolle im parallelen Grundschwung statt.
    <br> Allerdings legte sich nach kurzer Zeit eine Nebeldecke über uns, sodass unsere Sicht stark eingeschränkt wurde.
    <br> Nach dem Mittag zog ein heftiger Sturm auf, weshalb nach und nach alle Lifte schlossen.
    <br> Als auch der letzte aufgrund der bis zu 80 km/h starken Böen schließen musste war klar,
    <br> dass wir schon früher mit der Gondel nach unten fahren würden.
    <br> Doch auch diese wurde teilweise angehalten,sodass wir, viel später als sonst,
    <br> völlig erschöpft in unserer Pension ankamen.
    <br> Nach dem Abendbrot gab es dann einen Vortrag des ehemaligen Leiters der Bergrettungslawinenhunde,
    <br> der uns über die Entstehung der Lawinen und deren Gefahren aufklärte
    <br> und uns mithilfe eines Films Einblick in die Ausbildung von Lawinenhunden
    <br> und den Rettungseinsatz am Unfallort verschaffte.
    <br> Anschließend fielen wir müde ins Bett um uns auf den nächsten Tag vorzubereiten.
    <br> (Von Mathilda und Antonia)
    <br>
    <br>

    <b>Der sechste Tag der Skifahrt (12.01.2015) </b>
    <br> Heute lag durch den Sturm am Vortag viel Neuschnee,
    <br> allerdings war der Wind auf den Pisten bei weitem nicht so stark, wie Sonntag.
    <br> Die Bedingungen waren dementsprechend perfekt für sämtliche praktische Prüfungen
    <br> in den verschiedenen Leistungsgruppen.
    <br> Nachdem wir am Nachmittag mit dem Skibus zurück zum Hotel gefahren und
    <br> zeitig Abendbrot gegessen hatten,
    <br> wurden wir von einem Ranger besucht,
    <br> der uns eine Präsentation über den Tourismus und die Ökologie unseres Skigebiets gehalten hat.
    <br> (Von Anis und Fenja)
    <br>
    <br>

    <b>Der achte Tag der Skifahrt (14.01.2015) </b>
    <br> Wie jeden Tag, traf sich der Snowboardkurs zur allmorgentlichen Erwärmung.
    <br> Anschließend wurden zwei Prüfungen absolviert: flüssiges Kurvenfahren und angepasstes Geländefahren.
    <br> Nach der Mittagspause fuhren wir mit dem Lift erstmals zum Gipfel des Berges.
    <br> Danach durften wir frei üben.
    <br> (Von Tim und Alexander)
