    <b>Die Busfahrt (07.01.2015)</b>
    <br> Wir sollten um 6.45 Uhr auf dem Parkplatz vor unserer Schule sein.
    <br> Von dort aus erwartete uns eine 12 stündige Busfahrt nach Lavant (Österreich).
    <br> Im Bus versuchten viele ihren Schlaf nachzuholen, was nur Wenigen gelang.
    <br> Jedoch wurden wir durch Filme unterhalten.Um 19.00 Uhr kamen wir dann endlich an Lavanter Hof an.
    <br> Erschöpft von der Fahrt ging es dann in die Zimmer, um die Koffer auszupacken.
    <br> Anschließend gab es Abendbrot.
    <br> Wie zu erwarten gingen die meisten früh ins Bett mit großen Erwartungen für die nächsten Tage.
    <br> (Michael und Duc Anh)
    <br>
    <br>
    <b>Der erste Tag (08.01.2015)</b>
    <br> Zuerst liehen wir, nach einer 20-minütigen Busfahrt zum Fuß des Berges,
    <br> die Skiausrüstung aus. Bei der Gondelfahrt nach oben, konnten wir die atemberaubende Aussicht genießen
    <br> und wurden anschließend in drei Leistungsgruppen eingeteilt: Anfänger, Fortgeschrittene und Profis.
    <br> Den Rest des Tages verbrachten wir mit Übungen und Einfahren in den Gruppen. Am Abend ließen wir den Tag mit einem Spaziergang zu den höhergelegenen Dorfkirchen ausklingen.
    <br>(Lena)
