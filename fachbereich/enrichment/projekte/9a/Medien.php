<p><b>Andere Länder, andere Menschen? Wir schreiben den 24.06.2016: </b><br>
Die Klasse 9a des Dathe-Gymnasiums hat soeben mit den Vorbereitungen für die Talkshow zum 
Thema Rassismus begonnen. Doan, Quy Duy und Béla Anton Paulus verewigen die heutigen Geschehnisse. 
Die Aufzeichnung der Talkshow soll um 13:00 beginnen und es gibt noch viel zu tun: <br>
Das Studio muss ausgeleuchtet, Kameras und Ton müssen eingerichtet werden. Und das alles machen 
die Schüler unter Anleitung von Produktionsleiter Mischka Franke ganz allein. Glücklicherweise 
steht die Bühne noch vom Vortag und ist die Stimmung entspannt. In den Wochen vor der Talkshow 
haben verschiedene Gruppen unserer Klasse Kurzfilme zum Thema Rassismus gedreht.<br><br>
Es entstanden 
unterschiedliche Filme, beispielsweise eine Interviewcollage über die Vorurteile, von Passanten 
über Asiaten, Russen und Deutsche Unser heutigen Talkgäste, Alexander Freier, ein SPD-Politiker, 
welcher sich in der BVV als Vorsitzender des Jugendhilfeausschusses und Sprecher für Gleichstellung, 
Integration und Vielfalt der SPD-Fraktion engagiert, Sina Grauss und Karla Zierold Mitglieder der JumiCo 
(Jugend mit Courage), eine Organisation unserer Schule, die sich mit vielen Projekten wie z. B. 
„Dathe macht Mobil“ für die Flüchtlinge einsetzt.<br> Hierbei werden Spenden gesammelt, um in Kooperation 
mit einigen Flüchtlingsunterkünften den Flüchtlingen eine Fahrt mit öffentlichen Verkehrsmitteln zu 
ermöglichen. <br>
Unsere Klasse produziert zwar diese Talkshow, doch wie sind ihre eigenen Erfahrungen? <br><br>
Wie stehen sie zu Rassismus? <br>
Florens (Klasse 9a): „Natürlich entspricht nicht alles, 
was man sich erzählt, der Wahrheit. <br>
Trotzdem finde ich, dass einige Vorurteile wahr sind, doch man sollte vorsichtig mit seinem 
Denken sein, um nicht andere zu verletzen.“<br> 
Zacharie (Austauschschüler aus Orleans): „In meiner Schule gab 
es einen Lehrer, der sich sehr abwertend gegenüber dunkelhäutigen Schülern verhalten hat. Diese wurden bei
 vergessenen Hausaufgaben stärker geahndet als hellhäutige Schüler.“<br>
 Karla (JumiCo-Mitglied): „ Ich war mal mit einer Freundin bei einer Demo in Marzahn. 
 Und wir wurden auf dem Rückweg (S-Bahn) von einem betrunkenen Mann angepöbelt, 
 da wir vermutlich recht „links“ gekleidet waren. Dieser betete Hitler an und hob anschließend seinen Arm. 
 Wir haben uns aber nicht getraut gegen ihn Vorzugehen, da er uns einschüchterte.“<br><br>
 Es ist nun 12:30 Uhr. <br>
 Unsere Talkgäste erscheinen pünktlich. Nur noch ein letzter Schliff und bald kann die Show beginnen… 
 Die Talkshow war ein voller Erfolg! <br>
 Bei der ersten Durchlaufprobe gab es noch einige Probleme und so 
 waren die Erwartungen nicht mehr besonders hoch. Umso mehr sind wir jetzt mit dem Endergebnis zufrieden.
 Jetzt müssen wir leider noch alles Abbauen, doch das gehört eben bei der Produktion einer Talkshow dazu. 
 <br>
 Vor drei Jahren erhielt unsere Schule den <b>Titel „Schule ohne Rassismus/Schule mit Courage“, </b>ein Titel auf
 den wir stolz sind, ein Titel für den wir uns einsetzen. <br> Fotogalerie folgt
