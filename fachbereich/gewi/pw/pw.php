	  Am 11.05.2015 fand eine Exkursion des PW-Kurses statt. Den Projektbericht findet ihr<a href="./planspiel_bundestag.php"> hier. </a><br><br>
	  Das Fach Politikwissenschaft ist es, das in besonderem Maße die notwendigen Kompetenzen,<br>
	  Kenntnisse und Fähigkeiten zur Erreichung genau dieses zuvorderst gestellten Zieles des Schulgesetzes des Landes Berlin vermittelt. <br>
      Vermittelt und behandelt werden u.a.<br>
	  
	  
	<ul>
	<li>- die theoretischen Grundlagen der Demokratie und anderer 
		politischer Herrschaftsformen  (1. Sem.), </li>
	<li>- der verfassungsrechtlichen Wirklichkeit in der Bundesrepublik 
		Deutschland und das ökonomische Handeln in einer globalisierten Welt (2.Sem.), </li>
	<li>- die Europäische Union und die Geschichte und Bedeutung 
		aber auch Perspektiven des europäischen Integrationsprozesses (3. Sem.) und </li>
	<li>- Probleme und Konflikte in der globalisierten Welt auch am 
		Beispiel relevanter Sicherheitsorganisationen (4. Sem.).</li></ul><br>
	
	<center><IMG SRC="./fachbereich/gewi/pw/schulepw.jpg"><br><br></center>
	
	Dabei bietet der Rahmenlehrplan für das Fach eine Vielzahl an Gestaltungsmöglichkeiten durch wählbare Themenfelder,<br>
	die häufig an aktuellen Geschehnissen und Problemlagen ausgerichtet und problematisiert werden.<br>
	Der Fachbereich arbeitet konkret daran, einer  Vielzahl an Schülern die Möglichkeit, 
	sich dezidiert und vertieft mit den politischen Rahmenbedingungen<br>
	und aktuellen politischen, sozialen und ökonomischen Problemen wissenschaftspropädeutisch 
	und kenntnisreich auseinanderzusetzen, darüber zu diskutieren<br>
	und sich ein fundiertes, differenziertes Urteil zu bilden. Damit sollen die Schüler befähigt 
	werden als mündige und politisch versierte Staatsbürger politisch zu partizipieren.<br><br>
	Die unterrichtenden Lehrkräfte nutzen dabei die zur Verfügung stehenden Mittel und Medien 
	und animieren auch die Schüler dazu,<br>
	Medien gezielter und informativer einzusetzen. Hierbei können grundlegende und verschiedene 
	Formen der Präsentation, handlungsorientierte Lernformen und praktische Teilhabe von Politik<br>
	im Rahmen von bspw. Podiumsdiskussionen oder im Engagement für "Schule ohne Rassismus - 
	Schule mit Courage" ausprobiert, geübt und vertieft werden.<br>
	Gerade die inhaltliche und methodische Arbeit und Weiterentwicklung der Schule als Titelträger/ 
	"Schule ohne Rassismus - Schule mit Courage" steht im Blickpunkt des Fachbereiches.<br>
	In verschiedenen Formen werden thematische Anbindungen schulweit geschaffen und in verschiedenen Konzeptionen 
	in Projekten umgesetzt.<br><br>
	Ein besonderes Anliegen der Kollegen besteht vor allem darin, dassauch praktisches politisches Handeln 
	und politische Partizipation am Dathe-Gymnasium u.a.<br>
	durch die Teilnahme an der Juniorwahl oder auch im Rahmen der Durchführung einer Podiumsdiskussion mit den 
	Direktkandidaten des Wahlkreises zur letzten Bundestagswahl immer weiter forciert und impliziert werden.<br><br>
	In der Sekundarstufe I bildet das Fach Sozialkunde das äquivalent und ist in das Fach Geschichte integriert. 
	Hier erhalten die Schüler bereits Grundlagen und Kompetenzen im sozialen, politischen und ökonomischen Bereich.<br>
	Hierbei bietet der Rahmenlehrplan des Landes Berlin für das Fach Sozialkunde eine Orientierung.<br>
	Aufgrund der Begrenzung des Stundenumfangs des Faches Geschichte durch die politisch Verantwortlichen findet Sozialkunde 
	zumeist durch die Anbindung an historische Sachverhalte statt und kann nicht als eigenes Fach unterrichtet werden.<br>
