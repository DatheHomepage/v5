	  <b>Datenschutz – wir sind Opfer und Täter!</b><br>
	  Am 17.09./18.09.2013 führt Ingo Legler vom Berufsverband der Datenschutzbeauftragten für alle 7. Klassen eine Informationsveranstaltung zum Datenschutz durch. <br>

	  Ingo Legler spricht mit den Schülern über soziale Netzwerke, Chats, Video- und Musikdownloads, Cyberfreunde, Cybermobbing, Webcam, Mails und Abzocke.<br>
	  Er konfrontiert die Schüler mit dem Thema Datenschutz, da wo es die Schüler ganz persönlich betrifft.<br>
	  „Datenschutz geht zur Schule“ ist eine Initiative des Berufsverbandes der Datenschutzbeauftragen Deutschlands (BvD) e.V. <br>
	  Die ehrenamtlichen Mitglieder sind aktive Datenschutzbeauftragte. 

