	  <b>Politikunterricht mal anders: Schüler fragen – Politiker  antworten</b><br>
Anlässlich der Bundestagswahl luden am 09.09.2013 Schüler der Oberstufe die Direktkandidaten aus Friedrichshain-Kreuzberg zu einer Podiumsveranstaltung in die Dathe-Oberschule ein.<br>
Für die Schüler war dies eine einmalige Gelegenheit, Politiker persönlich zu erleben.<br>
Halina Wawzyniak (Die Linke), Hans-Christian Ströbele (Bündnis 90 / Die Grünen), Cansel Kiziltepe (SPD), Sebastian von Hoff (Piraten) und Benjamin Fritz (Junge Union) diskutierten unter der Leitung der Q3-Schüler <br>
Sarya Derado (15 Jahre) und Ralf Retzlaff (17 Jahre) Themen wie Mietpreise, Mindestlöhne und Bildungspolitik.<br>
Bei der Frage der steigenden Mieten kam es zu einer erhitzten Debatte, angetrieben durch die Aussagen des FDP-Kandidaten Helmut Metzner, <br>
der eine deutliche Gegenposition zu der von den anderen geforderten Mietpreisbremse einnahm. 
Nach der Diskussion standen die Politiker noch für Einzelgespräche zur Verfügung.<br>
Diese einmalige Chance wurde vor allem von Schülern genutzt, die noch nicht entschieden waren, wem sie ihre Stimme geben wollen. <br><br>
Die Schülermoderatorin Sarya Derado war von der Offenheit und Standhaftigkeit der Kandidaten überrascht.<br> 
Ihrer Meinung nach hat sich die zeitaufwendige und nicht immer einfache Planung der Veranstaltung auf jeden Fall gelohnt.<br><br>
<br>
Autorin: Emily Péterfia (Dathe-Oberschule, Q3)

