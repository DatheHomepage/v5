      <div class="teaser"> <b>Ein voller ERFOLG! Juniorwahl 2013</b><br><br>
	  

Mit einer Gesamtwahlbeteiligung von 89 % können wir zufrieden sein.<br>
Interessant sind dabei sicherlich Unterschiede zwischen dem Ergebnis der Juniorwahl und dem der Bundestagswahl.<br>
Die Schüler haben im Rahmen dieses fast alle Jahrgänge betreffenden Projektes der Fachbereiche Geschichte und Politikwissenschaft politisch partizipiert und sich vertieft mit Politik, Parteien, deren Programmen und unserem politischen System beschäftigt.<br> 
