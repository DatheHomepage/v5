      <div class="titel">- Planspiel Bundestag -</div>

      <div class="teaser">Am 11.Mai 2015 waren die Politikwissenschaft-Grundkurse unserer Schule im Deutschen Bundestag, 
um an einem Planspiel teilzunehmen, das den Weg der Gesetzgebung in origianl Ausschusssälen simuliert.<br><br>

Zuerst bekam jeder einzelne Schüler die Rolle eines Bundestagsabgeordneten zugeteilt. <br>
Dann simulierten wir zwei parallel verlaufende Gesetzesvorhaben zu den Themen "Tierschutz in der Landwirtschaft"
und "Einwanderung". <br><br>

<center><img " src="./planspielbundestag.jpg" ></center><br>


In den Ausschüssen wurde viel diskutiert und der Gesetzentwurf inhaltlich bearbeitet, sodass letztendlich 
eine Beschlussempfehlung gegeben werden konnte und im Plenum eine Schlussabstimmung über den Gesetzentwurf stattfinden 
konnte.<br>
Nachdem wir im Besucherrestaurant Paul-Löbe-Haus zu Mittag gegessen hatten, wurden wir zum Abschluss noch durch 
den Bundestag geführt und hatten im Anschluss daran die Möglichkeit, die Reichstagskuppel zu besuchen.<br><br>

Wieder einmal war es ein gelungener Projekttag, der auch in Zukunft in vergleichbarer Weise stattfinden sollte.<br><br>

(Flora, Q2)<br>

