<div class="subtitel">&gt;&gt; Dienstag &lt;&lt;</div>
<br>
<br> Exkursionstag 1. (04.04.17): Ein wenig müde, aber mit viel Vorfreude auf die Ostsee trafen wir uns am Dienstag 08:00 Uhr am Hauptbahnhof. Nach der dreistündigen Fahrt, in der wir den verlorenen Schlaf nachgeholt haben, konnten wir endlich die salzige Meeresluft von Stralsund atmen. Unsere Führung im Ozeaneum startete pünktlich um 12:45 Uhr und war sehr interessant. Dadurch konnte der deutsche Teil der Gruppe viele Informationen für ihre Klausurersatzleistung sammeln. Besonders sehenswert sind die großen Aquarien und das Freiluftgehege der Humboldtpinguine, welche erst seit ein paar Wochen wieder draußen wohnten. Danach stand uns der restliche Tag zur freien Verfügung und wir sind in kleinen Grüppchen durch die Altstadt und am Hafen entlang gebummelt. Das Fischbrötchen durfte hierbei natürlich nicht fehlen und auch das Postkartenschreiben nach Berlin wurde ebenfalls am Abend erledigt. Um 22:00 Uhr gingen wir dann erschöpft, aber zufrieden zu Bett.

