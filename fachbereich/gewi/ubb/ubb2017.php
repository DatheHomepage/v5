<div class="subtitel">&gt;&gt; Mittwoch &lt;&lt;</div>
<br>
<br> Mittwoch, 05.04.2017
<br> Am Morgen des zweiten Exkursionstages befanden wir uns immer noch in Stralsund. Nach dem ausgiebigen Frühstück stiegen wir in den Zug nach Prora ein. Nach der kurzen Fahrt liefen wir zum Naturerbezentrum Prora, um die Aussicht auf die Ostsee genießen zu können. Der erhoffte Sonnenschein blieb hinter den Wolken, sodass der Blick nicht optimal war. Nachdem wir den Baumwipfelpfad entlang gegangen sind, wollten wir bis nach Binz eine Strandwanderung von ca. 4 Kilometern machen. Wir verkürzten unseren Weg mit der örtlichen Bimmelbahn. Das Wetter ist besser geworden und vereinzelte Sonnenstrahlen versüßten uns die Wanderung. Durch die Sonnenstrahlen trauten sich zwei slowakische Gäste in die Ostsee. In Binz angekommen hatten wir Freizeit, wo die meisten sich Essen für die Fahrt holten oder sich die Stadt mit ihrer Seebrücke anschauten. Gegen 16 Uhr stiegen wir in den Zug nach Berlin ein. Nach dem anstrengenden Tag schliefen alle im Zug bis wir ins Bett fallen konnten.


