<div class="subtitel">&gt;&gt; Freitag &lt;&lt;</div>
<br>
<br> Nachdem wir unsere slowakischen Gäste am Donnerstagabend vom Busbahnhof abgeholt hatten trafen wir uns am Freitagmorgen in der Schule. Während in Raum 304 fleißig die Vorbereitung für das Frühstück lief, bekamen die Austauschschüler eine Schulführung. Dabei fanden sie besonders gefallen an der Biostation und den Trampolinen auf dem Außenhof.
<br> Als dann alle, nach der Begrüßung und offiziellen Eröffnung des Programms, gestärkt waren dank des Frühstücksbuffets ging es dann an den nächsten Programmpunkt: Unterrichtsbesuch. Die Slowaken hatten das Glück alle im Matheunterricht zu hospitieren.
<br> Im Anschluss daran zeigten wir ihnen noch die Umgebung der Schule, dabei hielten einige Kurzvorträge zur Berliner Mauer. Als Abschluss des offiziellen Programms für den Tag diente das Essen beim Burgermeister.

<br>
<br>

<hr class="line">
<div class="subtitel">&gt;&gt; Montag &lt;&lt;</div>
<br> Nachdem wir am Wochenende Berlin mit unseren Austauschschülern auf eigene Faust erkundet haben, ging es am Montag wieder in der Gruppe weiter. Erster Stopp war die Reichstagskuppel. Oben angekommen wurden viele Fotos geschossen und die Größe, Vielfältigkeit und Schönheit von Berlin bewundert. Nach einem kurzen Fußweg zur „Topografie des Terrors“ hatten wir eine sehr aufschlussreiche Führung durch die Ausstellung, die durchaus zum Nachdenken anregte. Um den Tag dann passend abzuschließen durften wir im Yorck-Kino in einer Privatvorstellung beim Film „Tschick“, der auch bei unseren Gästen sehr gut ankam, noch einmal herzlich lachen.
