<div class="subtitel">&gt;&gt; Allgemein &lt;&lt;</div>
<br>
<br> Der <b>Seminarkurs “Umwelt baut Brücken” </b>stellt einen Kontrast zum normalen Schulalltag an dem Dathe-Gymnasium dar.
<br> Er soll die Kreativität, Journalistischen-Fähigkeiten, sowie das Umweltbewusstsein der Schüler/innen fördern.
<br> Im Rahmen des Kurses wird <b>seit 2005/2006 ein Austausch </b> mit einer Schule aus der Slowakei durchgeführt.
<br>
<br>
<br> “Umwelt baut Brücken” ist ein Seminarkurs für das erste und zweite Halbjahr der Oberstufe. Beide Semester sind in das Abitur einbringbar.
<br> Im Laufe des Kurses wird zu verschiedenen Umweltthemen recherchiert und zum Ende des Semesters eine Zeitungseite erstellt.
<br>

<u>Journalistische Tätigkeiten:</u>
<ul>
    <li>Wie schreibe ich einen Artikel?
        <li>Wie fotografiere ich ein dokumentierendes Foto?
            <li>Wie gestalte ich eine Zeitungsseite?
                <li>Wie organisiere ich eine Schülerzeitung?
</ul>

<u>Austausch:</u>
<br> Im zweiten Semesters des Kurses erfolgt ein ca. siebentägiger Austausch mit Schüler/-innen aus Poprad, Slowakei.
<br> Diese Schüler hatten Deutsch als erste Fremdsprache und können sich dementsprechend gut verständigen. Man lernt sie über Steckbriefe und dann über das Internet näher kennen.
<br> In der Zeit, in der die Slowaken hier sind, gibt es ein besonderes Programm, in dem viele Ausflüge vorgesehen sind. In Berlin zu städtischen Sehenswürdigkeiten und in der Slowakei in das Naturschutzgebiet der hohen Tatra.
