<u>Besuch des  slowakischen Staatspräsidenten im Schloss Belvue</u>
<br>
<br> Am Mittwoch dem <b>26.2.2014</b> besuchte Ivan Gašparovic unseren Bundespräsidenten Joachim Gauck.
<br> Unser Seminarkurs “Umwelt Baut Brücken” wurde vom Bundespräsidialamt dazu eingeladen live beim Empfang dabei zu sein!
<br> Dank unserer bereits seit einigen Jahren laufenden Partnerschaft mit der Schule in Poprad und des Schüleraustausches konnten wir dabei sein,
<br> wie Gauck und Schadt das Slowakische Paar im Schloss Belvue empfingen.
<br> Die Vorbereitungen für diesen Austausch im April diesen Jahres sind auch schon im vollen Gange.
