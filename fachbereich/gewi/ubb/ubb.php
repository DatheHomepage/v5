<div class="subtitel">&gt;&gt; Slowakeiaustausch 2015 &lt;&lt;</div>
<br>
<br> Erfolgreiche Rückkehr vom Schüleraustausch mit Poprad (Slowakei) Die Schüler/innen des Seminarkurses Umwelt baut Brücken sind wieder wohlbehalten zurück und haben gemeinsam mit den slowakischen Austauschpartner/innen ein abwechslungsreiches Programm vom Besuche einer Eishöhle bis zur Information über das Müllmanagement eines 5-Sterne-Kempinski-Hotells absolviert. Zwischen dem einen und dem anderen lagen viele Wege in mittelalterlichen Städten und einige Bergkuppen in wunderschöner Natur. Berichte der Exkursionen werden demnächst auf der Homepage veröffentlicht.
