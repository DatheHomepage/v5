	<b>Welche Inhalte werden im Ethikunterricht behandelt?</b>
	<br>
	<br> Anhand der vier Kantischen Grundfragen werden beispielsweise folgende Inhalt thematisiert:
	<br> 1. Was kann ich wissen? Willensfreiheit vs. Hirnforschung (Klasse 9/10)
	<br> 2. Was soll ich tun? Gerechtigkeit (Klasse 7/8)
	<br> 3. Was darf ich hoffen? Jenseitsvorstellungen in den Religionen (Klasse 9/10)
	<br> 4. Was ist der Mensch? Mensch vs. Tier (Klasse 7/8)
	<br>
