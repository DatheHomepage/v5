	<b>„Habe Mut, dich deines eigenen Verstandes zu bedienen.“</b>
	<br>
	<br> Diese Forderung stammt von dem neuzeitlichen Philosophen Immanuel Kant und gilt als Leitgedanke der Aufklärung. Aber auch heute, mehr als 200 Jahre später, ist sie noch aktuell. Im Ethikunterricht wird der Forderung Kants im besonderen Maße Rechnung getragen. Doch tun das nicht auch andere Fächer?
	<br>
	<br>
	<b>Warum braucht Berlin das Fach „Ethik“?</b>
	<br>
	<br> - Ziele wie friedliches Zusammenleben und Respekt für den anderen entwickeln, werden nicht beiläufig thematisiert. Sie sind Inhalte des Faches selbst.
	<br> - Es passt zu einer multikulturellen Stadt wie Berlin, in der der Dialog zwischen Menschen unterschiedlicher Herkunft, Religion, Kultur unverzichtbar ist.
	<br> - Anders als im Religionsunterricht wird der Ethikunterricht weltanschaulich neutral unterrichtet.
	<br> - Ethik bietet trotz eines Lehrplans Freiräume für aktuelle Fragen der Schüler.
	<br>
	<br> Weitere Informationen rund um das „neue“ Fach finden Sie
