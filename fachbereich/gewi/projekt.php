<b>WIKIPEDIA - Projekt am 11.01.16 mit den Klassen 8c/d</b><br> <br>
Dieser Workshop wurde von Herrn D. Bartel geleitet und gestaltet und war ein sehr informativer
Beitrag zur informatischen Grundbildung unserer Schüler.<br><br>
Hierzu genauer:<br>
Im ersten Teil haben die Schüler eine Einführung in die Wikipedia bekommen. <br>
<ul>
    <li>
        Klärung einiger Grundbegriffe wie Enzyklopädie, Community, Urheberrecht und Freies Wissen
    </li>
    <li>
        Arbeit wird vollständig von Freiwilligen und Ehrenamtlichen geleistet, die nicht nur die gesamten Inhalte
        erstellen,
        sondern das Projekt auch selbst verwalten.
    </li>
    <li>einige Zahlen zu Wikipedia: 1,9 Mio. Artikel und rund 7.000 dauerhafte Mitarbeiter in Deutsch,
        weltweit 26 Mio. Artikel und 70.000 Mitarbeiter
    </li>
</ul>
<br>
Im zweiten Teil wurde es praktischer. Man konnte sehen, wie Wikipedia aufgebaut ist und funktioniert.
Neben dem eigentlichen Artikel konnte man sich die Diskussionsseite ansehen. <br>
Es wurde probehalber eine Veränderung vorgenommen, einmal anonym und einmal als
angemeldeter Wikipedia-Autor. Dabei wurde sichtbar, das alles, was in der Wikipedia verändert wird,
protokolliert wird und so jederzeit geprüft und auch zurückgesetzt werden kann.
<br>
<br>

Dies war der Übergang zum dritten Teil: Kann ich Wikipedia vertrauen?<br>

Die Schüler haben gesehen, welche Kriterien sich die Wikipedianer selbst gegeben haben: <br>
<ul>
    <li>
        Neutralitätsgebot, das Verbot eigener Forschung, eng daran angelehnt das Gebot der Quellen- und
        Belegpflicht und nicht zuletzt das immer schwierige Thema der "enzyklopädischen Relevanz".
    </li>
</ul>
Diese vier Grundpfeiler wurden genauer durchgesprochen und anschließend konkret angesehen - wie ein Artikel also von
der Neuanlage, als oft sehr schwacher und
schlechter Text, über verschiedene Phasen der Verbesserung zunehmend heranreift und manchmal über Jahre
dann zu einem ausgesprochen ausführlichen und qualitativ <b>hochwertigen Artikel</b> wird.
<br>
<br>

In einer abschließenden Praxisübung hatten die Schüler dann Gelegenheit, sich in kleinen Teams einen Artikel
vorzunehmen und ihn
anhand des vorher Gelernten zu prüfen, sowohl auf die Einhaltung der eigenen Erwartungen wie der "offiziellen"
Kriterien.
Über die Ergebnisse haben die Teams dann den anderen Schülern in kurzen Statements berichtet.

