<b>Datenschutzprojekt am 11.01.16 Klassenstufe 7</b><br><br>

Herr Ingo Legler vom Berufsverband der Datenschutzbeauftragten führte wieder für die 7.Klassen eine
Informationsveranstaltung zum Datenschutz durch.<br>
Mit seiner Erfahrung als Datenschützer klärte er über die Gefahren im Internet auf.
Er diskutierte mit den Kindern, welche Daten sie besser nicht preisgeben sollten und machte
mit Filmen deutlich, dass z.B. Fotos, einmal ins Netz gestellt, dort auch bleiben. <br>
Die Kinder lernten die Möglichkeiten der Handyortung kennen und
erhielten den Tipp, das GPS doch mal auszuschalten. Ihnen wurde gezeigt,
wie schnell heute Passwörter geknackt werden können und
welche Passwörter sicher sind. <br>
Und was den Schülern besonders gefallen hat: Sie konnten fragen und bekamen Antworten.

