Im Herbst 2009 hatten wir, Sch&uuml;lerInnen der Klasse 11
zusammen mit Sch&uuml;lernInnen des
Cou&shy;ber&shy;tin-Gymnasiums, die M&ouml;glichkeit, eine
Gedenkst&auml;ttenfahrt im Rahmen des
Reli&shy;gi&shy;o&shy;ns&shy;unterrichtes nach
Theresienstadt
zu unternehmen.
      <p class="MsoNormal">Theresienstadt
liegt in Tschechien und diente schon Kaiser
Joseph II. im 18. Jahrhundert als Fes&shy;tungs&shy;stadt,
wurde dann aber von den
Nazis als KZ umfunktioniert.</p>

      <p class="MsoNormal">Organisiert
und betreut von Frau Berendt und Herrn Rau sowie
den Zivildienstleistenden Sascha und Friedemann, erkundeten wir durch
wechselseitige F&uuml;hrungen, die wir selbst gestalteten, das
Vor&shy;zeigelager des
NS-Regimes.</p>

      <p class="MsoNormal">Beim Projekt
&bdquo;Von der Nummer zum Namen&ldquo; recherchierten wir
in Gruppenarbeit nach Biografien von Gefangenen und bekamen dadurch
einen
Eindruck &uuml;ber das Leben in Theresienstadt, dem dort<span style="">&nbsp;&nbsp; </span>er&shy;fahrenen
Leid, aber auch der nicht schwindenden Hoffnung, die
zum Beispiel in Form der Kinder&shy;oper Brundib&aacute;r, in
der es von Zusammenhalt in
schlechten Zeiten handelt, dargestellt wurde.</p>

      <p class="MsoNormal">Bei
F&uuml;hrungen durch das j&uuml;dische Viertel in Prag, dem
ehemaligen Ghetto, bekamen wir die ein&shy;ma&shy;li&shy;ge
Chance mit Frau Podmelova,
einer Zeitzeugin, ins Gespr&auml;ch zu kommen. Dabei war auch
inte&shy;res&shy;sant zu
sehen, wie die Gefangenen nach ihrer Befreiung zur&uuml;ck ins
Leben fanden.</p>

      <p class="MsoNormal">Sehr
beeindruckend war auch der Besuch von Lidice, eine
Stadt, die als Racheakt der Nazis dem Erd&shy;boden gleichgemacht
wurde, in der wir
ebenfalls eine &Uuml;berlebende trafen.</p>

      <p class="MsoNormal">Zum Abschluss
der Fahrt lie&szlig;en wir kleine Papierschiffchen
mit Teelichtern aufs Wasser treiben, in Ge&shy;denken an all die
Opfer des
Holocausts.</p>

