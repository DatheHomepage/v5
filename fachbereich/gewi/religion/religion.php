    <p>Der RU ist ein offenes Angebot an alle Sch&uuml;lerInnen, die sich f&uuml;r die Welt der Religionen interessieren. Die Teilnahme ist freiwillig. </p>
    <br>
    <a href="?sect=./fachbereich/gewi/religion/theresienstadt">Fahrt nach
Theresienstadt</a>
    <br>
    <br>
    <p>Seit 1994 gibt es RU an unserer Schule. Er wird verantwortet von der Evangelischen Kirche. Die Teilnahme ist freiwillig und setzt keinerlei religi&ouml;se Bindung oder Vorbildung voraus. Entscheidet sich jemand f&uuml;r die Teilnahme, erfolgt dies durch eine Anmeldung durch die Eltern oder ab dem 14 Lebensjahr durch den Sch&uuml;ler/ die Sch&uuml;lerin selber. </p>
    <p>Die Arche Noah, eines der Wahrzeichen unserer Schule, steht f&uuml;r Gerechtigkeit, Frieden und Bewahrung unserer Umwelt. Diese drei Schwerpunkte pr&auml;gen auch die Themen des Religionsunterrichts. Der Themenplan ist als Leitfaden zu verstehen. Die Reihenfolge kann variieren und immer wieder werden aktuelle Aspekte mit aufgenommen. </p>
