      <div class="teaser">Das Fach Geschichte wird an unserer Schule in der <i>Sekundarstufe
I </i>ab der Klasse 5 unterrichtet. Für das Fach
Sozialkunde sieht der Lehrplan von der Klasse 7 an ca. 1/3 des
Stundenvolumens der Fächerkombination Geschichte/Sozialkunde
vor. Im <i>Wahlpflichtunterricht </i>kann Geschichte in der Klasse 10
gewählt und somit über den obligatorischen Lernplan hinaus vorhandene
Kenntnisse vertieft und fachbezogene Fertigkeiten weiterentwickelt werden.
</div>
<div class="teaser">
      <br><br>
	  
	  	  Der Fachbereich
Gesellschaftswissenschaften ist der größte
Fachbereich am Dathe-Gymnasium und umfasst die Fächer <i>Erdkunde,
Geschichte, Sozialkunde </i>und<i> Politikwissenschaften. </i></p>
	  <br><br>
	  <img src="./fachbereich/gewi/geschichte/schulege.jpg"> <br><br>

	  Im gesellschaftswissenschaftlichen
Aufgabenfeld der gymnasialen Oberstufe sind diese Fächer
folgendermaßen
vertreten:<br>


- Geschichte, Politikwissenschaften und Erdkunde als <b>Grundkurs</b> im Kurssystem der Qualifikationsphase<br>


- Geschichte und Erdkunde als <b>Leistungsfächer</b>
(2. Prüfungsfach) im Kurssystem</p>


      <p>Der Fachbereich verfügt
über eigene Fachräume, die mit
fachspezifischen Materialien und modernen Medien ausgestattet sind. </p>


      
      <p>Der Fachbereich arbeitet intensiv an der Entwicklung einer <i>"Methodenkompetenz
im gesellschaftswissenschaftlichen Aufgabenfeld"</i>, um den
Schülerinnen und Schülern selbständiges
Lernen zu ermöglichen und ihre Handlungskompetenz zu
erweitern. Dies wird ergänzt und unterstützt durch
fachinterne Fortbildungen für die Lehrkräfte. </p>


      
      <p>Der Fachbereich bietet den
Schülerinnen und Schülern durch <i>Wandertage,
Projekttage und Museumsbesuche</i> die Möglichkeit, sich
ein Bild von der kulturellen, geographischen und historischen Vielfalt
Berlins und Brandenburgs, zu machen, um ihr Lebensumfeld intensiver
wahr zu nehmen und in seiner Komplexität besser zu verstehen. Kursfahrten
      dienen dazu, die Auseinandersetzung
mit anderen Kulturkreisen und Epochen zu vertiefen.</p>


      
      <p> Das Dathe-Gymnasium nimmt bis heute an der <b>Juniorwahl</b> teil, um die
Schülern praxisnah an die Wahrnehmung elementarer politischer
Rechte (Bundestags-/Landtagswahlen) heran zu führen. In diesem
Zusammenhang waren vor der letzten Bundestagswahl alle Direktkandidaten
des Bezirks Friedrichshain/Kreuzberg zu einer Podiumsdiskussion in der
Aula der Schule vertreten.</p>


