<b>Anmeldung – So geht’s</b><br><br>

Der Unterricht ist für alle Schülerinnen und Schüler offen. Bei Interesse bitte auf dem Schul-Anmeldebogen ‚Lebenskunde’ ankreuzen.
Nach der ersten Stunde bekommt ihr dann ein Anmeldeformular mit nach Hause, das zur nächsten Lebenskunde-Stunde mitgebracht bzw. 
im Schulsekretariat abgegeben werden kann.
Der Einstieg ist auch danach grundsätzlich jederzeit möglich. Einfach vorbeikommen und fragen. 
Im Sinne der Gruppe wird ein verbindliches und regelmäßiges Erscheinen vorausgesetzt.<br> 
Der freiwillige Unterricht lebt von Schüler/innen, die gerne und interessiert dabei sind.<br>
Sollte das mal nicht mehr der Fall sein, kann man sich schriftlich abmelden, möglichst bitte zum Schuljahresende oder Halbjahr.<br>
<br>Bis 14 Jahre braucht es für An- wie Abmeldung die Unterschrift eines Erziehungsberechtigten.<br><br><br>

