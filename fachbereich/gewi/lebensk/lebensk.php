Wer bin ich? Was ist wichtig? Was ist gerecht? Wieso funktioniert manches auf der Welt nicht so, wie es vielleicht vernünftig wäre?<br>
Wie will ich leben? Was kommt nach dem Tod? Wie begegne ich jemandem, der ganz anders ist als ich?<br> 
Viele Fragen im Leben sind gar nicht so leicht zu beantworten.
Oft gibt es keine vorgefertigten Lösungen, kein Richtig und kein Falsch. Wie spannend!
Und wie wichtig ist es, über uns und die Welt zu reflektieren, ins Gespräch zu kommen, sich auseinanderzusetzen, eigene Standpunkte zu entwickeln.<br>
So entwickelt man sich weiter. <br>
Wir denken selber in Lebenskunde, machen Erfahrungen, diskutieren, experimentieren, philosophieren, 
lernen voneinander und finden gemeinsam heraus.
Ausgehend von euren Erfahrungen und Sorgen, Träumen und Wünschen, 
setzen wir uns mit verschiedenen Lebensweisen und Haltungen auseinander,<br>
hinterfragen Vorurteile und gesellschaftliche Vorgänge, beschäftigen uns mit dem Sinn und 
auch dem Zauber des Lebens.
Eine wichtige Rolle spielen dabei humanistische Werte wie Selbstbestimmung, 
Verantwortung, Respekt, Toleranz.<br><br> 



<center><b>kreativ – persönlich – spielerisch – mit allen Sinnen</b></center><br>

Lebenskunde ist freiwillig und es gibt keine Noten. Ihr seid herzlich eingeladen, euch hier einzumischen und wohlzufühlen.<br>
Mit unseren Themen setzen wir uns möglichst kreativ auseinander (siehe unten: Schülermeinungen). 
So sind etwa Rollenspiele und Improvisationsübungen gut geeignet, sich einem Thema mit allen Sinnen zu
 nähern, dabei aufeinander zu achten, sich auszuprobieren und selbst zu entdecken. 
 Außerdem macht's Spaß.<br> 
Durch die im Vergleich zu den Klassen recht kleinen Gruppen können wir in angenehmer Atmosphäre sehr 
offen und persönlich werden.
Die allermeisten empfinden unsere Stunden als Bereicherung für den (Schul-)alltag und ihr Leben.<br>
