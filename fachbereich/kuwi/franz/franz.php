      <div class="teaser"><img style="border: 2px solid ; width: 100px; height: 80px; float: left; margin-right:20px;" alt="" src="fachbereich/kuwi/franz/frankreich.jpg"><b style=""> Warum Franz&ouml;sisch lernen?<o:p></o:p></b>

      
      <p class="MsoNormal"><span style=""></span>In einer Welt, die
immer mehr zusammenw&auml;chst, ist es ein enormer Vorteil, die Sprachen der
Nachbarl&auml;nder zu verstehen und sprechen zu k&ouml;nnen. Wer in Europa seinen Weg
gehen will, braucht die Sprachen der europ&auml;ischen Partner.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>In vielen
europ&auml;ischen L&auml;ndern sprechen die Menschen romanische Sprachen, die alle aus
dem Lateinischen hervorgegangen sind. Wenn man Franz&ouml;sisch spricht, wird es
einem viel leichter fallen, sp&auml;ter weitere romanische Sprachen (Spanisch,
Italienisch &hellip;) zu lernen, da gro&szlig;e Teile der Grammatik und des Wortschatzes
&auml;hnlich sind. </p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>Sicherlich sind Englischkenntnisse
heute unverzichtbar, aber sie reichen allein oft nicht mehr aus. Wer sp&auml;ter
einen Beruf ergreifen m&ouml;chte, in dem man international t&auml;tig ist, wird schnell
merken, dass Franz&ouml;sischkenntnisse die Berufschancen deutlich erh&ouml;hen. In der
Arbeitswelt genie&szlig;t Englisch die h&ouml;chste Nachfrage, gefolgt von Franz&ouml;sisch &ndash;
noch vor Spanisch und Italienisch. Franz&ouml;sischkenntnisse k&ouml;nnen in vielen
Berufen von Nutzen sein, z.B. als Journalist(in), Ingenieur(in), im
kaufm&auml;nnischen Bereich, als Manager(in), Lehrer(in), Historiker(in), als Arzt
oder &Auml;rztin usw.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>Man sollte auch
wissen, dass Frankreich der wichtigste Handelspartner Deutschlands ist: Das
deutsch-franz&ouml;sische Handelsvolumen ist gr&ouml;&szlig;er als das zwischen Deutschland und
den USA.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>Auch historisch
ist die Geschichte Deutschlands mit der unseres Nachbarlandes Frankreichs aufs
Engste verbunden. Frankreich gilt als ein Land von gro&szlig;er kultureller
Bedeutung. So kann Franz&ouml;sischlernen dazu beitragen, eine tiefe kulturelle
Allgemeinbildung zu erlangen.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;</span><span style="">&nbsp; </span>Man braucht nur einige Stunden, um von
Berlin aus nach Belgien, in die Schweiz oder nach Frankreich zu gelangen, wo
man seine Sprachkenntnisse anwenden kann. Auch sind diese L&auml;nder attraktive
Urlaubsl&auml;nder; es gibt abwechslungsreiche Landschaften mit Bergen und Meer
zwischen Mittelmeer und Atlantik.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>In den letzten
Jahren gibt es verst&auml;rkt Angebote &uuml;ber integrierte Studieng&auml;nge
(Ingenieurswissenschaften, Rechtswissenschaften, Kunst &hellip;), die in beiden
L&auml;ndern stattfinden. Auch mit dieser Zusammenarbeit zwischen Deutschland und
Frankreich im Bildungsbereich kann man seine Berufschancen deutlich erh&ouml;hen.</p>



      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>Franz&ouml;sisch ist
nicht nur in Europa von Bedeutung, sondern es ist Amtssprache in 35 L&auml;ndern
dieser Welt. So kann man sich z.B. in einigen L&auml;ndern in Afrika mit Franz&ouml;sisch
problemlos verst&auml;ndigen.</p>


      
      <p class="MsoNormal"><span style="">&nbsp;&nbsp; </span>Nicht zuletzt
m&ouml;chten wir darauf hinweisen, dass einem das Erlernen einer Fremdsprache im
Leben nie wieder so leicht gemacht wird wie in der Schulzeit, wenn die
Sch&uuml;ler/innnen &uuml;ber mehrere Jahre systematisch von ausgebildeten Sprachlehrern
begleitet werden. </p>


