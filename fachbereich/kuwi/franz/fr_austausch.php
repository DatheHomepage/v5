&Eacute;change Narbonne - Berlin 2009<br>

      <div class="teaser"><br>

Vom 24. Februar bis
zum 3. M&auml;rz fand unsere Frankreichreise
2009 im Rahmen des Sch&uuml;leraustauschs statt. Am 24., Dienstag,
gegen 10:00 Uhr
ging unser Flug mit einigen Stunden Versp&auml;tung Richtung
Barcelona. Von dort
sollte unser Bus nach Narbonne fahren. In Barcelona angekommen, hatten
wir zwei
Stunden Zeit, um uns die Stadt anzusehen. Gegen Abend kamen wir am
Lyc&eacute;e
Docteur Lacroix Narbonne an. Wir wurden von den Eltern unserer
Austauschpartner
empfangen und verbrachten den Abend bei den Gastfamilien.
      <p class="MsoNormal">Mittwoch morgen
versammelten wir uns im sogenannten
&bdquo;Aquarium&ldquo; des Lyc&eacute;e, um den Tagesplan
zu besprechen. Mit zwei Lehrern der
franz&ouml;sischen Schule hatten wir vor, die Stadt zu erkunden.
Die F&uuml;hrung gab
Einblick in das Leben der franz&ouml;sischen
Austauschsch&uuml;ler. Wir besichtigten die
Markthalle und eine &ouml;rtliche
&bdquo;Cath&eacute;drale&ldquo;. Den freien Nachmittag
verbrachtren
wir am Ufer des &bdquo;Canale de la Robine&ldquo; in Narbonnes
Innenstadt und am
Marktplatz. Dort wurden wir f&uuml;r die st&auml;dtische
Zeitung fotografiert, als wir
ein r&ouml;misches Denkmal, die &bdquo;Via Domitia&ldquo;,
besichtigten.</p>

      <p class="MsoNormal">Den
n&auml;chsten Tag gingen wir auf den Wochenmarkt, um
franz&ouml;sische kulinarische Spezialit&auml;ten zu kosten. Am
Nachmittag folgten wir
unseren Austauschpartnern in den Unterricht. </p>

      <p class="MsoNormal">Freitag fuhren wir
von der Schule mit einem Bus zur &bdquo;Grotte
de Limousis&ldquo;, einer Tropfsteinh&ouml;hle, etwas
au&szlig;erhalb von Narbonne. Nachdem wir
eine Stunde auf den Kollegen, der das Tor aufschlie&szlig;en
sollte, gewartet hatten,
genossen wir die F&uuml;hrung. Wir wurden durch die Grotte geleitet
und &uuml;ber ihre
Entstehung informiert. Die Lichtshow zu Ende der F&uuml;hrung war
ein echtes
&bdquo;Spectacle&ldquo;. F&uuml;r Freitag stand
au&szlig;erdem noch die Wanderung auf einen Berg an,
auf dem eine Schlossruine stand. Dort, auf
&bdquo;Ch&acirc;teaux Cathares&ldquo; machten wir
unsere Mittagspause. Nachmittags fuhren wir mit dem Bus weiter nach
Carcasonne,
um seine ber&uuml;hmte Altstadt zu bewundern. Diesen Ausflug
machten wir ohne unsere
Gastgeber, aber wir versuchten dennoch, unser bestes
Franz&ouml;sisch zu sprechen.<br>

      <o:p></o:p></p>

      <p class="MsoNormal">Das Wochenende
diente dem besseren Kennenlernen unter uns
Deutschen und den Gastfamilien. Einige blieben in Narbonne, andere
fuhren ans
Meer oder in die Berge.<br>

      <o:p></o:p></p>

      <p class="MsoNormal">Als wir uns am
Montag wiedertrafen, tauschten wir uns &uuml;ber
die Erlebnisse aus. F&uuml;r diesen Tag war eine Tour zusammen mit
den Franzosen in
das &bdquo;Amphoralis&ldquo;-Museum geplant. Dort gab es
antikes r&ouml;misches T&ouml;pferwerk aus
der Region zu sehen. Unsere Gruppe bekam einen franz&ouml;sischen
Fragebogen und
musste diesen mit Hilfe der Narbonner beantworten. Die Fragen drehten
sich um
Informationen aus dem Museum. Am Mittag besichtigten wir ein Weingut
namens
&bdquo;Terra Vinea&ldquo;. Besonders die F&uuml;hrung durch
die G&auml;nge und den riesigen
Weinkeller waren beeindruckend. </p>

      <p class="MsoNormal">Am Dienstag war Tag
der Abreise. Um 14:00 Uhr wurden wir von
unseren Gastsch&uuml;lern des Lyc&eacute;e verabschiedet und
fuhren dann mit dem Bus zur&uuml;ck
nach Barcelona. Nachts kamen wir ersch&ouml;pft aber
gl&uuml;cklich &uuml;ber die
erfahrungsreiche Reise wieder in Berlin an.</p>

