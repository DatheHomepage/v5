<b>Stundentafel Klassen 5-10</b><br>
Die Stundentafel weist für die Klassenstufen 5-7 jeweils zwei Wochenstunden Musik aus. In den Schnelllernerklassen 7 wird 
nur eine Wochenstunde Musik unterrichtet, denn hier findet zusätzlich ein fächerübergreifender zweistündiger Enrichmentkurs 
„Musik und Bewegung“ statt.
In der 8. Klasse sind es 1,5 Stunden, die epochal unterrichtet werden.
In der 9. und 10. Klasse findet Musik ebenfalls epochal statt; das bedeutet, dass der Unterricht 
in jeder Klasse ein Halbjahr lang mit je zwei Wochenstunden erfolgt und somit über das Jahr gerechnet einstündig ist.
<br>
<br>
<br>
<b>Wahlpflicht</b><br>
In Klasse 8 kann das Wahlpflichtangebot „Kulturwissenschaften“ (fächerübergreifend Musik, Kunst, Deutsch) 
mit drei Stunden in der Woche genutzt werden.
In Klasse 9 bzw. 10 können sich die Schüler*innen zusätzlich für Musik als Wahlpflichtkurs (zwei Stunden pro Woche) entscheiden. 
<br>
<br>
<br>


<b>Oberstufe</b><br>
In der Sekundarstufe II besteht die Möglichkeit, entweder einen Grundkurs mit 3 Wochenstunden oder einen Leistungskurs mit 5 Wochenstunden 
im Fach Musik zu wählen.
Hier wird ein besonderes Interesse an Musik vorausgesetzt. Das Beherrschen eines Instrumentes ist zwar wünschenswert, aber keine Bedingung.

Die durch den Rahmenlehrplan festgelegten Themen der Oberstufe sind:
1. Semester: Musik im Wandel der Zeit
2. Semester: Musik im gesellschaftlichen Kontext
3. Semester: Musik in Verbindung mit anderen Künsten und Ausdrucksformen
4. Semester: Musik verschiedener Kulturen


Musizierfreudige OberstufenschülerInnen können einen Zusatzkurs (Musik-Ensemblekurs) mit drei Wochenstunden belegen.
 Hier werden sowohl ein Chorkurs als auch ein Instrumental-Ensemblekurs angeboten. 
<br>
<br>
<br>
<b>Abitur</b><br>
Das Fach Musik kann aus dem Grundkurs heraus sowohl als 3. Prüfungsfach (schriftlich) als auch als 4. Prüfungsfach (mündlich) gewählt werden.
Zudem besteht die Möglichkeit, Musik als 5. Prüfungskomponente (Präsentationsprüfung oder eine schriftliche Hausarbeit / BLL) zu wählen.
