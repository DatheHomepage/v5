<b>Allgemeines</b><br>
Am Dathe-Gymnasium unterrichten momentan vier Musiklehrerinnen (Frau Beiküfner, Frau Bernhagen, Frau Grohé und Frau Jaecks), 
ein Musiklehrer (Herr Zauber) und eine Referendarin (Frau Lüdde).
<br><br>
Ansprechpartnerin für den Fachbereich Musik: Frau Jaecks
fb.musik@dathe-oberschule.cidsnet.de 
<br><br>
Im Unterricht wird viel Wert auf Musikpraxis gelegt, d.h. neben einer musiktheoretischen 
Grundbildung wird regelmäßig getanzt, gesungen und auf dem in der Schule vorhandenen Instrumentarium musiziert. 
Dabei wenden die Schüler*innen nicht nur Notenkenntnisse an, sondern lernen, selbstständig Musikstücke zu erarbeiten, 
gemeinsam zu proben und Ergebnisse zu präsentieren und anzuhören.<br> 
Unser Ziel ist es, die Schüler*innen für die vielfältigen Ausprägungen von Musik zu interessieren,
Vorurteile abzubauen und Freude am gemeinsamen Musizieren und Musikhören zu entwickeln.
<br><br>


<b>Konzerte</b><br>
An den regelmäßig stattfindenden Sommer- und Weihnachtskonzerten nehmen stets viele Schüler*innen teil: 
im Klassenverband, in Bands, Ensembles, AGs und mit Einzelbeiträgen, z.B. von Instrumental-schüler*innen.
Die Mitwirkenden werden dann bei einem Casting von Lehrer*innen und Oberstufenschüler*innen ausgewählt. <br>
