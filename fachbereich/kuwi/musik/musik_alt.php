<b>Allgemeines</b><br>
<br>
Es gibt zurzeit vier Musiklehrerinnen und einen Musiklehrer, die in zwei Fachräumen unterrichten:<br>
Frau Bernhagen, Frau Grohé <a href="http://www.mgrohee.de" target="_blank">(www.mgrohee.de)</a>, Frau Jaecks, Frau Schubert, Herr Zauber<br>
<br>
Ansprechpartnerin für den Fachbereich Musik: Frau Jaecks<br>
fb.musik@dathe-gymnasium.cidsnet.de <br>
<br>
Im Unterricht wird viel Wert auf Musikpraxis gelegt, d.h. neben einer musiktheoretischen Grundbildung wird regelmäßig gesungen, 
getanzt und auf dem in der Schule vorhandenen Instrumentarium gespielt. Dabei wenden die Schülerinnen und Schüler nicht nur Notenkenntnisse an, 
sondern lernen, selbständig Musikstücke zu erarbeiten, gemeinsam zu proben und Ergebnisse zu präsentieren und anzuhören.<br> 
Unser Ziel ist es, die Schülerinnen und Schüler für die vielfältigen Ausprägungen von Musik zu öffnen, Vorurteile abzubauen und Freude am 
gemeinsamen Musizieren und Musikhören zu bewirken.<br>
<br>
<b>Konzerte</b><br>
<br>
An den regelmäßig stattfindenden Sommer- und Weihnachtskonzerten nehmen stets viele Schülerinnen und Schüler teil: im Klassenverband, 
in Bands, Ensembles, AGs und mit Einzelbeiträgen, z.B. von InstrumentalschülerInnen. Die Mitwirkenden werden bei einem Casting von LehrerInnen 
und OberstufenschülerInnen ausgewählt.<br>
<br>
<b>Stundentafel Klassen 5-10</b><br>
<br>
Die Stundentafel weist für die Klassenstufen 5-7 jeweils zwei Wochenstunden Musik aus, in der 8. Klasse sind es 1,5 Stunden. 
In der 9. und 10. Klasse findet Musik epochal statt, das bedeutet, dass der Unterricht in jeder Klasse ein Halbjahr lang mit je 
zwei Wochenstunden erfolgt.<br>
<br>
Im Rahmen des Enrichmentangebots für Schnelllerner findet in Klasse 7 der Kurs „Musik und Bewegung“ zweistündig statt.<br>
In Klasse 8 kann das Wahlpflichtangebot „Kulturwissenschaften“ (fächerübergreifend Musik, Kunst, Deutsch) mit drei Stunden in der Woche 
genutzt werden.<br>
In Klasse 9 bzw. 10 besteht die Möglichkeit, sich zusätzlich für Musik als Wahlpflichtkurs (zwei Stunden pro Woche) zu entscheiden.<br>
<br>
<b>Oberstufe</b><br>
<br>
In der Sekundarstufe II besteht die Möglichkeit, entweder einen Leistungskurs (5 Wochenstunden) oder einen Grundkurs (3 Wochenstunden) 
im Fach Musik zu wählen.<br>
Ein besonderes Interesse an verschiedenen Musikformen wird vorausgesetzt. Das Beherrschen eines Instrumentes ist zwar wünschenswert, aber keine 
Bedingung.<br>
Die durch den Rahmenlehrplan festgelegten Themen der Oberstufe sind:<br>
1. Semester: Musik im Wandel der Zeit<br>
2. Semester: Musik im gesellschaftlichen Kontext<br>
3. Semester: Musik in Verbindung mit anderen Künsten und Ausdrucksformen<br>
4. Semester: Musik verschiedener Kulturen<br>
<br>
Musizierfreudige OberstufenschülerInnen können einen Zusatzkurs belegen, und zwar Chor oder Ensemblespiel.<br>
