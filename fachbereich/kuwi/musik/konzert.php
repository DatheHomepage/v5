<b>Sommerkonzert 2016 </b><br><br>
Die diesjährigen Sommerkonzerte haben uns bei sehr sommerlichen Temperaturen ein abwechslungsreiches, kurzweiliges, 
und vielfältiges Programm geboten. <br>
Neben den musikalischen Individualbeiträgen, den Beiträgen von Chor, Wahlpflicht und Leistungskurs, 
bereichern zunehmend Beiträge aus dem Enrichment- und Ganztagsangebot die kulturelle Vielfalt mit Tanz und Akrobatik. <br>
Vielen Dank an alle mutigen Künstler/innen, das Technik-Team, den FB Kunst für die Ausgestaltung 
(die im Übrigen in gezielt gesetzter Graffiti-Form auch auf der Fassade Form annimmt) und natürlich an die Musik-Lehrkräfte für 
die Umsetzung dieser wichtigen Schultradition gerade in den Zeiten der letzten Schulwochen. <br><br><br>


