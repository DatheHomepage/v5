      <br>

      </b> Hello,<br>

      <br>

I'm Lisa from Dathe School in Berlin. The name "Dathe" comes from the
former director of the berlin zoo. Prof. Dr. Dr. Dathe worked there
until 1989. Our school is a grammar school (Gymnasium) in
Friedrichshain with about 750 pupils, from class 5, and I have just
started learning English. I like my school because it has something
special: many animals, yes, living animals. <br>

      <br>

This biology station as we call it has got lots of animals, for
example: rabbits, hamsters, guinea pigs, mice, fish and many more
animals (we can't translate all names into English because some of the
animals have crazy names). Our school is a very, very good school. I
like it. Our teachers are nice. I like all subjects except for P.E. In
our school we can learn English, French and Latin as foreign languages.
In our assembly hall (we call it "Aula") we can eat our lunch. After
the lessons we can go to music clubs (we can play the drums for
example), play soccer (the girls, too!) and we can work in our biology
station. We clean the animals' cages and watch what the animals are
doing.<br>

      <br>

Besides, there are many events in our school, for example the spring
concert in April where the pupils make their own programme. - Of
course, the music teachers help us a bit! <br>

      <br>

Are you interested in visiting our school? You are very welcome! <br>

      <br>

Yours, Lisa.<br>

