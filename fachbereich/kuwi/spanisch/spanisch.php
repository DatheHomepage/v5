Warum Spanisch?<br>

Neben Chinesisch (Mandarin) und Englisch ist Spanisch die Sprache, die
in den meisten L&auml;ndern und von den meisten Menschen gesprochen
wird.<br>

Die spanische Sprache er&ouml;ffnet den Zugang nicht nur zu Spanien,
sondern zu weiten Teilen Lateinamerikas. Selbst in den USA wird in
vielen Gegenden schon fast so viel Spanisch wie Englisch gesprochen.<br>

F&uuml;r die, die Spanisch als dritte Fremdsprache w&auml;hlen und
deshalb schon etwas Franz&ouml;sisch gelernt haben, sind die
Anfangsbegriffe nicht schwer, weil ja beide Sprachen vom Lateinischen
abstammen. Deshalb kann man in der dritten Fremdsprache recht schnell
vorankommen.<br>

Das Wichtigste im Sprachunterricht ist es, die Sprache sprechen zu
lernen. Um Spanisch tats&auml;chlich zu benutzen, muss man gar nicht
immer nach Spanien oder Lateinamerika fahren. Es gibt immer mehr
spanischsprachige Menschen, die in Berlin leben oder als Touristen
hierher kommen. Die freuen sich, wenn auch die Berliner ein bisschen
Spanisch sprechen.<br>

Und nat&uuml;rlich m&ouml;chte man auch die Liedtexte von Shakira, Juanes und all den anderen verstehen k&ouml;nnen.<br>

Wir arbeiten mit einem Lehrbuch, aber auch mit anderen Materialien. Im
Spanischunterricht kann man zum Beispiel lernen ein Referat auf
Spanisch zu halten, E-Mails oder Weihnachtsgr&uuml;&szlig;e zu
schreiben, ein Kochrezept zu erkl&auml;ren (und das Gericht dann auch
tats&auml;chlich zuzubereiten) oder eine Reise ins spanischsprachige
Ausland zu planen.<br>

Aber um das zu k&ouml;nnen, muss man nat&uuml;rlich auch Grammatik und
Vokabeln lernen, Tests schreiben und Klassenarbeiten &ndash; all das,
was in der Schule nun mal dazugeh&ouml;rt.<br>

Spanisch als zweite Fremdsprache muss man mindestens bis zur 10. Klasse belegen.<br>

Die dritte Fremdsprache Spanisch w&auml;hlt man f&uuml;r mindestens
zwei Jahre. In der 10. Klasse kann man sich entscheiden, ob man
weitermachen oder lieber ein anderes Wahlfach belegen will.<br>

Auch in der Qualifikationsphase wird Spanisch im Kursangebot weiter
gef&uuml;hrt und kann auch als m&uuml;ndliches oder schriftliches
Abiturfach oder als f&uuml;nfte Pr&uuml;fungskomponente (Besondere
Lernleistung oder Pr&auml;sentationspr&uuml;fung) gew&auml;hlt werden.<br>

