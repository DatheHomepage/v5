Der ist schwarz, der ist wei&szlig;,<br>

der ist dumm, der ist schlau<br>

und zum Schluss alles umgekehrt<br>

einfach ist es, schnell geht es.<br>

Wenn du rennst<br>

verpasst du es<br>

            <br>

Janja
Wils, 10 c <br>

