Gro&szlig;stadt<br>

            </p>

            <div style="text-align: left;" class="intro"><span class="teaser">Dreckig prasselt ignoranter Regen<br>

Und verhallt Gel&auml;chter in der Kneipe<br>

Gro&szlig;stadtmenschen so sentimental wie Tiefk&uuml;hlpizzen<br>

In Flammen verschwinden sie<br>

Und tauchen aus der Asche wieder auf.<br>

Geschmack der Asche<br>

Geschmack des Alkohols<br>

Zu viel davon geschmeckt, Leber strapaziert.<br>

Schritte wandeln vom Stechschritt zum Tanzen<br>

Schopenhauerpessimistisches Tanzen<br>

            <br>

Dorian Beckmannshagen, 10d<br>

            <br>

            <br>

Tacheles<br>

            <br>

Ich werde gegr&uuml;&szlig;t aus zahnlosem Mund<br>

vom K&uuml;nstler mit gestrandeter Existenz<br>

mit ehrhaften Werten, denn<br>

Kunst ist nicht kaufbar<br>

Brot kostet und Hunger schmerzt.<br>

            <br>

Eisiger Wind weht in bespr&uuml;hten R&auml;umen<br>

Bringt den Geruch von Bier und Verfall<br>

Eine Frau sitzt da mit ger&ouml;teten Augen<br>

benebelt, schon in einer anderen Realit&auml;t<br>

Nur k&uuml;nstlerische Freiheit ist das<br>

zur&uuml;ckkehrt der Hunger und Hunger schmerzt.<br>

            <br>

Kurz sehn ich mich nach diesem Dasein<br>

Dann geh ich hinaus<br>

wehm&uuml;tig zwischen d&uuml;steren Eisenskulpturen<br>

zur&uuml;ck aus der anderen Welt<br>

Hier gibt es keinen Hunger, denn Hunger schmerzt.<br>

            <br>

Valentin
De Negri Fischer, 10b <br>

            <br>

            <br>

Die
Kommode<br>

            <br>

Nimm die L&auml;den beispielsweise<br>

geordnet, perfekt<br>

