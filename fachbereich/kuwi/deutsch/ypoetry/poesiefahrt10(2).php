Die Stadt, die Stadt der Tr&auml;ume,<br>

Ich gehe hier auf meinem Weg,<br>

der zu meinen Tr&auml;umen f&uuml;hrt,<br>

Die Liebe gibt mir die Kraft,<br>

Einsamkeit und Entt&auml;uschung kenn ich nicht<br>

Da ist der Zug, der Zug der Tr&auml;ume<br>

Ich steige ein, sehe das Licht <br>

Ich bin nicht alleine, Freunde sind da<br>

Wir l&auml;cheln<br>

Wir tanzen<br>

Wir schaffen das.<br>

