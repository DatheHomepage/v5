Den Dam <br>

            <br>

habitat in mijn stad <br>

beantwoordt aan al mijn behoeften, <br>

een alles overtreffende symfonie.<br>

            <br>

Golven, bochten, krommingen, koepels <br>

geen enkele functie kan het beschrijven, <br>

wiskunde schiet tekort, en wordt overwonnen.<br>

            <br>

Botten breken, hout versplintert, geluksreceptoren <br>

draaien op volle toeren, <br>

als een vogelvrij kind ontdek ik alles opnieuw <br>

biologie schiet tekort, en wordt overwonnen. <br>

            <br>

Snelheid, spanning, druk, valbeweging<br>

er kann geen formule gevonden worden,<br>

fysica schiet tekort, en wordt overwonnen.<br>

            <br>

Slechts &eacute;&eacute;n oase in mijn stad, <br>

kraait victorie over de takken van wetenschap.<br>

De realiteit is van geen belang,<br>

het onmogelijke is mogelijk.<br>

            <br>

            <br>

Der Damm<br>

            <br>

Ein einziges
Biotop in meiner Stadt<br>

entspricht all meinen W&uuml;nschen<br>

eine alles &uuml;bertreffende
Symphonie.<br>

            <br>

Wellen, Kurven, Kr&uuml;mmungen, Kuppeln<br>

Keine einzige Funktion kann es beschreiben,<br>

Mathematik reicht nicht aus und wird besiegt.<br>

            <br>

Knochen brechen, Holz zersplittert, Gl&uuml;cksrezeptoren<br>

laufen auf vollen Touren<br>

Wie ein vogelfreies Kind entdecke ich alles neu<br>

Biologie reicht nicht aus und wird besiegt.<br>

            <br>

Schnelligkeit, Spannung, Druck, Fallbewegung<br>

es kann keine Formel gefunden werden,<br>

Physik reicht nicht aus und wird besiegt.<br>

            <br>

Nichts als eine Oase in meiner Stadt,<br>

Kr&auml;ht Victoria &uuml;ber die Zweige der Wissenschaft.<br>

Die Realit&auml;t ist unwichtig,<br>

Das Unm&ouml;gliche ist m&ouml;glich.<br>

            <br>

Yoni Sel<br>

(Lyceum Linkeroever, Antwerpen) <br>

