            <p class="title-ressort" align="center">-
Die Projektfahrt nach Amsterdam-</p>

            <p class="intro" align="left">Vom
16.04. bis zum 19.04.2009 weilten neun Sch&uuml;ler und
Sch&uuml;lerinnen unserer Schule als Finalisten des internationalen
Poesie-Wettbewerbs in Amsterdam. Zum Finale traten 36 Jugendliche aus
Br&uuml;ssel, Antwerpen, Amsterdam und Berlin mit ihrem Gedicht zum
Thema "Junge Leute und die Stadt" vor die Jury.<br>

            <br>

Am Festival der Poesie nahmen Teil:<br>

Hoang, 10a<br>

Lennart, 10a<br>

Mohamed, 10a<br>

Cora, 10b<br>

Hoang, 10b<br>

Dimitri, 10c<br>

Ronja, 10d<br>

Florian, 10d<br>

Georg, 10d<br>

            <br>

Voller Freude geben wir bekannt, dass Cora L. (10b) eine Anerkennung
(Urkunde) erhalten hat. Ronja W. (10d) gewann den 3. Preis (Pokal). Der
1. Preis ging an eine Sch&uuml;lerin aus Amsterdam.</p>

