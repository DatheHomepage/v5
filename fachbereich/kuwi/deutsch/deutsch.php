Deutsch wird am Dathe-Gymnasium von der Klasse 5
(Schnelllernerklasse) und von der Klasse 7 an mit vier
Wochenstunden unterrichtet. In den Klassen 9/10/11 sieht die
Stundentafel drei Deutschstunden vor. Die Arbeitsgrundlagen
f&uuml;r das Fach sind sowohl moderne Lehrb&uuml;cher als auch
Lekt&uuml;ren, die die Lesekompetenz und die Sprach- und
Schreibkompetenz der Sch&uuml;ler f&ouml;rdern. <br>
