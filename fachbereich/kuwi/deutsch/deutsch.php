Die Biologiestation
unserer Schule wird vor allem im Bereich der Sekundarstufe I genutzt,
um Anschaulichkeit im Deutschunterricht zu garantieren. Die
Sch&uuml;ler und Sch&uuml;lerinnen k&ouml;nnen so bei
Tierbeschreibungen (Aufsatzform) direkt in der Station arbeiten und
sich Tiere aussuchen, die sie beschreiben k&ouml;nnen.

<br><br>
Deutsch wird am Dathe-Gymnasium zudem in der 9. Klasse als
Wahlpflicht-fach angeboten, um den Sch&uuml;lern, die Deutsch als
Neigungsfach zus&auml;tzlich zur Stundentafel w&auml;hlen, die
M&ouml;glichkeit zu kreativen Schreibarbeiten und freieren
Unterrichtsformen zu geben.
