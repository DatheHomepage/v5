&nbsp;Morris Gleitzmann
kommt aus Australien und ist ein international mehrfach ausgezeichneter
Autor von Jugendb&uuml;chern. Sein Jugendbuch
&bdquo;Then&ldquo; - &uuml;bersetzt einfach
&bdquo;Dann&ldquo; - thematisiert die Flucht zweier
j&uuml;discher Kinder aus einem Konzentrationslager in Polen.
Inspiriert wurde Morris Gleitzman von der Geschichte des polnischen
Arztes Janusz Korczak, der bei dem Versuch, j&uuml;dische Kinder zu
retten, sein Leben opferte.<br>

Die Sch&uuml;ler der Klasser 8c hatten im Rahmen des
Deutschunterrichts einen Buchdeckel entworfen, den sie zur Lesung Herrn
Gleitzmann &uuml;bergaben. <br>

