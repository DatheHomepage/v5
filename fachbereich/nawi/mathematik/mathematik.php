In der letzten Schulwoche nahmen 17 Schülerinnen und Schüler aus den Klassen 5, 7 und 8 am Berliner Matheathlon 2015 teil. <br>
Während eines Laufs über 400 bzw. 800 m hatten sie an drei Stationen mathematische Aufgaben zu lösen.<br>
