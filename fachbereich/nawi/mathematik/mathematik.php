<br><b>Tag der Mathematik 2017 </b><br><br>

Überraschend groß war an unserer Schule in diesem Jahr das Interesse für den Tag der Mathematik der Berliner Hochschulen und Universitäten am Samstag, dem 22. April. Insgesamt 40 Schülerinnen und Schüler aus den Klassenstufen 7, 8, 9 und der Oberstufe beteiligten sich in zehn verschiedenen Teams am Mannschaftswettbewerb. Traditionell nutzten auch die Leistungskurse Mathematik (2. Semester) die angebotenen Vorträge, um noch mehr über spezielle Anwendungsbereiche und die Vernetzung der Mathematik mit anderen Fächern zu erfahren.
Besonders erfolgreich waren die Teams aus der Klasse 7a (Platz 12 von 81 Mannschaften in Klassenstufe 7/8) und der Klasse 9a (Platz 9 von 68 Mannschaften in Klassenstufe 9/10). (Sh)

<br><br>
<hr class="line">
<br><b>	Känguru-Wettbewerb 2017</b><br><br>

Beim diesjährigen Wettbewerb beteiligten sich insgesamt 167 Schülerinnen und Schüler aus 17 Klassen. Nachdem nun die bundesweite Auswertung erfolgte, wissen wir, dass 13 der Beteiligten von unserer Schule zu den Preisträgern gehören. Fünf 1. Preise, vier 2. Preise und vier 3. Preise gingen an unsere Schule. Außerdem erhalten alle Teilnehmer eine Teilnahme-Urkunde mit der erreichten Punktzahl und einen kleinen Teilnehmerpreis.  <br>

