<br><b>	Mathe-Olympiade 2016</b><br><br>

Im November nahmen 20 Schülerinnen und Schüler unserer Schule an der zweiten Stufe der 
Mathematik-Olympiade teil und stellten sich im Wettbewerb mit anderen Teilnehmern aus 
Friedrichshain-Kreuzberg der mathematischen Herausforderung.<br>
Auch in diesem Jahr gehören wieder einige von ihnen zu den Preisträgern. <br>
Sie sind zugleich nominiert für die Landesolympiade Mathematik, die an zwei Tagen Ende Februar 
ausgetragen wird. Herzlichen Glückwunsch und weiter viel Erfolg!<br><br>
