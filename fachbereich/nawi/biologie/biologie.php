      <p><b>Biologie ist ein sehr umfassendes Unterrichtsfach</b><br>

     


Biologie ist ein sehr umfassendes Unterrichtsfach, weil sie am Ende der
technischen Revolution in allen Bereichen der Wirtschaft an Bedeutung
gewonnen hat: Landwirtschaft und Ern&auml;hrung, Kosmetika, Medizin
und dabei ganz besonders Bekämpfung von Seuchen, Technischer
Umweltschutz (z.B. Klimakatastrophe, Wald- und Artensterben) und ganz
aktuell Gentechnologie.<br>



      <br>



Das Unterrichtsfach Biologie soll deshalb die Schüler/innen in die Lage versetzen, in Situationen des privaten und
öffentlichen Lebens, in denen biologisches Verständnis und problemlösendes Denken erforderlich
sind, sachbezogen und verantwortungsbewusst zu entscheiden und zu handeln. Hierzu gehört auch, dass der Biologieunterricht die
Schüler/innen zu einem wissenschaftlich fundierten Welt- und Selbstverständnis führt.&nbsp;<br>



      <br>



      <b>So gilt es Verständnis und Verantwortungsbereitschaft für den Schutz der natürlichen Lebensgrundlagen, den Tier- und Naturschutz und
die Gesundheit des Menschen zu fördern</b>.<br>



      <br>



Deshalb ist es ein zentrales Anliegen des Dathe-Gymnasiums, junge Menschen für die Bewahrung der Lebensgrundlagen auf der Erde
zu motivieren und damit zusammenhängend die Erziehung zur Toleranz in allen Teilbereichen des schulischen und
außerschulischen Lebens.<br>



      <br>



Mit diesen Aufgaben und Inhalten kommt dem Unterrichtsfach Biologie im
Rahmen des Bildungsauftrags der Schule für fächerübergreifende Erziehung, wie z.B. Umwelt-, Gesundheits- und Sexualerziehung, eine zentrale Rolle zu.<br>



      <br>



Die rasante Entwicklung der Biologie im vergangenen Jahrhundert brachte es mit sich, dass Dinge Allgemeingut wurden, die 10 Jahre
früher noch nicht einmal Fachwissenschaftler für möglich hielten. Diese Entwicklung wird ganz wesentlich durch 
die Nutzung der Computertechnologien unterstützt (man spricht im Gegensatz zu den fr&üheren mit Wasser arbeitenden Forschungslaboren nun von 
den trockenen Laboren). </p>



      
      
