      <b>Methoden der Erkenntnisgewinnung</b><br>

      <br>

Das provoziert gleichzeitig die Erkenntnis, dass etwas, das momentan
Gültigkeit beansprucht, durchaus nur vorübergehend
Geltung hat und auch so gesehen und bewertet werden muss, weil es sich
später als überholt erweist.<br>

So ist es Aufgabe der Schule Biologie am besten durch
forschend-entdeckendes Lernen, exemplarisches Lernen und vernetztes
Lernen zu vermitteln. Es werden Analogien gebildet und
Zusammenhänge deutlich gemacht. Vernetztes Lernen verlangt,
von linearem Denken Abstand zu nehmen.<br>

      <br>

Aber im Unterrichtsfach Biologie erfolgt auch die reale Begegnung mit
der lebendigen Natur, besonders oft natürlich in der
Biologiestation. Die Begegnung mit den Pflanzen und Tieren,
insbesondere den Wirbel- und dabei den Säugetieren,
führt nicht nur zu<br>

      <br>

&middot; Erkenntnissen über biologische Zusammenhänge oder <br>

&middot; Fertigkeiten des Umgangs mit diesen Lebewesen und
Verfahren diese zu beobachten, sondern auch zu einer<br>

&middot; Emotionalen Begegnung mit Tieren und Naturerleben, deren
Wert nicht hoch genug eingeschätzt werden kann.<br>

      <br>

 Der bildende Wert wird deutlich, wenn man
das engagierte Stellungnehmen der Schüler/innen bei Fragen wie Kosmetika und Versuchstiere, Formen der
Landwirtschaft und artgerechte Tierhaltung, Schutz von Wald und Nordsee
oder der tropischen Regenwälder beobachtet.<br>

