	  <b>Fahrt der Deutsch und Bio Leistungskurse</b><br><br>
Am Ende des zweiten Halbjahres fuhren wir mit dem Deutsch und Bio LK endlich auf eine 
langersehnte zehntägige Kursfahrt nach Süditalien. <br>
Nach einem Tag, den wir komplett im Bus gefangen waren, sahen wir uns mit einigen Schülern als Guides Neapel 
an, wo wir auch unsere erste Nacht verbrachten.<br><br>

Am darauffolgenden Tag bestiegen wir den Vesuv und erkundeten die Ausgrabungen der antiken Stadt Pompeji. Am Nachmittag setzten wir dann
unsere Reise fort, um in der traumhaften Clubanlage L'Oasi del Faunmo in Casal Velino unsere verbleibenden Tage am Pool, den eingie von uns am
ersten Morgen bereits um 7Uhr das erste Mal genossen, und am Beachvolleyballfeld zu verbringen.<br><br>
<center><img src="./fachbereich/nawi/biologie/bilder/image9_klein.jpg">&nbsp;&nbsp;&nbsp;&nbsp;<img src="./fachbereich/nawi/biologie/bilder/image3_klein.jpg"></center><br>
Während die Deutschschüler die Spuren Goethes in Italien verfolgten, um seine Motivationen und Eigenarten nachzuvollziehen, informierten sich
die Biologieschüler über die vielfältigen Lebensräume im Süden Italiens. Dazu organisierten sowohl die Schüler, 
als auch der Club einige Vorträge, die durch 
Mikroskopieren, kreativem Schreiben und anderen praktischen Aktivitäten unterstützt wurden, wie beispielsweise 
Schnorchelausflüge sowohl Tags, als auch Nachts.
Eine willkomnmene Abwechslung zum Schulalltag! <br>
Während wir dort waren, stiegen die Temperaturen auf bis zu 37°C, doch nicht nur die Sonne, sondern auch die 
Vielzahl sportlicher Aktivitäten brachten 
uns ordentlich ins Schwitzen. Es wurde eine Wanderung durch die beeindruckende Natur an der Amalfiküste unternommen, bei der unsere Kondition 
getestet wurde, eine Kajakfahrt auf dem Fluss Alento durch Stromschnellen, Einengungen und gegen unsere Mitschüler und am letzten Tag ,machten
wir dann eine Fahrradtour durch den Cilentpo Nationalpark zum Strand, um uns vom Meer mit tosenden Wellen, die 
uns auch einige Male unter Wasser drückten, zu
verabschieden.<br><br>
<center><img src="./fachbereich/nawi/biologie/bilder/image1_klein.jpg"></center><br>
Doch bei all diesen anstrengenden wunderschönen Aktivitäten, beeindruckte uns am meisten das Schnorcheln, 
da es für viele das erste Mal war und wir die Meeresbewohner in ihrer ganzen Pracht einmal selbst 
beobachten konnten. Bei einer so tollen Fahrt blieb auch die Abschlussparty nicht aus, bei der letzendlich sowohl alle
 Schüler, als auch alle Lehrer im Pool landeten.<br><br>
Erschöpft und mit gebräunter Haut oder aber Sonnenbrand ging es dann am Samstag Mittag wieder mit dem Bus Richtung Heimat, in dem wir uns mit 
Musik und Spielen die Zeit vertrieben.<br><br>
- Mathilda & Vivien, Q2


   
