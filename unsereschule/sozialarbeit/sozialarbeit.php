<b>Rückblick: Einjähriges Jubiläum der Schulsozialarbeit und Frühlingsfest am Donnerstag den 6. April 2017</b>
<br>
<br>
Eine Vielzahl an Schüler*innen nahm an den Angeboten, wie Zirkusworkshop, Seifenblasen-Aktion und Fotobox teil. Zudem konnten sie sich über ein Freiwilliges Soziales Jahr und weitere Projekte des Trägers Technische Jugendfreizeit- und Bildungsgesellschaft (tjfbg), über die Ferienfahrten und Aktionen der Schülervertretung, sowie über Theaterprojekte an Schulen vom Verein Eukitea informieren. Viele engagierte Schüler*innen präsentierten ihre Projekte wie Hofverschönerung, Schulapp, Jahrbuch und Betreuung der Kaninchen der Schulsozialarbeit.
<br>
Ein großes Lob geht an die fleißigen Spender*innen, die Kuchen gebacken und es ermöglicht haben für die Ferienfahrt und für die Kaninchen Ramba und Zamba Geld zu sammeln.
Danke an alle engagierte Schüler*innen, die dieses Frühlingsfest mitgestalten haben.
<br>
Es war ein schönes Fest und wir freuen uns nächstes Schuljahr unser zweites Jubiläum zu feiern und bis dahin schöne Projekte mit den Schüler*innen zu gestalten.
<br>
Marielle Ziller und Mathilde Chevallier" 
