                <b>Schulprogramm der Dathe-OS</b>
                <br>Die 2. Fassung des Schulprogramms wurde nun durch die zuständige Schulaufsicht genehmigt. Damit
                findet ein 2,5-jähriger Prozess mit intensiver Arbeit und Diskussion aller an Schule Beteiligten
                seinen vorläufigen Abschluss.
                <br> Im Genehmigungsschreiben heißt es: " ... Das Programm überzeugt inhaltlich und formal
                uneingeschränkt. Besonders würdigen möchte ich die prägnanten Erläuterungen der
                Entwicklungsschwerpunkte. ... den aktiv Beteiligten ist eine persönliche, adressatenbezogene
                Darstellung gelungen, die den Stand und das Wohin der Schulgemeinschaft hervorragend verdeutlicht.
                ... Ich bedanke mich bei allen Beteiligten für die geleistete Arbeit und wünsche Kreativität, Erfolg
                und Kraft bei der täglichen pädagogischen Arbeit."
                <br> Diesem Dank schließe ich mich als Schulleiterin an und freue mich über die Veröffentlichung auf
                unserer Homepage.
