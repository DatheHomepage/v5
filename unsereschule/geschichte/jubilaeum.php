      <p>Am Freitag, dem 25.01.2008, fand in der unter der Regie
von Frau Dr.
Zanner festlich geschm&uuml;ckten Aula der Schule die
Festveranstaltung zum 50j&auml;hrigen Jubil&auml;um der
Schulbiologiestation statt.<br>

      </p>

Aus Platzgr&uuml;nden konnten nur 220 geladene G&auml;ste
anwesend sein, darunter u.a. auch der Bezirksb&uuml;rgermeister
Herr Dr. Schulz, der Gr&uuml;nder der Station Herr Vogler,
Angeh&ouml;rige von Prof. Dr. Dr. Dathe, viele
Sch&uuml;lerinnen und Sch&uuml;ler, Lehrerinnen und Lehrer
sowie Eltern der Schule.<br>

      <br>

Herr Noth f&uuml;hrte durch ein abwechslungsreiches Programm, in
dem die Tiere der Biostation eine Hauptrolle spielten.
H&ouml;hepunkte waren die Live-&Uuml;bertragung aus dem
Schabengehege, das Schneckenrennen der Achatschnecken sowie die
Kr&ouml;tenf&uuml;tterung. Sch&uuml;lerinnen und
Sch&uuml;ler der verschiedenen Tierpflege-Arbeitsgemeinschaften
stellten ihr Fachwissen unter Beweis.<br>

      <br>

Herr Vogler, Sebastian Zeh und Frau Weidemann erinnerten als Festredner
an die wechselvolle Geschichte der Biologiestation und die
Notwendigkeit ihrer Erhaltung. Sie dankten den zahlreichen
flei&szlig;igen Unterst&uuml;tzern und Helfern, ohne deren
engagierte Mitarbeit die Station nicht funktionieren kann. Der
B&uuml;rgermeister sicherte in seinem Gru&szlig;wort die
Schaffung einer festen Betreuungsstelle zur Tierpflege zu.<br>

      <br>

Im Rahmen der Veranstaltung wurde auch ein Kooperationsvertrag zwischen
der Blumberger M&uuml;hle und der Dathe-Oberschule geschlossen.
Kern der Vereinbarung ist eine jeweils dreit&auml;gige Exkursion
der 5.Klassen in das inmitten der Schorfheide gelegene NABU-Zentrum.
Unter der fachkundigen Anleitung von Rangern der Naturschutzwacht
besch&auml;ftigen sich die Kinder mit dem Thema
&bdquo;Nachhaltige Waldnutzung&ldquo;.<br>

     


