Unser Schulgeb&auml;ude wurde nach einem Entwurf von
Stadtbaudirektor Hoffmann in den Jahren 1907/08 als
Gemeindedoppelschule errichtet und im Jahre 1909 den
st&auml;dtischen Beh&ouml;rden &uuml;bergeben.<br>

Im Jahr 1957 wurde durch Herrn Vogler durch das Aufstellen und
Gestalten von zun&auml;chst einigen Aquarien im Keller des
Geb&auml;udes der Grundstein f&uuml;r die Schul-Biologiestation
gelegt.<br>

Die sind sicher zwei gute Gr&uuml;nde, sich dem Geb&auml;ude,
seinem Baumeister und der Stadtgeschichte in unterschiedlicher Weise zu
n&auml;hern. Es bietet sich die Gelegenheit, die Unterrichts- und
Erziehungsarbeit an unserer Schule praktisch, lebensnah und konkret zu
gestalten. Das im Schulprogramm fixierte Vorhaben, die
Schul-Biologiestation noch intensiver in den Unterrichtsalltag
einzubeziehen, kann mit Leben erf&uuml;llt werden.<br>

Es bietet sich an, die vielen traditionellen Veranstaltungen unserer
Schule mit neuen Vorhaben unter einem gemeinsamen Motto in das
Bewusstsein zu r&uuml;cken und die Bindung und Identifikation mit
der Schule bei allen Beteiligten zu st&auml;rken.<br>

