      <div class="title">-Warum wir weiter Dathe-Schule
bleiben -</div>


      <div class="teaser"><br>


      <br>


Soll die Dathe-Schule umbenannt werden? Das war im Jahr 2009 in der
Diskussion. Eine Politikerin von den Gr&uuml;nen war auf die
Information gesto&szlig;en, dass Prof. Dr. Dr. Heinrich Dathe mit
21 Jahren der NSDAP beigetreten war und setzte sich seitdem
f&uuml;r eine Diskussion &uuml;ber den Namen unserer Schule
ein.
Historiker, Weggef&auml;hrten, ein Sohn Dathes, Lehrerinnen und
Lehrer der Dathe-Schule, Eltern und Sch&uuml;lerinnen und
Sch&uuml;ler trafen sich daraufhin zur Diskussion des Namens im
Rahmen der Gespr&auml;chsrunde &bdquo;Dathe im
Dialog&ldquo; an unserer Schule. Letztendlich entschied die
Schulkonferenz, dass das Gymnasium seinen Namen behalten soll.
Wie denken wir Sch&uuml;lerinnen und Sch&uuml;ler der Klasse
10a dar&uuml;ber? Wie stehen wir zu Dathes
Vergangenheit und zu
einer Umbenennung unserer Schule?<br>


      <br>


Am 5.11.2010 fand aus Anlass des 100.Geburtages von Prof. Dr. Dr.
Heinrich Dathe der Dathe&ndash;Tag an unserer Schule statt: Die
Sch&uuml;lerinnen und Sch&uuml;ler besch&auml;ftigten sich
ausf&uuml;hrlich mit Dathes Leben, indem sie recherchierten und
eine Dokumentation &uuml;ber Dathes Leben schauten.
Zudem war J&uuml;rgen Mladek geladen, der aus seinem neuen Buch
&bdquo;Professor Dathe und seine Tiere&ldquo;, einer Biographie
Dathes, vorlas. So war das Hintergrundwissen vieler
Sch&uuml;lerinnen und Sch&uuml;ler erweitert und gefestigt und
es war m&ouml;glich, sie nach ihren Meinungen zu befragen.<br>


      <br>


      <br>


Wir, die Klasse 10a, f&uuml;hrten au&szlig;erdem noch ein
Projekt des Dathe-Tages weiter. Dazu teilten wir uns zu diesem Thema
nach den jeweiligen Interessen in drei Gruppen auf. Die einzelnen
Gruppen unterst&uuml;tzten sich gegenseitig und stellten sich die
jeweiligen Arbeitsergebnisse immer wieder vor. Die Informatik-Gruppe
setzte sich vor allem mit der Biographie Dathes auseinander.
Deswegen wurde eine umfangreiche Recherche zum Leben und Wirken Prof.
Dr. Dr. Heinrich Dathes durchgef&uuml;hrt.<br>


Aus diesen Informationen
erstellten wir eine Website f&uuml;r unsere Homepage. Die Fakten
halfen auch der Kunstgruppe bei der Gestaltung eines Zeitstrahls im
Eingang der Schule und der Geschichtsgruppe bei der Vorbereitung der
Diskussion in der Klasse. Die Kunstgruppe sammelte Ideen zur Gestaltung
des Schuleingangs, welche wir in den n&auml;chsten Wochen
k&uuml;nstlerisch umsetzen wollen. Nach einer intensiven Diskussion
&uuml;ber mehrere Entw&uuml;rfe einigten wir uns auf eine
k&uuml;nstlerische Darstellung des Portraits Dathes, aus dessen
Rahmen V&ouml;gel emporsteigen, welche in ihrer Farbenvielfalt
allm&auml;hlich zunehmen. Zus&auml;tzlich wird ein Zeitstrahl
mit den wichtigsten Fakten aus Dathes Leben zu sehen sein. Die
Geschichts-Gruppe besch&auml;ftigte sich mit dem historischen
Hintergrund f&uuml;r Dathes Leben in der NS-Zeit. Dabei ging es um
Recherche, um die Vorbereitung einer Diskussion in unserer Klasse zu
diesem Thema und um Vor&uuml;berlegungen f&uuml;r ein
Gespr&auml;ch mit Dathes Sohn, Herrn Falk Dathe.<br>


      <br>


Die Diskussion fand im Rahmen des
Geschichtsunterrichts mit der ganzen Klasse statt.
Die&nbsp;Frage, warum Dathe in die NSDAP eingetreten war,
ist sicherlich nicht leicht zu beantworten, doch auf einigen Fakten
st&uuml;tzend, kamen wir zu der Auffassung, dass Dathe nicht aus
antisemitischen Gr&uuml;nden eingetreten war.<br>


Auch der einstige Direktor des Leipziger Zoos, der unter den Nazis im
KZ litt, hatte versichert, dass Dathe &bdquo;nichts vom Nazismus
verst&uuml;nde&ldquo; und ihm nach dem Krieg erlaubt, seine
Arbeit im Zoo wieder aufzunehmen. Auch damals war Dathe schonauf seine Regime-Nähe
&uuml;berpr&uuml;ft worden. Er hat seine Vergangenheit nie
verschwiegen.<br>


Vielmehr, denken wir Sch&uuml;lerinnen und Sch&uuml;ler, dass
seine Entscheidung f&uuml;r die NSDAP vor allem wissenschaftliche
Gr&uuml;nde hatte. Er studierte damals Zoologie, Botanik und
Geologie an der Universit&auml;t Leipzig, als der Direktor den
Studenten versicherte, dass der Staat bankrott sei und keiner eine
Stellung als Wissenschaftler bekommen w&uuml;rde.<br>


Kurz darauf ver&ouml;ffentlichten die Nationalsozialisten, dass die
Wissenschaft jetzt wieder Ansehen ben&ouml;tigen und bekommen
w&uuml;rde. Einige Sch&uuml;lerinnen und Sch&uuml;ler
vermuteten auch, dass sich Dathe im Zusammenhang mit seiner Erziehung
aus &bdquo;Liebe zum Vaterland&ldquo; der NSDAP angeschlossen
habe.<br>


Auf die Frage, ob wir eine Umbenennung f&uuml;r richtig hielten,
hatten doch alle Sch&uuml;lerinnen und Sch&uuml;ler der Klasse
10a nach Abschluss der Diskussion dieselbe Meinung: Nein, wir sind
schon seit Jahren die Dathe-OS. Wir stehen mit der Bio-Station und den
engen Beziehungen zum Tierpark f&uuml;r Biologie. Und damit
f&uuml;r Dathe nicht als Nationalsozialisten, sondern als Biologen.<br>


      <br>


zusammengestellt im Namen der Klasse 10a<br>


Laura und Jakob<br>


