&nbsp; <br>

Gro&szlig;e Haustierausstellungen bei denen die Sch&uuml;ler/
innen ihre Haustiere mitbrachten, sollten zum besseren
Verst&auml;ndnis der Natur beitragen. Sp&auml;ter kamen zu den
Aquarien Terrarien und viele Tiere hinzu.<br>

      
<br>
Heute besitzt die Biologie-Station &uuml;ber 1000 Tiere in rund 50
Tierarten!!<br>

Leider ist die 1996 nach modernsten Gesichtspunkten ausgebaute Station<br>

inmitten der Gro&szlig;stadt Berlin, durch viele
Sparma&szlig;nahmen sehr stark gef&auml;hrdet. <br>

      <br>

Wer spenden m&ouml;chte, solle das bitte &uuml;ber den&nbsp;<a href="?sect=./organisation/fv/fv" target="_self">F&ouml;rderverein</a>
tun.<br>

      <br>

Doch nicht nur die Biologie Station ist auf dem neusten Stand gebracht
worden, sondern auch die Fachr&auml;ume Biologie, Physik, Chemie
und Informatik wurden 1995 modernisiert. Die Schule besitzt im Bereich
Informatik zwei R&auml;ume, mit einmal 10 Rechnern mit
Netzwerkverbindung, den Betriebsprogrammen DOS, UNIX und dem
Anwenderprogramm Windows 3.1., sowie einen Raum mit 16 vernetzten
Windows 98 Rechnern. Au&szlig;erdem wird der Schule von der Telekom
ein kostenloser&nbsp;Internet-Zugang zur Verf&uuml;gung
gestellt.<br>

      <br>

1996 (5 Jahre nach dem Tod von Prof. Dr. Dr. Heinrich Dathe) wurde der
Schule, nach langen Reibereien mit den zust&auml;ndigen
&Auml;mtern und Beh&ouml;rden, feierlich der Name <b>Dathe
Oberschule</b> &uuml;berreicht. Der Name des Gr&uuml;nders<br>

und Leiters des Berliner Tierparks, wurde ausgew&auml;hlt, weil
unsere Schule die&nbsp;Biologie-Station
besitzt, die durch Dathe gef&ouml;rdert wurde und so der direkte
Umgang der Sch&uuml;ler mit den Tieren und Pflanzen der&nbsp;Biologie-Station
in und au&szlig;erhalb des
regul&auml;ren Unterrichtes gew&auml;hrleistet ist. Das
w&auml;re ganz im Sinne von&nbsp;<a href="?sect=./unsereschule/geschichte/profdathe" target="_self">Prof. Dr. Dr. Heinrich Dathe</a>
gewesen.<br>

      <br>

