<br>
In den Jahren 1908 sowie 1909 wurde das heutige DATHE-GYMNASIUM
errichtet. Die Baukosten betrugen seiner Zeit etwa 750.000 Reichsmark.<br>

Zun&auml;chst war das Geb&auml;ude durch bauliche
Ma&szlig;nahmen in die 98.Volksschule (Knabenschule) und die 87.
Volksschule (M&auml;dchenschule) eingeteilt. Noch heute kann man
&Uuml;berbleibsel aus dieser Zeit an der Seite zur Helsingforser
Stra&szlig;e sehen. &Uuml;ber den Eing&auml;ngen sowie auf
der Balustrade, sind Steinfiguren,&nbsp;die in M&auml;dchen-
oder Jungenform dargestellt sind. Diese und auch weitere Figuren,<br>

am&nbsp; gesamten Schulgeb&auml;ude, stammen von dem Bildhauer Joseph Rauch<br>

      <div style="text-align: left;">und werden zu seinen
besten Werken gez&auml;hlt.<br>

