      <br>

Nach 1945 wurden aus der Knaben/ M&auml;dchen - Schule die 6. und
14. Oberschule Friedrichshain. 1991 wurde die H&auml;lfte des
Geb&auml;udes als Gymnasium eingerichtet, zun&auml;chst mit
drei Jahrg&auml;ngen von der 7.-9. Klasse. Die andere
H&auml;lfte wurde von der Hoffmann-Grundschule genutzt. 1994 wurde
die Grundschule in einen anderen Hoffmannbau in der Lasdehner Str.
verlegt.<br>

      <br>

1957 entstand auch auf Eigeninitiative von Herrn Vogler langsam
die&nbsp;<a href="?sect=./biologiestation/biologiestation" target="_self">Biologie-Station</a> allerdings nicht
mit so vielen Tieren und "Perfektionismus" (wenn trotz der vielen Geldm&auml;ngel und
Improvisationen<br>

von "perfekt" die Rede sein kann) wie heute. Herr Vogler organisierte Aquarien und die entsprechenden "Pfleger" (Sch&uuml;ler und/ oder
Lehrer ).<br>

