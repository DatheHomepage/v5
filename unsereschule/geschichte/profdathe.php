        <u>Dathes Kindheit:</u>
        <br> Heinrich Dathe wurde am 7. November 1910 als Sohn eines B&uuml;rovorstehers in Reichenbach geboren.
        <br> Dathe verbrachte seine gesamte Kindheit in Reichenbach.
        <br>

        <br>

        <u>Schulzeit:</u>
        <br> Im Gegensatz zu Gesang &amp; Sport, interessierte Dathe sich schon damals sehr f&uuml;r Biologie, aber auch f&uuml;r Deutsch und Zeichnen.
        <br> Er beobachtete oft in seinem Heimatort die Natur und die V&ouml;gel. 1924 zog er dann nach Leipzig mit seiner Familie.
        <br> Er ging oft in den Leipziger Zoo, um Tiere zu zeichnen. Er verglich einen Zoo jedoch mit einem Gef&auml;ngnis der Tiere.
        <br>

        <br>

        <u>Studium:</u>
        <br> Dathe studierte ab 1930 Zoologie, Botanik und Geologie an der Universit&auml;t Leipzig.
        <br> Die NSDAP versprach, dass die deutsche Wissenschaft wiederauferstehen soll, weshalb sich viele Studenten, darunter auch Dathe, sich ihnen anschlossen.
        <br> und so trat 1931 Heinrich Dathe der NSDAP bei. </div>

    <div id="1939" style="display: none;"> <b>Dathe
im Zweiten Weltkrieg </b>
        <br> Auch Heinrich Dathe wurde 1939 in den 2. Weltkrieg eingezogen. 2 Wochen sp&auml;ter kam er jedoch zur&uuml;ck aufgrund eines Oberarmschusses.
        <br> 1940 wurde Heinrich Dathe stellvertretender Direktor des Leipziger Zoos.
        <br> W&auml;hrend seiner Auskurierung lernte er auch seine sp&auml;tere Frau Elisabeth kennen. 1943 heirateten sie.
        <br> Danach musste er aber wieder an die Front, diesmal nach Italien. Dathe kam in englische Kriegsgefangenschaft.
        <br>

    </div>

    <div id="1945" style="display: none;"> <b>Die
Nachkrieszeit </b>
        <br> Dathe kehrte 1947 aus seiner englischen Kriegsgefangenschaft nach Friedrichsfelde zur&uuml;ck. Daraufhin wollte er seine Stellung im
        <br> Leipziger Zoo wieder aufnehmen, wurde aber abgewiesen, da er Mitglied der NSDAP gewesen war. Der Direktor des Zoos jedoch vertraute Dathe und
        <br> legte ein gutes Wort f&uuml;r ihn ein, da er wusste, dass Dathe kein Nazi-Sympathisant war und stellte ihn wieder ein.
        <br> 1954 wurde Dathe nach Berlin berufen. Seine Frau folgte ihm nach 2 Jahren und sie lebten gemeinsam
        <br> mit ihren 3 Kindern (Falk, Holger und Almut) in einer kleinen Wohnung in der Gustav-Adolf Stra&szlig;e . </div>

    <div id="1954" style="display: none;"> <b>Dathes
Zeit im Tierpark </b>
        <br> In Berlin er&ouml;ffnete Dathe im Jahr 1955 seinen Tierpark. Dort f&uuml;hrte er praktisch ein zweites Leben mit seinem Ziehkind, dem Malaienb&auml;rchen Evi.
        <br> Dathe lebte nur f&uuml;r seine Arbeit und nahm sich wenig Zeit f&uuml;r seine Familie oder Privates. Ein Zitat von ihm lautet:
        <br> &bdquo;Ich lebe, um zu arbeiten!&ldquo;
        <br> Jeden Samstagnachmittag konnte man ihn in der Sendung &bdquo;Tierpark &ndash; Teletreff&ldquo; sehen, in welcher
        <br> er eine halbe Stunde lang &uuml;ber den Tierpark redete und &uuml;ber die verschiedenen Tiere berichtete. Einst sagte er: &bdquo;Ein Tierpark steht und f&auml;llt mit der Popularit&auml;t seines Direktors.&ldquo;
        <br> 1963 wurde das Brehmhaus er&ouml;ffnet, in dem die Tiere in Geb&auml;uden auf Freifl&auml;chen
        <br> pr&auml;sentiert wurden. Diese Freifl&auml;chen dienten als B&uuml;hne f&uuml;r die Tiere.
        <br> 1966 erhielt Heinrich Dathe den Nationalpreis f&uuml;r Aufbauarbeit.
        <br> Zusammen mit seinen S&ouml;hnen k&auml;mpfte Dathe 15 Jahre lang f&uuml;r den Bau eines Elefantenhauses. Dieses wurde im
        <br> Jahr 1989 er&ouml;ffnet. In diesem Jahr heiratete Dathe erneut (seine Frau Elisabeth ist am 16. April 1987 gestorben).
        <br>

        <br> Am 07. Dezember 1990 empfing er ein K&uuml;ndigungsschreiben, konnte jedoch einen Aufschub von 6 Monaten erringen.
        <br>

