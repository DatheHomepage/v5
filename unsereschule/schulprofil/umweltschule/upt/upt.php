Am 30. Mai wurde er verwirklicht. Unser Projekttag zum Thema „Umweltschutz“.
Schon viele Wochen vorher begannen die ersten Überlegungen.
Es wurden viele gute Ideen und Vorschläge geäußert, um diesen Tag so schön wie nur möglich zu gestalten.
Das erwies sich als nicht so einfach, denn es mussten eine Menge Dinge beachtet werden.
Zum Ende hin, wurde uns dann die Zeit knapp und so kam es dazu,
dass sich selbst an schulfreien Tagen ein paar Schüler für „den letzten Schliff“ in der Schule trafen.
Und dann waren auch nur noch zwei Blöcke bis zum 30. Mai übrig.
In diesen Unterrichtsstunden bereiteten wir uns weiter mit guter Unterstützung von Fr. Weidemann für den Projekttag vor. 
<br><br>
30. Mai :
Nach einem kurzen Gespräch für letzte Fragen liefen wir alle mit unseren Materialien bepackt zu den Stationen,
an denen wir den restlichen Tag durch viele verschiedene Möglichkeiten den anderen Schülern etwas zu dem jeweiligen Thema beibringen wollten.
Da wurden zum Beispiel Portemonnaies aus alten Tetrapacks gebastelt um zu zeigen, wie man Altes wieder brauchbar machen kann.
Es wurde ein Film zum Thema „Energie“ gesehen und die Schüler sollten danach Sparmaßnahmen für Licht, Wasser, Strom und Wärme aufschreiben. 
Am Ende dieses Tages, hatten wir eine Menge tolle Ergebnisse die sich wirklich sehen lassen konnten. 

Lena
<br><br>

In meinen Augen, war es ein schöner und lehrreicher Tag. Wir als Gruppe (Aufwindkraftwerk) hatten sehr viel Spaß.
Die Klassen waren alle sehr pflegeleicht und haben die Aufgaben gut bis sehr gut gemeistert. 
Alles in Allem war es sehr toll. Ich freue mich schon auf den nächsten Umwelttag!

Kenneth 

<br><br>
Ich fand den Projekttag sehr gelungen, denn jeder hat etwas über die Umwelt gelernt, durch zum Beispiel Experimente oder Filme.
Dabei durfte jeder, der im Naturwissenschaftskurs war, auch einmal vorn die Position des Lehrers einnehmen und Klassen unterrichten.
Bei den Experimenten war es spannend zu beobachten, wie die Schüler versuchten, die Aufgaben so gut wie möglich zu bearbeiten.

Bruno

<br><br>

Der Umwelttag wurde eingeführt um den Schülern verschiedene  Energiesparformen näher zubringen und
da die Schule den Titel „Umweltschule“ verteidigen will. Der Umwelttag wurde von Fr. Weidemanns Naturwissenschaftskurs der Jahrgangsstufe 7/8 geleitet mit der Unterstützung von Fr. Albanis Naturwissenschaftskurs. Der Projekttag wurde den Klassen 5-9 vorgestellt. Ihnen wurden verschiedene Projekte vorgestellt, wie zum Beispiel „die Solartrichter“. Im Großen und Ganzen habe ich den Umwelttag als gelungen empfunden und es hat sehr Spaß gemacht. Ich denke die Schüler sind mit einem erweiterten Wissen nach Hause gegangen. 

Kostas
<br><br>
Der Umwelttag an unserer Schule sollte den Schülern den Klimaschutz beschreiben und Schutzmaßnahmen erläutern.
Wir als naturwissenschaftlich bestimmte Schule, mit dem Titel „Umweltschule“ sind wohl gut geeignet dies zu tun.
Außerdem möchte die Schule ja diesen Titel verteidigen, aber dies gelingt nur, wenn sie auch dieses Jahr wieder Aktionen  wie den Umwelttag startet. Das Besondere an diesem Tag war, dass die Schüler der Naturwissenschaftskurse „die Lehrer“ waren und an ihren Stationen die anderen Schüler unterrichtet haben. Dadurch war der Tag nicht so verkrampft und die Schüler hatten viel Spaß.

Leon 

