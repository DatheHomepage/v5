<div class="teaser">
	<b>Wir sind wieder "Umweltschule"! </b>
	<br>
	<br> Am 30.06.2014 haben wir zum 5.Mal in Folge den Titel "Umweltschule in Europa" erhalten. Wir freuen uns darüber und sehen es als Höhepunkt unserer diesjährigen Aktivitäten im Bereich Umweltbildung, in denen unter anderem die Themen Nachhaltigkeit und Umgang mit Ressourcen sowie der Umweltschutz von Bedeutung waren.
	<br>
	<br> Spannende Projekte des Schuljahres waren unter anderem:
	<br> - Umwelt baut Brücken
	<br> - Junge Reporter für die Umwelt
	<br> - Internationaler Workshop Litter less Winners
	<br> - Ausbau der Braunkohleförderung trotz Energiewende?
	<br> - Fisch trifft Tomate/Schülertag im IGB
	<br> - Besuch der Blumberger Mühle zur nachhaltigen Ernährung und nachhaltigen Waldnutzung
	<br> - Leben im Ausland
	<br> - Indien - Besuch aus dem Land der Widersprüche
	<br> - Ich werde Zootierexperte - Tierparkprojekt
	<br> - Wir retten die Welt - Globale Müll- und Wasserprobleme
	<br> - Baumpflanzaktion
	<br>

	<br> Außerdem fanden zum 2. Mal die Umweltaktionstage statt. Mit ausgewählten Aktionen bzw. Projekten verschiedener Fachbereiche und außerschulischer Partner wurde das Thema Bildung für nachhaltige Entwicklung auch praktisch umgesetzt.
	<br>
	<br> Themen der Aktionstagewaren unter anderem:
	<br> - Naturschutz-Geocaching Tour am Teufelssee
	<br> - Schutz bedrohter Tier- und Pflanzenarten - Artenschutzkoffer
	<br> - "Schatztruhe Mülltonne"
	<br> - Leben in einer globalisierten Welt - "KEEP COOL"
	<br> - Entwicklungszusammenarbeit und Armutsbekämpfung
	<br> - Nachhaltiger Tourismus
	<br> - "Klobalisierte Welt" - Bedeutung von Wasser
	<br> - Ressource Erdöl - Plastikmüll - Baumwolltaschenprojekt
