      <div class="teaser"> <br> <b><center>Baumpflanzaktion:</b></center>
	<br>
Am 22.11.2013 hat die Klasse 6a eine Baumpflanzaktion gemacht. All dies hing mit der Klassenfahrt zur Blumberger M&uuml;hle zusammen. 
Das Thema der Klassenfahrt war &#187  Nachhaltige Waldnutzung. 
Uns war der Umweltschutz sehr wichtig, deshalb haben wir uns an einer Ausschreibung der  Berliner Umweltstiftung beteiligt.<br>
Wir erinnern uns an diesen Tag: Auf den Pl&auml;tzen au&szlig;erhalb des Schulgel&auml;ndes liefen an diesem Tag mehrere L&ouml;cher 
(die wir vorher gegraben hatten), wegen dem Regen mit Wasser voll. Als Sch&ouml;pfinstrumente hatten wir einen Eimer und eine Gie&szlig;kanne. 
Es dauerte ziemlich lange, doch zum Schluss haben wir es geschafft in jedes Loch 3 Rotbuchen zu pflanzen.<br>
Wir haben die meisten B&auml;ume auf dem Schulgel&auml;nde gepflanzt, aber einige auch in der N&auml;he der Metro. 
Trotz des schlechten Wetters haben wir die B&auml;ume eingepflantzt. <br>
