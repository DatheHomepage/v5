		Umweltprojekt der Klassen 8c, 8d erfolgreich! <br>
		Die SUS der beiden Klassen nahmen an einem zweiwöchigen Umweltprojekt zur Verkleinerung des CO2-Fußabdruckes teil.<br>Wir kooperierten dazu mit einem jungen Start-Up aus Berlin, das dabei ist, eine Online-Plattform bzw. Smartphone-App zum CO2-Verbrauch zu entwickeln. In einer Auftaktveranstaltung am 13.01.2017, wurde den SuS vermittelt, in welchen Bereichen menschengemachte CO2-Emissionen eine Rolle spielen. <br>Danach sollten sie sich der Herausforderung stellen, innerhalb von zwei Wochen persönlich möglichst viel CO2-Ausstoß zu vermeiden. Dafür erhielten sie über Email/per Smartphone tageweise Aufgaben zur Einsparung von CO2 übermittelt, deren Bewältigung sie dann mit dem Dokumentieren (per Foto oder per Nachricht) an die Veranstalter/greensoeasy zurückmeldeten.<br>Am 07.02.2017 erfolgte die Auswertung des Projektes durch das Team von greensoeasy in der MZH. Alle SuS erhielten von Hr. David Hentschel persönlich ein Zertifikat über ihren persönlichen CO2-Fußabdruck der letzten zwei Wochen. Die 63 SuS konnten 1.953kg CO2 einsparen, umgerechnet sparte jeder Teilnehmer pro Tag 2,21kg CO2 ein. Um einen Temperaturanstieg von über 2° C zu vermeiden, müssten pro Tag von jedem Verbraucher „nur“ 0,9kg pro Tag gespart werden. Die SuS waren also sehr erfolgreich! Die SuS haben das Projekt mit 4 von 5 möglichen Punkten bei der Auswertung als positiv bewertet und gaben den Initiatoren viele nützliche Hinweise zur Weiterentwicklung ihrer App. Auch für uns war die Zusammenarbeit mit diesem außerschulischen Partner neu.Für Nachahmer oder interessierte Kolleg*innen geben wir gern unsere Erfahrungen weiter. Vielen Dank von uns an alle Kolleg*innen, die die Umsetzung ermöglicht haben.

