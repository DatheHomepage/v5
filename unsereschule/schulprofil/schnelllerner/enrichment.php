<div class="titel">- Enrichmentangebot unserer Schule -</div>

<div class="teaser">
    <br>
    <br> Jede Schülerin und jeder Schüler der Schnelllernerklassen besucht in sechs Schuljahren aus jedem Aufgabenfeld mindestens drei Bausteine.
    <br> Die Zuordnung der Bausteine zu einem Aufgabenfeld ist vom jeweils angebotenen Inhalt abhängig.
    <br> Die Zuordnung zu einer Klassenstufe wird in Abhängigkeit vom altersgerechten Inhalt des jeweiligen Zusatzkurses und nach entwicklungspsychologischen Gesichtspunkten festgelegt.
    <br> Einzelne Zusatzkurse können auch teilweise oder durchgängig in einer Fremdsprache unterrichtet werden.
    <br> Die Zusatzkurse werden projektartig und fächerverbindend unterrichtet.
    <br> Außerschulische Lernorte sind möglich und gewünscht. Kurse im Rahmen der Sommerakademie ergänzen das Konzept.
