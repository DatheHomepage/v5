    <br> Was ist neu ab 2011/2012?
    <br> o Das neue Modell der Schnelllerner sieht vor, dass diese Sch&uuml;lerinnen und Sch&uuml;ler weiterhin anders lernen als die in Klasse 7 in das Gymnasium kommenden Kinder, denn der regul&auml;re Stoff wird mit 4 bis 5 Wochenstunden weniger vermittelt. In der gewonnenen Zeit werden die Sch&uuml;ler nun zus&auml;tzliche
    <br> wissenschaftsprop&auml;deutische Begabtenkurse aus unterschiedlichen
    <br> Aufgabenfeldern absolvieren.
    <br> o Die in diesem Zug aufgenommenen Sch&uuml;lerinnen und Sch&uuml;ler &bdquo;&uuml;berspringen&ldquo; nicht mehr gemeinsam die 8. Klasse, sondern erlangen das Abitur nach 12 Jahren, wie alle anderen auch.
    <br> o Mit speziellen Begabungsf&ouml;rderungskursen im Rahmen des regul&auml;ren Unterrichts in Klasse 5 bis 10 sollen die Sch&uuml;lerinnen und Sch&uuml;ler bei der Ausbildung ihrer individuellen Begabungen angeregt und unterst&uuml;tzt werden.
    <br> o Geplant ist, im Umfang von 4 bis 5 Wochenstunden, zus&auml;tzliche Projekte und Kurse etwa zu den Bereichen Text und Sprache / Kunst und Kreativit&auml;t / Forschen und Experimentieren / Fremdsprachen und Kultur / Medien und Information / Rhetorik und Kommunikation anzubieten.
    <br> o Diese Kurse werden jahrgangs- und f&auml;cher&uuml;bergreifend angeboten.
    <br> o &Uuml;ber den Besuch dieser Kurse muss ein Studienbuch gef&uuml;hrt werden. Die Leistungen werden benotet.
    <br> o Die Wochenstundenzahl pro Jahrgangsstufe ist auf das normale Ma&szlig; von z.B. 30 Stunden in Klasse 5 reduziert, der regul&auml;re Fachunterricht umfasst davon aber
    <br> nur 25 bis 26 Stunden.
    <br>

    <br> Was bleibt wie bisher?
    <br> o In diesen Schulversuch werden, wie bisher nur Sch&uuml;lerinnen und Sch&uuml;ler aufgenommen, die einen einheitlichen Test im Rahmen eines Aufnahmeverfahrens durchlaufen haben.
    <br> o Die Sch&uuml;lerinnen und Sch&uuml;ler dieser Klassen erlernen den Unterrichtsstoff in einer k&uuml;rzeren Zeit (Schnelllerner). Die dadurch gewonnene Unterrichtszeit flie&szlig;t in die Begabungsf&ouml;rderungsangebote.
    <br> o Wir bieten Englisch als Fortsetzung der 1. Fremdsprache und ab Klasse 6 Franz&ouml;sisch oder Spanisch als 2. Fremdsprache an. Auch Kinder, die an der Grundschule mit Franz&ouml;sisch oder Spanisch begonnen haben, k&ouml;nnen auf Antrag in unseren f&uuml;nften Klassen mit Englisch beginnen.
    <br> o Das erste Halbjahr der Klasse f&uuml;nf ist eine Probezeit. Es gelten die Bestimmungen der Sekundarstufe I. Wer die Probezeit nicht besteht, kehrt an die Grundschule zur&uuml;ck. (In den letzten Jahren kam dies nicht vor.)
    <br> o Wer in den Klassenstufen 5 bis 10 nicht versetzt wird, muss den Schulversuch verlassen. Ggf. kann bei Nichtversetzung in eine Regelklasse der Dathe-Oberschule gewechselt werden, ein Anspruch darauf besteht nicht.
    <br> o In der gymnasialen Oberstufe lernen diese Sch&uuml;lerinnen und Sch&uuml;ler gemeinsam mit denjenigen, die die Dathe-Oberschule ab Klasse 7 mit regul&auml;rer Stundentafel und Fachunterricht besucht haben.
    <br> o Es werden die gleichen Pr&uuml;fungen in Klasse 10 und 12 abgelegt.
