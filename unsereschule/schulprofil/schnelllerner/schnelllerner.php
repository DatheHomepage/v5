<p class="teaser">Auch in Zukunft werden an der Dathe-Oberschule begabte und hochbegabte Sch&uuml;lerinnen und Sch&uuml;lern mit &uuml;berdurchschnittlichen Leistungen und hoher Lernbereitschaft ab der 5. Klasse in einem breiten Fachangebot lernen k&ouml;nnen.
    <br>

    <br> Obwohl der Schulsenator Herr Z&ouml;llner zun&auml;chst anders entschieden hatte, wird auch die Dathe-Oberschule den bestehenden Schulversuch zur Begabtenf&ouml;rderung zum neuen Schuljahr 2011/2012 in ver&auml;nderter Form beibehalten. Verpflichtende Zusatzkurse werden k&uuml;nftig das beschleunigte Lernen erg&auml;nzen, gleichzeitig wird das bisherige kollektive &Uuml;berspringen der 8. Jahrgangsstufe abgeschafft und damit
    <br> auch die Anzahl der Wochenstunden auf das sonst &uuml;bliche Ma&szlig; reduziert.
    <br>

    <br> Die Presse hatte dar&uuml;ber ausf&uuml;hrlich berichtet. Der rbb sendete einen Beitrag in der Abendschau am Freitag, den 05.11.2010.
