<div class="titel">- Schnelllerner an der Dathe-Oberschule</div>
<div class="subtitel">- Kein &Uuml;berspringen der 8. Klasse mehr - </div>
<br>

<p class="teaser"> Der Schulversuch, mehrere der so genannten Schnelll&auml;uferklassen an der Schule aufzunehmen, dauert nun schon viele Jahre an. Bislang hat die Dathe-Oberschuledurchweg gute Erfahrungen mit den Sch&uuml;lern dieser Klassen gemacht. Die Sch&uuml;ler solcher Klassen &uuml;bersprangen gemeinsam das achte Schuljahr und wurden direkt in das Neunte versetzt, ohne dabei auf den Planstoff des achten Schuljahres zu verzichten: die Inhalte wurden bereits im Voraus behandelt. Mit der Schulzeitverk&uuml;rzung auf 12 Abiturjahre wird es bei Dathe ab 2011 neue &Uuml;berlegungen geben. Die Dathe-Oberschule wird dann ab der 5. Klasse Schnelllernerklassen anbieten.
</p>
