      <div class="teaser"> <br>
Trotz einer rechtzeitigen Antragstellung (Januar 2014) für die Nachbesetzung der Stelle von Herrn Niedrig 
und der Genehmigung dieser FAV-Stelle durch das BZA, ist diese seit dem  02.01.2015 immer noch nicht besetzt. 
Unsere FÖJ-Praktikanten meistern die Arbeit jedoch bereits seit November 2014 weitestgehend allein, 
da Herr Niedrig wegen Krankheit zuletzt nicht mehr arbeitsfähig war. Es wird immer deutlicher, 
dass dies nur bedingt möglich ist – uns fehlt einfach die dritte Arbeitskraft. <br><br>

<center><img src="./bilder/schlange.jpg"></center><br><br>

Unsere FÖJ-Kräfte werden noch bis Ende August zur Verfügung stehen. Sie haben jedoch beide noch 3 Wochen Urlaubsanspruch 
und Teilnahmepflicht an Seminarfahrten. Es ist absolut sinnvoll, dass Sie ihren Urlaub vor und nicht in den Sommerferien nehmen,
wenn niemand  sie vertreten kann. Um dies zu gewährleisten und der oben geschilderten Unterbesetzung zu begegnen,
werden die Kollegen*innen des FB Biologie nach Möglichkeit und Absprache mit der SL  mit ihrem offenen Stundendeputat verstärkt
in der Biostation  arbeiten. Als Gegenleistung dafür werden Sie dann nicht für Vertretungsunterricht eingesetzt. 
Zurzeit ist die Situation noch verschärft, da beide Praktikanten erkrankt sind. Wir haben deshalb bereits  zum Teil in den Ferien
und in der letzten Woche die Tierpflege übernommen und wurden dabei von Herrn Zappe, Herrn Waligora und Frau Salden tatkräftig unterstützt. 
Vielen Dank dafür.<br><br>
Falls jemand  Interesse und Lust auf Unterstützung  hat, würden wir uns natürlich sehr freuen. 
Sollte jemand Eltern oder Großeltern kennen, die ehrenamtlich tätig werden möchten, sind wir auch hierfür offen. <br><br>
Falls jemand im Kollegium zum Frühjahr einen Baumschnitt vornimmt, sind wir auch an Knabber-Zweigen-Ästen, 
sowie dekorativen Kletterästen interessiert. Frisches Möhrengrün nehmen wir auch immer gern entgegen!<br><br>

