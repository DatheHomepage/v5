            <p>Schulen mit Aquaristik-Arbeitsgemeinschaften haben
sich zu einem Ring
der Vivarienschulen zusammengeschlossen. Die partnerschaftliche
Zusammenarbeit der Schulen und Fachschaften wird durch Rundbriefe und
j&auml;hrliche Treffen bei den Mitgliedsschulen sichergestellt. Der
Ring dient dem Austausch von Erfahrungen und Problemen. Dem Ring der
Vivariumschulen geh&ouml;ren derzeit bundesweit zehn Schulen an.<br>

