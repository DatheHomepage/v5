            <div class="intro" align="justify">
            <p>&nbsp;

Aufgrund der weltweiten Zerst&ouml;rung von Naturr&auml;umen,
der Verkarstungen und Ausbreitung von W&uuml;sten, des Abholzens
von Urw&auml;ldern, des massiven Eingriffs in den Verlauf von
B&auml;chen, Fl&uuml;ssen sowie der zunehmenden Belastung der
Atmosph&auml;re findet ein weltweites Artensterben statt.<br>

            <br>

Aber nicht nur diese Umweltbelastungen, sondern auch die
Konsumgewohnheiten menschenreicher Industrienationen gef&auml;hrden
Arten. Aus diesem Grunde hat der World Wide Fund For Nature (WWF) einen
Artenschutzkoffer konzipiert, in dem Materialien enthalten sind, die
teilweise vom Zoll beschlagnahmt wurden, weil derartige nicht mehr
eingef&uuml;hrt werden d&uuml;rfen und k&uuml;nstlichen
Naturprodukten zum Verwechseln &auml;hnlich sind. <br>

            <br>

Mit Hilfe dieses Koffers kann zum Beispiel im Rahmen eines Rollenspiels
die R&uuml;ckkehr ausl&auml;ndischer Arten protokolliert werden
(Anleitung liegt bei). Die Problematik des Imports von Krokodilleder,
Schlangenh&uuml;ten, Korallen,
Schildkr&ouml;tengeh&auml;usen o. vermittelt werden. Die
Biologie-Station ist die Berliner Ausleihstation f&uuml;r
WWF-Artenschutzkoffer.<br>

