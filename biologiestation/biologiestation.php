    <div class="shadow"><b>06.12.2015: Neue Zwergkaninchen zu Hause auf der Biologiestation!</b></div>

    <p>Die Tiere kommen aus dem Tierheim Ladeburg bei Bernau, welches vom Tierschutzverein Niederbarnim e. V. geführt
        wird. Der Verein hat uns die Tiere zur Hälfte der üblichen Schutzgebühr überlassen und freut sich schon auf
        Nachrichten über die Entwicklung unseres neuen Paares.
        <br>
    </p>


    Seit 1957 besteht die Biologiestation mit ihren mehr als 50 Tierarten an der Dathe-Oberschule. Die Biologie-Station
    ist f&uuml;r die mit dem Namenspatron Heinrich Dathe verbundene inhaltliche Festlegung f&uuml;r die Dathe-Oberschule
    von zentraler Bedeutung. Sie bestimmt wesentlich das Profil der Schule, indem sowohl w&auml;hrend des Unterrichts
    als auch au&szlig;erhalb des Unterrichts w&auml;hrend der Pausen und in den nachmitt&auml;glichen
    Arbeitsgemeinschaften diese den Sch&uuml;lerinnen und Sch&uuml;lern unbeschr&auml;nkt zur Verf&uuml;gung steht.
    <br>


    <br> Die Biologie-Station steht aber auch anderen Schulen, Kitas, Lehrerseminaren und interessierten Personen zur
    Verf&uuml;gung. Seit &uuml;ber 40 Jahren k&ouml;nnen Kinder und Jugendliche, Studenten, Lehrer(innen) und
    Erzieher(innen) sowie Einzelpersonen im Rahmen von F&uuml;hrungen, Projekttagen, Praktika sowie in der Aus- und
    Weiterbildung einen umfangreichen Tierbestand nutzen.
    <br>


    <br> Die Sch&uuml;lerinnen und Sch&uuml;ler der Dathe-Oberschule k&ouml;nnen nach Einwilligung der Eltern probeweise
    Tiere mit nach Hause nehmen und dabei ausprobieren, ob sie sich wirklich um ein eigenes Tier k&uuml;mmern, einem
    nicht seltenen Familienkonflikt.
    <br>


    <br> Die Sch&uuml;lerinnen und Sch&uuml;ler pflegen zusammen mit erfahrenen Mitarbeiterinnen und Mitarbeitern sowie
    den Lehrkr&auml;ften die Tiere. Der Tierbestand ist reichhaltig, wobei auf artgerechte Tierhaltung besonders
    geachtet wird und ggf. die Zahl der Tierarten beschr&auml;nkt bleibt. Das Eintreffen neuer Tiere und vor allem
    Geburten sind Highlights, wobei bereits beachtliche Erfolge zu verzeichnen waren.
