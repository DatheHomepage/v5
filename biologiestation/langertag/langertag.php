	<b>Walk on the wild side</b><br>
	6. Langer Tag der StadtNatur am 16./17. Juni 2012<br><br>
	Im Berliner Großstadtdschungel kann man einiges erleben. Berlin hat aber noch eine <br>
	viel wildere Seite zu bieten, als den Stadtbewohnern bisher bekannt ist. So sind <br>
	Waschbär, Habicht, Biber und Co. hier zu Hause, auch wenn sie für das ungeübte <br>
	Augen nicht immer gleich zu entdecken sind. Am 6. Langen Tag der StadtNatur zeigt <br>
	Ihnen die Stiftung Naturschutz Berlin gemeinsam mit rund 200 Akteuren nicht nur, <br>
	welche tierischen Bewohner direkt vor Ihrer Haustür leben, sondern führt auch in <br>
	Naturräume, die meist nicht zugänglich sind – vom wild-grünen Hinterhof bis zum <br>
	streng geschützten Naturschutzgebiet.<br><br>
	Für alle kleinen und großen Kapitäne gibt es in diesem Jahr ein ganz besonderes <br>
	Highlight: Bei einer Schiffrundfahrt über die 7-Seen rund um den Berliner Wannsee <br>
	besteht mit ein wenig Glück die Möglichkeit, bisher unbekannte Stadtbewohner <br>
	kennenzulernen. Halten Sie ihr Fernglas bereit, wenn Derk Ehlert, der Wildtierexperte <br>
	des Berliner Senats, Sie mit Biber, Seeadler, Eisvogel, Milan und viele weiteren <br>
	Seltenheiten bekannt macht.<br><br>
	Aus insgesamt 500 spannenden und informativen Veranstaltungen können die <br>
	Besucher am 16./17. Juni ihr persönliches Stadtabenteuer zusammenstellen.<br>
	Darunter Streiftouren durch die Lebensräume Berliner Wasserbüffel, <br>
	Entdeckungsreisen durch die Anbauflächen der Urban Gardening-Bewegung sowie <br>
	Höhenflüge im Kletterparadies der Jungfernheide.<br><br><br>
