      <p><b>Aufgaben der Biologie-Station</b></p>

Zu den
regelm&auml;&szlig;igen Besuchergruppen geh&ouml;ren
Kinderg&auml;rten, Grundschulklassen, Grund und Leistungskurse
verschiedener Gymnasien, Studentengruppen und Fachseminare mit
Biologie-Referendaren. Besonders erfolgreich war die Betreuung von
Gruppen mit behinderten Kindern.<br>

      <br>

F&uuml;hrungen durch die Station werden gern von den
verantwortlichen Lehrkr&auml;ften &uuml;bernommen. Den
W&uuml;nschen der Besucher entsprechend sind die F&uuml;hrungen
allgemein gehalten oder thematisch ausgerichtet, z.B. zur Systematik,
Morphologie, Physiologie, Genetik, Verhaltensbiologie oder zu Aspekten
der artgerechten Tierhaltung. <br>

      <br>

Die Schule ist bereit, zus&auml;tzliche &ouml;ffentliche
Aufgaben zu &uuml;bernehmen. Positive Erfahrungen wurden bei der
Durchf&uuml;hrung von Betriebspraktika f&uuml;r
Sch&uuml;lerinnen und Sch&uuml;ler gesammelt. Wir wollen
Jugendlichen die Ableistung eines &ouml;kologischen Jahres
(F&Ouml;J) erm&ouml;glichen und sind ggf. bereit,
Zivildienstleistende aufzunehmen.<br>

