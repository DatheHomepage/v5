<div class="subtitel"> Anwesenheit im offenen Ganztagsbetrieb </div>


<p>
    Die Teilnahme an einer oder mehreren Arbeitsgemeinschaften ist für jeden Schüler/jede Schülerin freiwillig. Nach Voranmeldung erwarten wir eine regelmäßige und verbindliche Teilnahme. Die AG-Leitungen erstellen eine Teilnehmerliste und dokumentieren die Anwesenheit.
    <br>
    <br> Die Arbeitsgemeinschaften werden in der Regel für ein halbes Schuljahr gewählt. Die Eltern unterstützen bitte die Auswahl und die regelmäßige Teilnahme ihrer Kinder. Genau wie beim „normale Unterricht" wird erwartet, dass man sich bei Nichtteilnahme beim AG-Leiter/in entschuldigt.
    <br>
    <br> Neben den Arbeitsgemeinschaften gibt es auch offene Angebote, bei den eine Voranmeldung nicht notwendig ist. Diese Angebote stehen allen Schüler/innen zwischen 13:45 und 16 Uhr offen.
    <br>
    <br> Übrigens: Gerne gehen wir auch auf Vorschläge von euch für neue AG-Ideen und -Interessen ein. Bringt euch ein, sprecht uns an!
