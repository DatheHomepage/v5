<div class="titel">Informationen zum Ganztagsbetrieb am Dathe-Gymnasium</div>
Das Dathe-Gymnasium ist seit diesem Schuljahr eine offene Ganztagsschule mit viertägigem Ganztagsangebot für alle Jahrgänge.
<br>
<br> Ob naturwissenschaftlich profiliert, ob mathematisch interessiert oder musisch begabt, jeder Schüler findet auch nach Unterrichtsschluss interessante Angebote an unserer Schule.
<br>
<br> Unser Ziel ist es, unsere Schülerinnen und Schüler davon zu überzeugen, dass der Besuch von Arbeitsgemeinschaften kein Verlust ihrer Freizeit darstellt, sondern ihnen die Möglichkeit eröffnet, in dem vertrauten und geschützten Bereich unserer Schule spannende und abwechslungsreiche Erfahrungen machen zu können. Die Schülerinnen und Schüler können nach Möglichkeit ihre Kenntnisse vertiefen, ihre Erfahrungen ausbauen und neue Impulse erhalten.
<br>
<br> Wir erhoffen uns dadurch nachhaltige Lernerfolge, eine Stärkung des Sozialraums Schule, eine stärkere Identifikation mit der Schule und eine positive Außenwirkung.
<br>
