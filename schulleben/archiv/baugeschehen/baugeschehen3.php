      <br>

      <em>Ausbau von zwei Kellerr&auml;umen
f&uuml;r schulische Nutzung:</em><br>

Der Computerkellerraum und die Keramikwerkstatt (linker
Seitenfl&uuml;gel) werden entsprechend der Baurechtlichen Vorgaben
ausgebaut und k&ouml;nnen nach Fertigstellung wieder genutzt
werden. Es werden normgerechte Fenster eingebaut, so dass die linke
Innenhofseite im Bereich der Kellerfenster als Baustelle eingerichtet
wird. Dadurch wird auch ein Teil der Fahrradst&auml;nder nicht
nutzbar sein. </p>

