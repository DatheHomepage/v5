      <p class="intro" align="left"><strong>Zum
Baugeschehen allgemein</strong>(Stand&nbsp; Dezember 2008)<strong>:</strong></p>

      <p class="intro" align="left">An
den zahlreichen Baustellen der Dathe-Schule hat sich einiges getan,
wenn auch nicht alle Zeitpl&auml;ne eingehalten wurden.</p>

      <p class="intro" align="left">Auf der
Schulhoferweiterungsfl&auml;che wurden noch im Herbst die
ersten Pflanzungen vorgenommen und die ersten Spielger&auml;te
montiert. &Uuml;bergeben werden soll das Bauvorhaben im
Fr&uuml;hjahr.</p>

      <p class="intro" align="left">Die
Sanierung der Turnhalle verz&ouml;gert sich weiter. Momentan ist
das Ziel formuliert, die Arbeiten zu Beginn des zweiten Halbjahres
abzuschlie&szlig;en und die Halle zu &uuml;bergeben.</p>

      <p class="intro" align="left">Die
&Uuml;bergabe der beiden neuen Arbeitsr&auml;ume im Keller
steht unmittelbar bevor.</p>

      <p class="intro" align="left">Der
Einbau der Sicherheitstechnik macht Fortschritte.<b> </b>Die
Hausalarmanlage ist fertiggestellt. Der Einbau der elektrischen
Fluchtwegbeleuchtung soll bis zum Februar abgeschlossen sein.</p>

      <p class="intro" align="left">Die
neuen Unterrichtsr&auml;ume werden noch nicht zu Beginn des zweiten
Halbjahres &uuml;bergeben werden k&ouml;nnen. Voraussichtlich
im M&auml;rz gibt es dann deutlich bessere Unterrichtsbedingungen
im Haus.</p>

      <p class="intro" align="left">Bis
zum Ende des Jahres werden die Fenster der Unterrichtsr&auml;ume
zum Innenhof ausgetauscht und die Fassade erneuert.<b> </b>Dazu
wird das Geb&auml;ude im Innenhof noch einmal komplett
einger&uuml;stet werden.</p>

      <p class="intro" align="left"><b>Feierliche
Einweihung des neuen Versammlungs- und Klausurraumes/
Mehrzweckhalle(September 2008)</b> <br>

      <br>

&Uuml;ber den Fortschritt bei den Bauarbeiten in der Schule wurde
an dieser Stelle regelm&auml;&szlig;ig berichtet. Um so
erfreulicher, dass nun nach der &Uuml;bergabe der beiden
Musikr&auml;umen im vergangen Herbst auch zu Beginn dieses
Schuljahres ein weiteres Etappenziel erreicht wurde. <br>

      <br>

Im Beisein des Stadtbezirksb&uuml;rgermeisters Herrn Dr. Schulz,
zahlreicher G&auml;ste aus Verwaltung und Politik, Vertretern des
planenden Architekturb&uuml;ros, der bauausf&uuml;hrenden
Firmen sowie den Angeh&ouml;rigen der Dathe-Oberschule wurde im
Rahmen einer kleinen Feierstunde die sanierte ehemalige kleine
Turnhalle als Mehrzwecksaal an uns als k&uuml;nftige Nutzer
&uuml;bergeben. <br>

      <br>

Ein nicht unerheblicher Teil der 7,5 Millionen Euro, die in den
vergangenen und den n&auml;chsten Jahren in die Sanierung der
Schule und des schulischen Umfeldes investiert wurden bzw. noch werden,
hat die marode Sporthalle in einen hellen, freundlichen Ort
f&uuml;r vielf&auml;ltige Veranstaltungen verwandelt.<br>

Die dabei enth&uuml;llte Fotogalerie dokumentiert eindrucksvoll
diese Metamorphose.<br>

Hier werden k&uuml;nftig Auff&uuml;hrungen des Darstellenden
Spiels, Konzerte und Konferenzen stattfinden k&ouml;nnen. <br>

      <br>

      <br>

Umrahmt wurde die Veranstaltung von Max Ritter am Klavier, einer von
Frau Knybba mit der Klasse 8c erarbeiteten szenischen Darstellung sowie
dem Auftritt des Mittelstufenchors unter der Leitung von Frau
Beik&uuml;fner mit Franziska Dr&auml;ger am Saxophon.<br>

Das bew&auml;hrte Technikteam (Pascal Redlich, Robert Henke, Thuong
Nguyen, Sven Jeroschewski) sorgte auch in der neuen Umgebung
f&uuml;r den guten Ton.<br>

An dieser Stelle sei noch einmal allen, die an der Planung und
Realisierung dieses Projektes beteiligt waren, ganz herzlich gedankt. </p>

