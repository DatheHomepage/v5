      <b>12./13.12.2012 | Winterabend </b><br>
Wie in jedem Jahr fand an unserer Schule das traditionelle Weihnachtskonzert statt.
Zusätzlich zu diesen beiden Abenden (12./13.12.) haben wir am Mittwoch den 12.12. den Winterabend veranstaltet.
Hier haben die 9. Klassen in der 3. Etage zu u.a. Essen und Trinken aber auch zu verschiedenen Aktivitäten zum Thema Weihnachten
 in verschiedenen Ländern eingeladen. Der Abend lief unter dem Namen „Winterfest in aller Welt“.<br>
Der Erlös des Verkaufs geht an eine ausgewählte Hilfsorganisation.  Dabei  hatten sowohl 
alle Mitwirkenden als auch Kunden mit Stimmzetteln das Recht, diese Organisation zu bestimmen.
Der Andrang war so enorm, dass die Stimmzettel nicht gereicht haben.<br>
Es war wie immer viel los.<br>
Genauere   Informationen finden Sie<a href="http://winterabend.dathebook.de" target="_blank"> hier. </a>
