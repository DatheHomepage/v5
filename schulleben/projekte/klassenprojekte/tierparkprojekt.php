<div class="titel">- Tierparkprojekt vom 7. März bis 10. März 2017 -</div>
In dieser Woche war die Klasse 7a in der Tierpark-Schule Berlin.<br>
Unter zehn Dreier-Gruppen wurden die Tiere Afrikanischer und Asiatischer Elefant, Asiatischer Rothund, Nagel Manati,
Tüpfelhyäne, Kaiserschnurrbarttamarin, Rothandtamarin, Panzernashorn, Uganda-Giraffe und Bergzebra ausgelost.<br>
Die Expertenteams hatten vier Tage lang Zeit, einen Vortrag zu ihrem Tier auszuarbeiten.<br>
Am ersten Tag wurden die Tiere im Tierpark genau beobachtet und Aufgaben bearbeitet.<br>
Zusätzlich wurden Sachmappen ausgegeben und auf dieser Basis Steckbriefe erstellt.
Am zweiten Tag wurden die Info-Plakate erstellt und gestaltet, es wurden Gedichte und Geschichten geschrieben und
Comics gezeichnet.<br>
Zum Schluss ging es zu einer kommentierten Fütterung der Giraffen.<br>
Am dritten Tag wurden die Vorträge geübt und einige davon vor den Mitschülern gehalten.<br>
Am letzten Tag wurden die restlichen Vorträge angehört und von 13 Uhr bis 14 Uhr beim jeweiligen Tier vor Verwandten
und Besuchern gehalten. <br>
Als Belohnung für die harte Arbeit durften die mesopotamischen Damhirsche gefüttert werden (normale Besucher dürfen
dies nicht).<br>
Am Ende jedes Tages durfte man noch im Tierpark bleiben. <br> <br> <br>
