      <div class="titel" align="center">- Ein "Tag für Afrika" -</div><br>
      <div class="teaser"><b>"Hallo liebe Freunde vom Dathe-Gymnasium"</b> - dieses Bild erreichte uns einige Tage zuvor.<br><br>
   Am <b>04.05.2015</b> begrüßten im Rahmen des Enrichment-Unterrichts <i>"Leben im Ausland"</i> 
   die Klassen 8a, 8b und 9a Frau Katrin Rohde aus Burkina Faso / Ouagadougou. Frau Rohde 
   hatte mit 40 Jahren in Deutschland ihren Besitz verkauft und lebt seit 25 Jahren in Burkina Faso, 
   nimmt Waisenkinder auf und begleitet diese erfolgreich durch die Schulzeit bis hin zur beruflichen Ausbildung und in die Jobs.

   <br>
   <br>
   <br>
   
   <center> <img src="./schulleben/projekte/klassenprojekte/afrika/freunde.jpg"> &nbsp; &nbsp; &nbsp;<img src="./schulleben/projekte/klassenprojekte/afrika/afrika1.jpg"></center>
   
   <br>
   <br>
   
   Mit Frau Rohde verbrachten unsere Schülerinnen 
   und Schüler <i>"einen Tag in Burkina Faso / Afrika"</i>. Frau Rohde 
   war überrascht und gerührt, als sie erlebte, wie die Jugendlichen 
   der 8. Klassen ihr Verständnis vom Alltagsleben in Burkina Faso u.a. in einer Nachrichtensendung 
   aus dem Land, in Szenen, Dialogen und  Expertengesprächen, aber auch am Marktstand, bei traditionellen Spielen 
   oder beim Musikmachen zeigten.
   
   <br>
   <br>
   
   Den Jugendlichen erzählte Frau Rohde  anschaulich und mit Herz, unterstützt von Fotos aus ihrer Heimat 
   Burkina Faso, von ihren Kindern bei A.M.P.O. Sie beantwortete lebhaft alle Fragen der Interviewer und des 
   Publikums und vertiefte so die Kenntnisse der Jugendlichen über 
   Burkina Faso und dem Leben in einem der ärmsten Länder der Welt. Für alle war dieser <i>"Tag in Afrika"</i> 
   bewegend und informativ zugleich.   


   
   
