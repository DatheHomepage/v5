<br><br>
Mediengestützte Podiumsdiskussion in der MZH der Dathe-Oberschule
Lehrer/innen und Schüler/innen der Klassen 10 bis 12 sind herzlich eingeladen!
Das Projekt findet am Montag, dem 28.Januar von 13.45 bis 15.15 Uhr statt.
<br><br>
In vielen Klassenzimmern herrschen heute Angst, 
Stress und Gleichgültigkeit. Schüler und Lehrer fühlen sich den Anforderungen, 
sich in immer kürzerer Zeit immer mehr Wissen aneignen zu sollen, nicht gewachsen. 
Mehr noch: sie sehen keinen Sinn, sich in kürzester Zeit Wörter und Zahlen zu merken, 
deren Bedeutung sie nicht begreifen. Und sie sehen, dass die Anstrengung nicht lohnt: 
ein guter Abschluss bietet keine Garantie für einen guten Job. Außerdem achten immer mehr 
Unternehmer auf Fähigkeiten, für die kein Zeugnis ausgestellt wird: 
auf Teamgeist und Urteilskraft, Charakter und Eigensinn, Mitgefühl und Umsicht. 
Gesucht werden Mitdenker, nicht Anpasser. Wie müsste also Lernen im 21. Jahrhundert aussehen? 
Diskutieren Sie mit, wie Schulen zu Orten werden, an denen Kinder und Jugendliche ihre Entdeckerfreude, 
ihre Gestaltungslust, ihre Kreativität und Offenheit nicht mehr verlieren, sondern vertiefend erfahren und entfalten können.
