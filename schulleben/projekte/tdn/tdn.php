      Am 04. Juni 2012 haben SchülerInnen der 10. Klasse des WPU Geographie und LehrerInnen unserer Schule ihr Projekt "Die Klimahelfer" zum Tag 
	  der Nachhaltigkeit auf dem Berliner Alexanderplatz vorgestellt.<br>

      

      <br>



      <br>





<div class="titel" align="center">- Nachhaltigkeit macht Schule - </div>

<br>Unter dem Motto "Das Klima rettet sich nicht von allein! Auch mit kleinen Schritten kann man das Klima retten!"
sind SChülerInnen der 10. Klasse dabei unsere Schule umweltfreundlicher zu gestalten. Dabei wurden beispielsweise
Heiz- und Lüftverhalten der LehrerInnen und SchülerInnen untersucht um Einsparpotenziale zu ermitteln.
Ergänzend dazu wurde ein Workshop in der Schule angeboten.
<br>
Das Projekt wurde im Rahmen des Schülerwettbewerbs 2012 mit dem Sonderpreis ausgezeichnet.
<br>
