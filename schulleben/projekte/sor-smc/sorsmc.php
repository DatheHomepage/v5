				  <b>Am 8. April 2016 </b>bewies das <b>Dathe–Gymnasium</b> erneut, <br>
				  dass sie ihren Titel <b>"Schule ohne Rassismus – Schule mit Courage" </b>nicht nur zur Zierde trägt, 
				  sondern Motivation zeigt, an speziellen Projekttagen den Schüler_innen diesen Slogan nahe zu bringen. 
				  Die Schüler der 10. Klassen konnten zwischen 12 verschiedenen Gruppen wählen. 
				  <br><br>
				  Zur Auswahl standen beispielsweise Workshops, die Rassismus in Sport und Alltag, Homophobie, Islamophobie und weitere Arten 
				  der Diskriminierung behandeln. <br> 
				  Das Projekt wurde durch freie Journalisten_Innen, Vertreter_Innen der Kontakt- und Beratungsstelle für Migranten_Innen und 
				  Geflüchtete (KuB) bereichert. Auch Themen, denen man sich vielleicht nicht zu 100% bewusst war, wie Critical Whiteness 
				  und Hatespeech oder "positiver" Rassismus wurden thematisiert. Es wurde nahe gebracht, wo bzw. warum Rassismus und andere 
				  Diskriminierungen auftreten und wie man sich dem bewusst entgegenstellen. <br>
				  Dieses Projekt fand nun zum dritten Mal in Folge statt und eine Weiterführung dieser wichtigen Tradition ist in Planung. <br>
				  Die Meinungen gerade zu diesem Projekt sind beinahe einstimmig positiv. Frau Preuß, Politiklehrerin an der Schule, 
				  setzt besondere Prioritäten in das Projekt: "Ich persönlich finde es erschreckend, wie stark sich offener Rassismus 
				  in der Mitte der Gesellschaft erneut etabliert, gerade unter dem Deckmantel der Angst vor dem Unbekannten. Dem muss man 
				  entgegenwirken." <br>
				  Nach dem Befragen einzelner Schüler_Innen hat sich vor allem eines hinaus kristallisiert; 
				  dass viele von ihnen, wenn auch nur passiv, selbst Erfahrungen mit dem Thema gesammelt haben und nicht mehr hilflos zusehen, 
				  sondern etwas ändern wollen. 
				  Marie-Sophie Czech, eine Schülerin, hat sich im Unterthema "Homophobie – Kann denn Liebe eine Sünde sein?" eingetragen:<br>
				  "Es ist schrecklich, zu wissen, dass einem im 21. Jahrhundert immer noch vorgeschrieben wird, wen man lieben darf und wen nicht. 
				  Ich selbst habe homosexuelle Freunde, die es nicht immer leicht haben und ich habe mir von diesem Projekt erhofft, einfach in 
				  Zukunft eine helfende Hand sein zu können." <br>
				  Es ist auch der Tag an sich, der für Schüler und Lehrer notwendig ist.<br> 
				  Da der Projekttag von Schülern_Innen für Schüler_Innen organisiert wird, kann man sich in verschiedene Rollen, 
				  z.B. die eines Lehrers_In, hinein versetzen, um so zu lernen sich gegenseitig eher zu respektieren und Verständnis 
				  für Klasse und Lehrkraft zu schulen. <br>
				  Liza Chan Q2

                  
