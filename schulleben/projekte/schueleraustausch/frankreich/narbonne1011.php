	  In diesem Schuljahr nahmen
erneut 27 Sch&uuml;lerinnen und
Sch&uuml;ler aus
unseren zehnten Klassen am<br>

erlebnisreichen Sch&uuml;leraustausch mit 27 franz&ouml;sischen
Sch&uuml;lerinnen und Sch&uuml;lern
aus unserer<br>

Partnerschule <a target="blank" href="http://www.lyc-lacroix-narbonne.ac-montpellier.fr">Lyc&eacute;e
Docteur Lacroix</a>
in Narbonne (S&uuml;dfrankreich) teil.<br>

Im Dezember 2010 haben uns
unsere franz&ouml;sischen G&auml;ste in
Berlin f&uuml;r eine Woche besucht und sind auf den Spuren neuer
deutscher
Geschichte gewandelt.<br>

Sie besichtigten zum Beispiel die
Gedenkst&auml;tte Berliner
Mauer und die Gedenkst&auml;tte Hohensch&ouml;nhausen.<br>

&nbsp;&nbsp;&nbsp; Einen
H&ouml;hepunkt f&uuml;r die deutschen
und franz&ouml;sischen Sch&uuml;lerinnen und Sch&uuml;ler
bildete der gemeinsame Tagesausflug
nach Potsdam. <br>

Die Abende und das Wochenende in den deutschen
Gastfamilien boten
zahlreiche M&ouml;glichkeiten,<br>

sich n&auml;her kennen zu
lernen. Nach einer anstrengenden
aber gewinnbringenden Woche fiel der Abschied dann recht schwer.<br>

      <br>

Mit
Spannung und Freude
bestiegen die deutschen Sch&uuml;lerinnen
und Sch&uuml;ler im April 2011
das Flugzeug,
um ihre Partnersch&uuml;ler zu besuchen.<br>

Fragen kamen auf;<br>

      <br>

Komme
ich in meiner
Gastfamilie zurecht?<br>

Verstehe ich
das,
was meine
franz&ouml;sischen Gasteltern zu
mir sagen?<br>

Reicht mein
Franz&ouml;sisch,
um mich zu verst&auml;ndigen?<br>

      <br>

Diese
Bef&uuml;rchtungen waren jedoch nach dem freudigen Empfang schnell
vergessen.<br>

      <br>

Durch die Ausfl&uuml;ge
nach Carcassonne und Perpignan, in die
Grotte von Limousis und ans Mittelmeer lernten alle die beeindruckende
RegionLanguedoc-Roussillon n&auml;her kennen.<br>

In der Arbeitsgemeinschaft
Frankreichaustausch haben alle
beteiligten
Sch&uuml;lerinnen und Sch&uuml;ler Impressionen dazu in
PowerPoint-Pr&auml;sentationen und
Filmen festgehalten.<br>

Eine Auswahl dieser Arbeiten findet sich demn&auml;chst hier.<br>

