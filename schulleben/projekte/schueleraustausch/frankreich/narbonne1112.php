	 <b>Notre impression de France</b>
	 <br><br>
Lange haben wir darauf gewartet, unsere Austauschpartner wiederzusehen und nun sind die neun gemeinsamen Tage in Narbonne auch schon wieder vorbei.<br> Wir können sagen, dass die Zeit schön und aufregend war und von vielen Eindrücken geprägt wurde. <br>Seinen Austauschschüler nach drei Monaten wieder zu sehen, war für jeden ein anderes und besonderes Gefühl.<br>
Freundschaften wurden geschlossen oder gefestigt, viele neue Orte erkundet und die französische Lebensweise kennengelernt. 
<br>Die Stadt Narbonne ist zwar schön, aber dennoch ein ziemlicher Kontrast zu Berlin.<br> Es gibt viele kleine Straßen, alte malerische Häuser und freundliche Menschen, welche wir bei unseren zahlreichen Ausflügen trafen.<br> 
Am Tag unserer Abfahrt regnete es zum ersten Mal seit vier Monaten, man kann also sagen der Himmel weinte gemeinsam mit den traurigen Schülern.<br> Diese wurden jedoch durch die Möglichkeit getröstet, einen verlängerten Austausch zu machen oder sich einfach in den Ferien mit ihren neuen Freunden zu treffen.<br>
Von daher können wir den Austausch den nächsten Schülergenerationen nur weiterempfehlen und wünschen euch genauso viel Spaß und Freude!
<br><br>
<i>Madita Ruppender und Marc Geißler.</i> 
