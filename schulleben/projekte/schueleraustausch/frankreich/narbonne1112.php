	 <td>	 <b>Attention, les Français arrivent</b>
<br>
Es ist mal wieder so weit, zum nun 5. Jahr infolge organisiert <br>unsere Schule den 8-tägigen Frankreichaustausch.<br> Aus dem gesamten 10. Jahrgang nehmen 26 Schüler erst je einen Franzosen auf, um im März des nächsten Jahres dann selbst nach Narbonne, im Süden Frankreichs, zu reisen.<br> Die betreuenden Lehrer sind wie schon so oft Frau Just und Frau Ramm.
<br>Einmal wöchentlich treffen wir uns zur Frankreich-AG, wo wir über uns und<br> unsere Correspondants reden, uns näher kennenlernen,<br> Essen und Spaß haben können. Dort sind wir auf die Idee gekommen, unsere Corres am Mittwoch den 30.11. <br>in der Mehrzweckhalle mit einem Buffet willkommen zu heißen.<br> Dafür werden Kuchen, Muffins und vielleicht sogar Croissants gebacken, 
damit sie sich wie zu Hause fühlen. Das sollte natürlich auch durch unser restliches Verhalten zu erreichen sein, schließlich sind die Franzosen in ihrer Zeit in Deutschland (29.11.- 07.12.)<br> Teil unserer Familie. 
Tagsüber, während wir in der Schule sitzen,erkunden sie Berlin, aber abends und am Wochenende können sie das Alltagsleben in Berlin kennenlernen. 
Wir hoffen, ihr habt einen guten Eindruck bekommen und meldet euch, wenn ihr so weit seid, eventuell auch für den Frankreichaustausch an.<br>
<br>
<i>Madita Ruppender und Marc Geißler</i>
