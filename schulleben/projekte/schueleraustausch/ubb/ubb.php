Umwelt-Baut-Brücken
ist ein europäischer Schüleraustausch der von
verschiedenen großen Unternehmen gefördert wird.
Unsere Schule betreibt einen Austausch mit einem Slowakischen
Gymnasium. Der Ergebnisse werden mit der Zusammenarbeit des
Tagesspiegels veröffentlicht.<br>


