Sonntag, der gro&szlig;e Tag, an dem wir unsere Austauschsch&uuml;ler 
kennen lernten. Gegen 9:00 Uhr kamen sie in Berlin an, doch geplant war 10:00 
Uhr und um diese Uhrzeit fanden wir Dathe-Sch&uuml;ler uns auch nacheinander ein um 
unsere Austauschpartner abzuholen. Der restliche Tag stand uns vollkommen zur 
freien Verf&uuml;gung. Einige Austauschsch&uuml;ler konnten w&auml;hrend der Nachtfahrt keinen 
Schlaf finden, deswegen holten sie diesen tags&uuml;ber nach. Andere erkundeten 
Berlin, gingen ins Kino oder unternahmen etwas mit anderen Dathe-Sch&uuml;lern und 
ihren Austauschpartnern. 
      <p class="MsoNormal">Am Montag gab es um 10 Uhr ein Begr&uuml;&szlig;ungsfr&uuml;hst&uuml;ck, bei 
welchem ein paar warme und aufmunternde Worte von Frau Schulze nicht fehlen 
durften. Als dann von den Verantwortlichen das Buffet er&ouml;ffnet wurde, fingen die 
kleineren Gespr&auml;che und das fr&ouml;hliche Beisammensein an. Nach einiger Zeit begann 
die Vorbereitung des Projekts und wir setzten uns in unseren Gruppen 
(Infokasten, Reporter, Interview, Kommentar und Foto) zusammen, um zu 
besprechen, was wir am folgendem Tag, Dienstag, dem 2. M&auml;rz, im &Ouml;kowerk beachten 
mussten. Der restliche Vormittag und Mittag stand uns erneut zur freien 
Verf&uuml;gung. Nachmittags trafen wir uns am Brandenburger Tor wieder und hatten 
eine gemeinsame F&uuml;hrung durch den Reichstag, um dort erst einen Vortrag zu h&ouml;ren 
und dann die gl&auml;serne Kuppel zu erkunden.<br>
      <O:P></O:P><br>
Dienstag um 9:30Uhr 
trafen sich alle am S-Bahnhof Grunewald und gingen in das Naturschutzzentrum 
&Ouml;kowerk Berlin e.V. , dort h&ouml;rten wir einen Vortrag &uuml;ber das &Ouml;kowerk, wo uns zum 
Beispiel erkl&auml;rt wurde, womit es sich besch&auml;ftigt, wie zum Bespiel die Arbeit 
mit Schulklassen, Ausstellungen und rumf&uuml;hren von Besuchern. Wir erfuhren, wie 
sie arbeiten und wie es finanziert wird. Nach dem Vortrag teilte sich unsere 52 
k&ouml;pfige Gruppe durch drei und dann wurden wir auf dem Gel&auml;nde des &Ouml;kowerks herum 
gef&uuml;hrt, nun war Zeit f&uuml;r Fotos und Fragen, die wir f&uuml;r unsere Interviews und 
Reportagen brauchten. Nach dem &Ouml;kowerkbesuch teilte sich die vollst&auml;ndige Gruppe 
wieder auf und wir hatten wiederum Zeit f&uuml;r uns und/ oder f&uuml;r 
Unternehmungen.<span>&nbsp;&nbsp;&nbsp;</span><br>
      <O:P></O:P><br>
Am Mittwoch, um 9.00 Uhr 
trafen wir uns in der Schule und fanden uns in den Gruppen zusammen, um 
Reportagen, das Interview, den Infokasten zu schreiben und die Fotos 
auszuw&auml;hlen, um dann am Donnerstag alles beendet zu haben. Damit die Artikel in 
einer oder mehreren Zeitungen abgedruckt werden konnten.</p>

      <p class="MsoNormal">Dies wurde nur durch ein Mittagessen in der Aula f&uuml;r die 
Gastsch&uuml;ler unterbrochen. Der erste Schreibtag war beendet, als die Artikel in 
ihrer Rohfassung fertig gestellt waren. Den Nachmittag hatten alle Sch&uuml;ler/innen 
wieder zur freien Verf&uuml;gung. </p>

      <p class="MsoNormal">Der Donnerstag begann, wie der Mittwoch, um 9.00 Uhr wieder 
in den Gruppen. Alle Artikel wurden bearbeitet und in ihre endg&uuml;ltige Fassung 
