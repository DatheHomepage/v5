war mit dem Verbleib in der Schule allerdings noch lange nicht zu Ende. Nach dem 
Fertigstellen der Artikel hatte jeder wieder etwas Zeit f&uuml;r sich oder f&uuml;r 
Erkundungen. Um 19 Uhr trafen sich die Interessierten, - das hei&szlig;t fast alle- an 
der O<sub>2<span>&nbsp; </span></sub>Arena, um gemeinsam ein Eishockeyspiel der 
Eisb&auml;ren zu erleben.<br>
      <O:P></O:P><br>
Am Freitag trafen wir uns um 8:45 Uhr am 
Eingang des Naturkundemuseums. Dort zeigten wir es bis 11 Uhr den 
Austauschsch&uuml;lern und hatten dann bis 13 Uhr Gelegenheit die Gegend der 
Museumsinsel zu besichtigen, denn um 13 Uhr war ein Besuch im Pergamonmuseum 
geplant. Bis 20 Uhr hatten wir wiederum Zeit zur freien Verf&uuml;gung, bis die 
Abschlussfeier begann. Dies lag allerdings in eigener Organisation zweier 
Sch&uuml;ler, wie auch zuvor schon der Besuch des &Ouml;kowerkes. Die betreuenden Lehrer 
feierten in dieser Zeit allerdings ihre eigene Abschiedsfeier und hatten mit der 
Abschiedsfeier der Jugendlichen nichts zu tun. </p>
Der ganze Samstag und den 
gr&ouml;&szlig;ten Teile des Sonntages lagen in Eigenverantwortung der deutschen Sch&uuml;ler. 
Gegen 20 Uhr mussten wir unsere Austauschpartner wieder zum Bus bringen und bis 
Anfang Juni von ihnen verabschieden, denn dann werden wir sie in der Slowakei 
besuchen und ihre Gastfreundlichkeit kennen lernen. </div>



