      <div class="teaser" style="text-align: left;">Sonntag, der 
06.06.2010<o:p></o:p> 
      
      
      <p class="MsoNormal">Am Morgen des Anreisetages hatten wir alle gute Laune. Zum 
einen, weil wir endlich unsere Austauschpartner wiedersehen w&uuml;rden und zum 
Anderen, weil Reisen in Gruppen immer Spa&szlig; machen.<br>


Das lag vielleicht auch 
daran, dass der Bus direkt nach verlassen des Aldi-Parkplatzes vom WM-Fieber 
erfasst wurde. Es waren nur noch Fanges&auml;nge zu h&ouml;ren, die die morgendliche 
M&uuml;digkeit im Nu vertrieben und sofort die Stimmung im Bus auflockerten.<br>


Alle 
Reisenden waren sehr aufgeregt, da die Meisten noch nicht in diesem Land namens 
Slowakei waren. Doch zuerst musste man Polen durchqueren bis man an seinem Ziel 
ankommt.<br>


Man merkte schlagartig, dass man die Grenze zwischen 
Deutschland/Polen &uuml;berschritten hatte. Nicht weil uns die Reifen vom Bus geklaut 
wurden (dieser Running Gag musste sein), sondern weil die Autobahn auf 
polnischen Gebiet mehr Schlagl&ouml;cher hat als der Mond Krater.<br>


Diese &bdquo;kleine&ldquo; Holperstrecke hielten jedoch alle tapfer 
aus. Wir revanchierten uns mit den immer wiederkehrenden Fu&szlig;ballges&auml;ngen, so 
wusste dann auch halb Polen das wir da waren.<br>


Nach einer langen Fahrt durch 
Polen waren wir endlich in der Slowakei angekommen.<br>


Die ersten Eindr&uuml;cke 
waren einfach nur wundersch&ouml;n. Diese Landschaft ist einmalig in Europa. In allen 
vier Himmelsrichtung sah man Berge, - riesige Berge.<br>


Jedoch standen die 
gr&ouml;&szlig;ten und sch&ouml;nsten Berge bei Poprad. So erkannten wir auch aus dem Bus 
herraus, dass wir am Ziel angekommen sind, denn vor uns erstreckten sich die 
pr&auml;chtigsten und gr&ouml;&szlig;ten Berge der Tatra.<br>


Nach nun ungef&auml;hr 12 Stunden 
Busfahrt wurden wir schon sehnlichst von unseren Gastfamilien erwartet. Kaum 
hatten wir alle begr&uuml;&szlig;t und unsere Kontakte wieder augefrischt, ging es zu den 
Gastfamilien nach Hasue, wo schon leckeres Essen auf uns wartete.<br>


Auf dem Weg 
dort hin mussten wir durch gro&szlig;e Plattenbausiedlungen fahren, die der kleinen 
Stadt mit sch&ouml;nem Stadtkern einen so schlechten Charackter gaben. Jedoch war die 
Landschaft sch&ouml;ner und tr&uuml;bte nicht den neugewonnenen Eindruck Poprad's.<br>


In 
den &bdquo;vier W&auml;nden&ldquo; unserer Gastfamilien angekommen, wurde erst mal deftig 
gegessen. Anschlie&szlig;end war man so voll und m&uuml;de, dass einem nichts anderes &uuml;brig 
blieb als schlafen zu gehen.<br>


Wir alle freuten uns auf den n&auml;chsten Tag, um 
die ersten Eindr&uuml;cke der &bdquo;neuen Familie&ldquo; mit unseren Freunden auszutauschen. 
Auch waren wir gespannt was uns morgen an diesem unbekannten Ort in der Slowakei 
erwartet.</p>



      
      
      <p class="MsoNormal">Montag, der 7. Juni</p>



      
      
      <p class="MsoNormal">Am Montag trafen wir uns in der Plattenbauschule der 
slowakischen Austauschpartner. Zwei traditionell gekleidete M&auml;dchen begr&uuml;&szlig;ten 
uns, indem sie uns Brot gaben, welches in eine Sch&uuml;ssel mit Salz getaucht werden 
sollte. Eine sehr merkw&uuml;rdige Art der Begr&uuml;&szlig;ung, die auch nicht unbedingt gut 
schmeckte. <br>


Nach einer kurzen Begr&uuml;&szlig;ung und einer Pr&auml;sentation der Stadt 
Poprad und der Schule, versuchten sich einige am slowakischen Volkstanz, w&auml;hrend 
die anderen Volleyball spielten. Die f&uuml;r uns ungew&ouml;hnlichen Tanzschritte 
bereiteten einige Probleme, aber der Paar- und Gruppentanz erwies sich als sehr 
lustig und hilfreich. <br>


Nach einer kurzen Stadtf&uuml;hrung konnte dann jeder 
seinen Nachmittag alleine gestalten. Einige gingen Go-Kart fahren, was viel Spa&szlig; 
machte, aber zu einigen schlimmen Verbrennungen f&uuml;hrte. <br>


Die anderen gingen 
Paintball spielen oder in das gro&szlig;e Einkaufszentrum MAX, um sich neu 
einzukleiden, da wir auf die hei&szlig;en Temperaturen nicht vorbereitet waren. Am 
Abend lernten wir dann die ganze Familie richtig kennen und erkundeten die 
Umgebung. Alle wurden herzlich aufgenommen und sofort mit reichlich Essen 
versorgt.<br>


Mein Highlight des Tages war 
der Ritt durch das Heimatdorf meiner Austauschpartnerin auf dem 
Familienpferd.</p>



      
      
      <p class="MsoNormal">Dienstag, der 8. Juni 2010&nbsp;<o:p></o:p></p>



      
      
      <p class="MsoList" style="margin-bottom: 0pt;">Angefangen hat der Tag mit einer 
kleinen Entt&auml;uschung, denn die Flo&szlig;fahrt auf dem Fluss Dunajec musste 
buchst&auml;blich ins Wasser fallen, da der Fluss wegen des Hochwassers einen zu 
hohen Pegelstand hatte. :( Die &Uuml;berschwemmungen hatten gro&szlig;e Sch&auml;den, wie z.B. 
Erdrutsche ausgel&ouml;st und viele Familien ins Ungl&uuml;ck gest&uuml;rzt.<br>


Eine kleine 
Entsch&auml;digung daf&uuml;r war die Greifvogel-Show auf der Burg Lublau aus dem 17.Jh.. 
Als besonders kann wohl angemerkt werden, dass die Burg einen gotischen, einen 
barocken und einen Renaissance-Stil hat.<br>


Wir hatten ein bisschen Angst um 
den, im Vergleich zu den V&ouml;geln, wirklich winzigen Hund des Falkners, aber der 
schien dann letztendlich doch einen entscheidenden Vorteil gegen&uuml;ber den 
angebundenen V&ouml;geln zu haben. Besonders beeindruckt hat uns die Geschwindigkeit 
des Falken beim Sturzflug und die Intelligenz des Raben, der es schaffte, an 
einer Schnur etwas Fleisch St&uuml;ck f&uuml;r St&uuml;ck hochzuziehen. Das sind echt tolle 
Tiere! Am Ende durften wir alle sogar einen der Adler auf den Arm nehmen.<br>


Ein 
weiterer H&ouml;hepunkt des Tages war der Besuch der Bade-Attraktion Poprads: Aqua 
City. Es scheint vielleicht etwas kleiner gegen&uuml;ber unserem Tropical Island, 
aber es hat dort riesigen Spa&szlig; gemacht mit zwei tollen Rutschen, drei 
Au&szlig;enbecken, einem Sportbecken und einem wirklich gro&szlig;en Whirlpool-Becken. Dort 
fand um 21:30 Uhr noch eine Lasershow statt, die Toni leider nicht miterleben 
konnte, da ihr letztes Bus nach Svit (einer 9 km entfernten Kleinstadt) um 22 
Uhr fuhr. Die Anderen hatten daf&uuml;r noch einen &auml;u&szlig;erst gelungenen Abschluss des 
Tages.</p>



      
      
      <p class="MsoNormal">Mittwoch, der 09.06.10</p>



      
      
      <p class="MsoNormal">Am Mittwoch besuchten wir die Belianska Tropfsteinh&ouml;hle, die 
sich<span>&nbsp; </span>im &ouml;stlichen Teil der Belianske Tatras befindet. Ein Teil der 
H&ouml;hle wurde erstmals schon 1882 erschlossen, aber begehbar und elektrisch 
beleuchtet ist sie erst seit 1896. Bedingt durch die niedrige Temperatur von nur 
5.5 &deg;C war warme Kleidung angesagt. Schmale G&auml;nge f&uuml;hrten uns zu der 
atemberaubenden und sehr tief in der Tatra liegenden H&ouml;hle, in denen man neben 
zahlreichen Stalagmiten und Stalagtiten auch noch mehrere kleinere Seen 
bewundern konnte. Unsere H&ouml;hlenf&uuml;hrerin f&uuml;hrte uns den 1370 m langen Weg &uuml;ber 
die insgesamt 860 Stufen an tiefen Abgr&uuml;nden entlang und erz&auml;hlte uns au&szlig;erdem 
noch einiges &uuml;ber die Legenden der H&ouml;hle. Nicht alle konnten angesichts dieser 
tiefen Schluchten im inneren des Gebirges einen k&uuml;hlen Kopf bewahren. Nach 70 
Minuten Wanderung kamen letztendlich aber alle wieder heil und froh aus der 
H&ouml;hle heraus.<br>


Unser n&auml;chster Halt an diesem Tag war die Altstadt von K&auml;smark. 
Dort hatten wir die Gelegenheit zwei Kirchen zu besichtigen. Eine davon ist die 
gr&ouml;&szlig;te Holzkirche der Slowakei. Durch die Hitze fiel es uns allerdings schwer 
die Konzentration zu bewahren. Im k&uuml;hlen Lyzeum von K&auml;smark aber hatten wir dann 
die M&ouml;glichkeit, uns eine Auswahl der insgesamt 150 000 alten, zum Teil auch 
deutschen B&uuml;cher anzusehen, die die gr&ouml;&szlig;te europ&auml;ische, ehemalige 
Hochschulbibliothek zu bieten hatte. Unter ihnen befinden sich auch einige der 
&auml;ltesten Schriften der Geschichte aus aller Welt.<span>&nbsp; </span>Besonders 
interessant f&uuml;r uns waren neben den B&uuml;chern auch die kuriosen Strafen f&uuml;r 
damalige Sch&uuml;ler, die zum Beispiel f&uuml;r Zusp&auml;tkommen mit bis zu 24 Stunden 
aufrechtem Stehen im Kerker weitaus weniger glimpflich davongekommen sind wie 
wir heutzutage.<br>


Nach dem letzten Punkt auf unserem historischen Tagesplan 
fl&uuml;chteten sich alle erst mal in ein k&uuml;hles Restaurant, um dort ein 
ausf&uuml;hrliches Mittagessen zu sich zu nehmen, bevor es wieder mit dem Bus zur&uuml;ck 
nach Poprad ging.<br>


Dies wurde nur durch ein Mittagessen in der Aula f&uuml;r die 
Gastsch&uuml;ler unterbrochen. Der erste Schreibtag war beendet, als die Artikel in 
ihrer Rohfassung fertig gestellt waren. Den Nachmittag hatten alle Sch&uuml;ler/innen 
wieder zur freien Verf&uuml;gung.</p>



      
      
      <p class="MsoNormal">(Donnerstag folgt noch ...)</p>



      
      
      <p class="MsoNormal">Freitag, der 11.06.10</p>



      
      
      <p class="MsoNormal">Am Freitag, dem letzten gemeinsamen Tag, treffen wir uns zur 
Begr&uuml;&szlig;ung um 8 Uhr in der Schule, mit einem mehr oder weniger zeitaufw&auml;ndigem 
Anreiseweg, da einige weit au&szlig;erhalb von Poprad wohnen.<br>

Der Klassenraum ist eigentlich ein Deutschraum und 
bietet f&uuml;r uns noch M&ouml;glichkeiten die Grammatikkenntnisse aufzufrischen. Der 
Gro&szlig;teil der Sch&uuml;ler berichtet sich allerdings &uuml;ber die Geschehnisse am 
Vortag.<br>


Dann kommen die slowakischen Lehrer, die uns &uuml;ber den heutigen 
Ablaufplan aufkl&auml;ren und uns in Gruppen von 3-4 Deutschen aufteilen. In diesen 
Gruppen werden wir jeweils 2 Schulstunden am Unterricht wie Deutsch, Englisch, 
Physik, Geographie oder Sport teilnehmen.<br>


Die slowakische Schule hat 
allerdings keinen Blockunterricht , so wie wir und auch die 
Unterrichtsatmosph&auml;re ist ein bisschen anders.<br>


So sind die Klassen zum 
Beispiel zahlenm&auml;&szlig;ig ziemlich klein, d.h. zirka 15-20 Sch&uuml;ler.<br>
F&uuml;r uns Deutsche war der Unterricht schwer 
verst&auml;ndlich, da wir kein Wort slowakisch beherrschen, aber wir konnten 
Lernstoff in Physik Klasse 12 erkennen, den wir bereits in Klasse 11 behandelt 
haben. Als wir nachfragen, wieso man im Lernstoff so hinterher h&auml;ngt, erkl&auml;ren 
uns die Slowaken, dass sie durch den vielen Deutschunterricht kaum andere F&auml;cher 
hatten und erst sp&auml;ter mit dem richtigen Unterrichtsstoff beginnen.<br>


Nach den 
2 Stunden treffen wir uns wieder im Raum und gestalten zusammen ein Plakat f&uuml;r 
die Slowaken als Andenken an den Austausch.<br>


Um 13 Uhr k&ouml;nnen wir dann wieder 
mit unseren Austauschpartnern Unternehmungen anstellen, um uns am Abend in einem 
kleinen Club mit entspannter Atmosph&auml;re f&uuml;r die Abschiedsparty zu 
treffen.<br>


Dort essen wir gemeinsam und mit vollen B&auml;uchen wird noch bis sp&auml;t 
in die Nacht getanzt.<br>


Sogar die Lehrer genie&szlig;en teilweise die Musik der 
heutigen Jugend auf der Tanzfl&auml;che.</p>


