	  <b>Litter Less steht für weniger Abfall.</b> Ziel des Projektes ist es, Schüler/innen auf das Thema Müll 
	  aufmerksam zu machen und sie zum Handeln zu bewegen. In Deutschland, Frankreich, Kanada, Montenegro, Rumänien, Dänemark und 
	  anderen Staaten ist am 30.10.2012 ist die zweite Auflage der Litter Less Kampagne gestartet. Artikel, Fotos und Filme zum Thema 
	  Müll konnten eingereicht werden.<br><br>
	  
Die Schüler des Seminarkurses “Umwelt Baut Brücken” haben im ersten Semester Klausurersatzleistungen erstellt die den Kriterien dieses<br> internationalen Journalismuswettbewerb entsprechen und nun eingesendet werden können.

Im vergangenen Jahr haben bereits zwei Schülerinnen bei diesem Wettbewerb tolle Preise gewonnen, unter Anderem einen Workshop in Stockholm!<br><br>
