<div class="teaser">
	<br>
	<b>JumiCo</b> ist ein Zusammenschluss von Jugendlichen, die ihr Engagement und ihre Alternativen
	<br> zu helfen aufgreifen wollen um die Willkommenskultur gegenüber Flüchtlingen zu stärken und
	<br> zu ihrer Integration ins Alltagsleben beizutragen. Dies erfolgt anhand von schulinternen
	<br> Spendenaktionen und Freizeitangeboten für geflüchtete Menschen um einen Halt für sie
	<br> darzustellen zu können und ein Zeichen zu setzen.
	<br>
	<br>

	<a href="http://jumico.org" target="blank"><i>Link zur Website für weitere Informationen</i></a>
	<br>
	<br>

	<b>Du möchtest selbst aktiv werden?</b>
	<br> Wir treffen uns jeden Dienstag im Dathe Gymnasium, du bist jederzeit willkommen!
	<br> Falls du Interesse hast, informiere dich auf <a href="http://jumico.org" target="blank">unserer Website</a>.
	<br>
	<br> Wenn du regelmäßig direkt in Kontakt mit Geflüchteten treten möchtest, dann kannst du
	<br> dich auch gerne an uns wenden, da wir Kontakt zu einigen Flüchtlingsunterkünften haben.
	<br>

