<br> Die Klausurpl&auml;ne sind i.d.R. aktuell und stehen jedermann zur Verf&uuml;gung. Nachschreibetermine sind aufgelistet,
jeder Sch&uuml;ler findet schnell den gesuchten Termin.
<br> Die Sch&uuml;ler berichten von Austauschreisen und ihrer Mitarbeit in einer der zahlreichen Arbeitsgemeinschaften.
<br>

<br> Alle Fragen zu diesem Angebot, der Schule, den Sch&uuml;lern und Lehrern beantwortet unsere Redaktion
