F&uuml;r die umfangreiche Arbeit der Homepage AG stehen selbiger rund f&uuml;nfzehn Rechner in einem der 2 neuen
Informatikr&auml;ume zur Verf&uuml;gung. An ihnen wird die Seite getestet, gestaltet und mit Inhalten versorgt. Grunds&auml;tzlich funktioniert die gesamte Arbeit in Form einer Kommunikationszentrale.
<br>

<br> &Uuml;ber die offizielle Homepage der Dathe-Oberschule erhalten die Lehrerinnen/Lehrer und Sch&uuml;lerinnen/Sch&uuml;ler
gleicherma&szlig;en schnell aktuelle Informationen. Der Newsletter wird von der Redaktion geschrieben, wie auch die anderen
allgemeinen Inhalte. Die Fachbreiche jedoch sind f&uuml;r ihre Inhalte selbst verantwortlich.
<br>

<br> Wichtige Ergebnisse aus den Schul- und Gesamtkonferenzen, sowie Berichte der Gesamtsch&uuml;ler-
und Gesamtelternvertretung werden auf diesen Seiten ver&ouml;ffentlicht.
<br> Des weiteren informiert unser Schulf&ouml;rderverein &nbsp;&uuml;ber seine T&auml;tigkeiten und Vorhaben.
<br>

<br> Grundgedanke der Arbeit an der Homepage ist ein fl&uuml;ssigerer Austausch zwischen den Lehrern und Sch&uuml;lern,
