<u>Auftaktveranstaltung</u>
<br>
<br> Am Donnerstag, den 16.10.2014 haben wir unsere neue Veranstaltungsreihe, die Dathe-Akademie, eröffnet.
<br>
<br> Unter dem Titel „Auftakt mit Giraffen und Exoten aus der Biologie-Station“ haben Schüler/innen als Tierexperten und der Gast Herr Dr. Sicks, Kurator für Säugetiere im Tierpark Berlin, einen tierischen Abend gestaltet.
<br>
<br> Es war ein würdiger Auftakt für die hiermit geplante Reihe von Abendvorträgen. Die anwesenden Gäste konnten in einem sehr unterhaltsamen Vortrag Interessantes über Giraffen erfahren. Freuen wir uns zum Jahresbeginn auf die Fortsetzung.
