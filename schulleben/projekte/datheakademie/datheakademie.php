<div class="subtitel">Von der ersten Idee zum fertigen Werk</div>
<br> Am Mittwoch, den 14.10.2015 fand die erste Veranstaltung der Dathe-Akademie des Schuljahres 2015/2016 statt, die sich mit dem Thema „Von der ersten Idee bis zum fertigen Werk“ befasste. Die Moderatoren Antonia Becker und Tillmann Drews, die diese Veranstaltung gemeinsam mit anderen Schülern planten und ausrichteten, führten ein interessantes Interview mit dem Autoren Ingo Schulze, in dem er seine persönlichen Erfahrungen mit den Zuschauern teilte und Geschichten aus seinem Leben als Autor erzählte. Mit dabei waren außerdem zwei Schülerinnen der Q1, die eigene Werke vorstellten und mit den Moderatoren über ihre Inspirationen sprachen. So auch Ingo Schulze, der aus einem seiner ersten Romane "33 Augenblicke des Glücks" las. Anschließend gab es eine Fragerunde, bei der sich die Gäste mit Ingo Schulze austauschen konnten. Wir bedanken uns an dieser Stelle nochmal herzlich bei Herrn Schulze und bei den Gästen für den gemütlichen und interessanten Abend.
