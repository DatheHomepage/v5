<u>Europas Sündenfall – Lampedusa und auch Berlin?</u>
<br>
<br> Am Mittwoch, den 14.1.2015 fand die zweite Veranstaltung der Dathe-Akademie statt.
<br> Gemeinsam mit der Journalistin Ellen Trapp und dem ehemaligen Flüchtling Bruno Watara
<br> führten Alwine H. und Justus S. ein sehr interessantes und für die Besucher der Veranstaltung
<br> aufschlussreiches Interview über die Flüchtlingspolitik Europas.
<br> In der anschliessenden, sehr lebhaften, Fragerunde ging es vor allem darum,
<br> welche Möglichkeiten wir selbst haben, Flüchtlingen zu helfen.
<br> Wir bedanken uns nochmals für den Besuch von Ellen Trapp und Bruno Watara.
