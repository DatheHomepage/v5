<div class="subtitel">Nachwuchs bei den Silberäffchen</div>
<br> Im Bild sieht man, wie das Männchen Silvio beide Jungtiere, die am 09.10.2012 geboren wurden, trägt. Das
Männchen übernimmt die Betreuung und übergibt dem Weibchen Silvana die Jungtiere hauptsächlich nur zum Stillen. So
hat sie genügend Zeit zum Fressen, damit sie die Jungtiere gut versorgen kann.
