<b>2017</b><br>
Das Dathe-Gymnasium unterhält nun schon viele Jahre die Patenschaft mit den Silberäffchen Silvio und Silvana des Tierparks Berlin. Auf unseren Exkursionen in den Tierpark gehen wir immer gern genau da vorbei.
Auch in diesem Jahr spenden wir wieder die 1000€. <br>
Am 06.12.017 war es wieder soweit. Für genaueres klicken Sie <a href="./schulleben/schuelerschule/silberaeffchenpatenschaften/patenschaftSilberaeffchen2017.pdf" target="_blank"> hier. </a>
<hr>
Patenschaft für die Silberäffchen wurde am 02.05.2015 erneuert
<br> Im Rahmen ihrer Projekttage:“ Ich werde Tierexperte“ haben die Schülerinnen und Schüler der Klasse 7c am
Freitag unsere Patenschaft für Silvio und Silvana und deren Nachwuchs erneuert. Die Experten für diese Tiere, Alena
Noll und Anna Lehmann, überreichten nach einem interessanten Vortrag zu dieser Tierart dem Kurator Dr. Pauly einen
Scheck von 1000€.
