<b>Patenschaft für die Silberäffchen waurde am 02.05.2015 erneuert</b>
<br> Im Rahmen ihrer Projekttage:“ Ich werde Tierexperte“ haben die Schülerinnen und Schüler der Klasse 7c am
Freitag unsere Patenschaft für Silvio und Silvana und deren Nachwuchs erneuert. Die Experten für diese Tiere, Alena
Noll und Anna Lehmann, überreichten nach einem interessanten Vortrag zu dieser Tierart dem Kurator Dr. Pauly einen
Scheck von 1000€.
