      <td style="height: 1195px; width: 652px;" valign="top">
      <div class="teaser"><br>

      <br>

      <div class="teaser">
      <p>Rund 45 Lehrer/innen, Sch&uuml;ler/innen, Eltern und
der Schulleiter sahen sich am 11. November 2008 in der neuen kleinen
Aula unserer Schule gemeinsam die Kurzfassung des Films
&bdquo;Treibh&auml;user der Zukunft - Wie Schule gelingen
kann&ldquo; an. </p>

      <p>Was wir sahen, war erstaunlich: Durch das Angebot neuer
Unterrichtsmethoden und eine enge Zusammenarbeit des Lehrerkollegiums
ist es in den gezeigten Schulen gelungen, Leistungsbereitschaft,
Fantasie und Freude am eigenst&auml;ndigen Lernen bei den
Sch&uuml;ler/innen zu erwecken. Diese Sch&uuml;ler/innen hatten
nicht mehr die Rolle der passiv Belehrten inne. Sie sind zu aktiv
Gestaltenden geworden. Es war beeindruckend zu sehen, welch positive
Wirkungen dies f&uuml;r den Alltag der Lehrer/innen und
Sch&uuml;ler/innen in den dargestellten Schulen hatte. </p>

      <p>Nach dem Film setzten wir uns in einem gro&szlig;en
Kreis zusammen und diskutierten &uuml;ber die zahlreichen
Ans&auml;tze und Ideen aus dem Film. Teilweise wurde dabei bereits
hinterfragt, ob diese oder jene Idee auch bei uns in der Schule
umsetzbar w&auml;re. Von gro&szlig;em Vorteil bei dieser
Diskussion war, dass sehr viele Lehrerinnen und Lehrer sowie der
Schulleiter anwesend waren, da sie es schlie&szlig;lich sind, die
prim&auml;r f&uuml;r die Gestaltung des Unterrichts
zust&auml;ndig sind. </p>

      <p>Insbesondere folgende Vorschl&auml;ge zur
Weiterentwicklung unserer Schule wurden thematisiert:</p>

      </div>

      <ul>

        <li>
          <div class="teaser">In einer Pilotwoche
k&ouml;nnten neue Lernformen ausprobiert werden, wie etwa
jahrgangs&uuml;bergreifendes Lernen, Abschaffung des
45-Minuten-Taktes, offener Unterricht, individualisiertes Lernen. </div>

        </li>

        <li>
          <div class="teaser">Lernen als Prozess
k&ouml;nnte mehr im Vordergrund stehen.</div>

        </li>

        <li>
          <div class="teaser">Der Unterricht
k&ouml;nnte, wie bereits bei den Wahlpflichtf&auml;chern
Naturwissenschaften, Gesellschaftswissenschaften und
Kulturwissenschaften angestrebt, st&auml;rker
f&auml;cher&uuml;bergreifend und interdisziplin&auml;r
erteilt werden.</div>

        </li>

        <li>
          <div class="teaser">Es k&ouml;nnte
jahrgangsstufen&uuml;bergreifend unterrichtet werden.</div>

        </li>

        <li>
          <div class="teaser">Es k&ouml;nnten
zun&auml;chst mehr Doppelstunden und sp&auml;ter auch
Blockunterricht eingef&uuml;hrt werden.</div>

        </li>

        <li>
          <div class="teaser">Die Schule k&ouml;nnte
verst&auml;rkt f&uuml;r au&szlig;erschulische Personen
ge&ouml;ffnet werden: Eltern k&ouml;nnten z. B. ihre
beruflichen Qualifikationen im Unterricht oder in Projekten einbringen.
Generell k&ouml;nnten sich Eltern mehr an den Projekttagen
beteiligen.</div>

        </li>

        <li>
          <div class="teaser">Es k&ouml;nnten
M&ouml;glichkeiten geschaffen werden, dass sich
Sch&uuml;ler/innen nach dem Unterricht unkompliziert in der Schule
treffen (Sch&uuml;lertreff). Eltern k&ouml;nnten einen
Sch&uuml;lertreffpunkt nachmittags an der Schule betreuen (vor
allem f&uuml;r die j&uuml;ngeren Sch&uuml;ler/innen).
Oberstufensch&uuml;ler/innen k&ouml;nnten nach dem Unterricht
an der Schule Hausaufgabenbetreuung anbieten. </div>

        </li>

      </ul>

      <div class="teaser">
      <p><br>

Auf Grund des sehr konstruktiven Miteinanders in unserer
Diskussionsrunde gelang es abschlie&szlig;end, einige Ideen zu
konkretisieren. Wir verst&auml;ndigten uns auf das folgende weitere
Vorgehen: </p>

      </div>

      <ul>

        <li>
          <div class="teaser">Im n&auml;chsten
Schulhalbjahr sollen an unserer Schule probeweise verst&auml;rkt
Doppelstunden eingef&uuml;hrt werden. </div>

        </li>

        <li>
          <div class="teaser">Anfang des Jahres 2009 soll
im Rahmen von Dathe im Dialog eine Podiumsdiskussion zum Thema
Blockunterricht (und eventuell auch Jahrgangs- und Epochenunterricht)
veranstaltet werden. Die genaue Themenwahl und Vorbereitung dieser
Veranstaltung werden in enger Zusammenarbeit mit allen Beteiligten
erfolgen.</div>

        </li>

        <li>
          <div class="teaser">In den Fachkonferenzen soll
die Idee einer Pilotwoche diskutiert werden, in der neue Lernformen
erprobt werden k&ouml;nnen. Dazu z&auml;hlen z. B. jahrgangs-
und themen&uuml;bergreifendes Arbeiten, Arbeiten im Team,
Projektarbeit, interdisziplin&auml;res Arbeiten, offener Unterricht
in Werkst&auml;tten, verst&auml;rkte Binnendifferenzierung,
Pensenb&uuml;cher u. v. m. </div>

        </li>

      </ul>

      <div class="teaser">
      <p>Insgesamt hatte diese Veranstaltung einen lebendigen und
von Wertsch&auml;tzung getragenen Charakter, sie hat Spa&szlig;
gemacht und einiges angesto&szlig;en. </p>

      <p>Dar&uuml;ber freuen wir uns sehr und werden gerne
weitere Anregungen aufgreifen.<br>

Nicht nur Ideen, sondern auch Mitentwickler sind willkommen.</p>

      <br>

      <p>Das GEV-Team<br>

Nadya Derado, Anne Schmidt-Gertz, Eva Walzik</p>

