Das Thema Suchtmittelgebrauch der Kinder hat im letzten Schulhalbjahr
viele Eltern besch&auml;ftigt. Daher finden derzeit in vielen
Klassen
der Dathe-Oberschule Workshops zum Thema Suchtpr&auml;vention mit
den
Sch&uuml;lerinnne und Sch&uuml;lern statt.<br>

Am Dienstag, dem 23.03. gibt es in diesem Zusammenhang eine
Veranstaltung f&uuml;r die Eltern.<br>

      <br>

Rauchen, Akohol, der Gebrauch illegaler Drogen und
&uuml;berm&auml;&szlig;iger Medienkonsum k&ouml;nnen in
die Sucht
f&uuml;hren. Wie k&ouml;nnen wir unsere Kinder davor
sch&uuml;tzen ?
Wie erkennen wir &uuml;berhaupt, ob ein Kind gef&auml;hrdet ist
? Wann
und wie sollten wir einschreiten ? Was k&ouml;nnen wir tun, wenn
wir
vom Suchtmittelgebrauch anderer Kinder in der Klasse h&ouml;ren ?<br>

      <br>

Diese und &auml;hnliche Fragen wird uns Herr Andreas Engel,
Koordinator
f&uuml;r Suchtprophylaxe in Friedrichshain-Kreuzberg, ein
ausgewiesener
Experte f&uuml;r dieses Thema, beantworten.<br>

      <br>

Herr Engel wird nicht nur &uuml;ber das Thema Sucht und
Suchtprophylaxe
informieren, sondern auch Handlungsm&ouml;glichkeiten aufzeigen.
Wir
Eltern k&ouml;nnen Fragen stellen und uns an der Diskussion
beteiligen.<br>

      <br>

Im Kreis der ElternvertreterInnen haben wir bereits Fragen zum Thema
gesammelt und an den Referenten gesandt. Gerne leiten wir auch Ihre
Fragen (bitte an <span style="font-style: italic;">GEV-Vorsitz(
