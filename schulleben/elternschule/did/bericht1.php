      <p>In unmittelbarer N&auml;he der Schule - entlang der
Helsingforser Stra&szlig;e - entsteht auf dem Gel&auml;nde des
ehemaligen Wriezener Bahnhofs ein &ouml;ffentlicher Park.
Ungew&ouml;hnlich daran ist, dass der neue, etwa drei Hektar
gro&szlig;e Park gemeinsam mit Anwohnern, Sch&uuml;lerinnen und
Sch&uuml;lern, Initiativen und lokalen Unternehmen realisiert wird.
Das Dathe-Gymnasium ist an diesem ganz besonderen Projekt aktiv
beteiligt. Grund genug f&uuml;r uns, in unserer Schule eine
Abendveranstaltung zu diesem Thema zu organisieren. </p>

      <p>Unser Ziel war es, Eltern, Sch&uuml;ler/innen,
Lehrer/innen und auch Anwohner/innen &uuml;ber den aktuellen Stand
der Parkentwicklung zu informieren. Wir wollten z. B. dar&uuml;ber
berichten, wie das Projekt Wriezener Bahnhof &uuml;berhaupt
entstanden ist, wer es finanziert und wer entscheidet, wie der Park
gestaltet und genutzt werden soll. Au&szlig;erdem wollten wir
dar&uuml;ber diskutieren, welche Ideen und Anregungen es zur
Weiterentwicklung des Parks gibt. </p>

      <p>Mit diesem Abend er&ouml;ffnete das Dathe-Gymnasium
&uuml;brigens eine neue Veranstaltungsreihe mit dem Titel "Dathe im
Dialog". Geplant sind hier Themenabende, die sich an die gesamte
Schulgemeinschaft - also Sch&uuml;ler/innen, Lehrer/innen,
Schulleitung und Eltern &ndash; gleicherma&szlig;en wenden.
Dathe im Dialog soll neue Impulse und Anregungen f&uuml;r den
Schulalltag liefern. Um an diesen Abenden eine gemeinsame Diskussion zu
erleichtern, k&ouml;nnten z. B. Impulsreferate von Gastreferenten,
von Sch&uuml;lern, Lehrern bzw. Eltern gehalten werden oder zum
Start ein Film gezeigt werden. </p>

      <p>Doch aller Anfang ist schwer: Obwohl die Veranstaltung
zum Modellprojekt Wriezener Bahnhof gut vorbereitet war und auch die
Einladungen an alle Sch&uuml;lerinnen und Sch&uuml;ler
rechtzeitig verteilt worden waren, blieb die Zahl der G&auml;ste
entt&auml;uschend gering. Fast alle der anwesenden Personen in der
Aula hatten irgendwie mit dem Projekt zu tun. Schade, denn so
verpassten viele Sch&uuml;ler/innen und Eltern einen
&uuml;beraus interessanten Abend. </p>

      <p>Bereits bevor die eigentliche Veranstaltung losging,
konnte man in der Aula zahlreiche, ganz unterschiedliche Modelle eines
"gr&uuml;nen Klassenzimmers" bewundern, die Sch&uuml;lerinnen
und Sch&uuml;ler im Kunstunterricht entworfen und angefertigt
hatten. Viel Zeit blieb dazu allerdings nicht, denn die vier
Teilnehmer/innen der geplanten Podiumsdiskussion waren schnell
startklar. </p>

      <p>Einen &Uuml;berblick &uuml;ber das Gesamtprojekt
bot Frau Ines-Ulrike Rudolph, Architektin bei "tx-architekten", die von
Anfang an das Projekt Wriezener Bahnhof mit viel Energie begleitet,
Mitglied in der "AG Wriezener Bahnhof Plus" ist und die Interessen der
Anwohner/innen vertritt. Sie erl&auml;uterte in ihrem Vortrag u. a.
auch, wie das Projekt Wriezener Bahnhof in dem gro&szlig;en
Modellvorhaben der Bundesregierung "Innovationen f&uuml;r familien-
und altengerechte Stadtquartiere" eingebettet ist. Einen zweiten
Vortrag, der sich auf die Projekte der Schule im Zusammenhang mit dem
Park konzentrierte, hielt Benjamin Dummer, Sch&uuml;ler des 13.
Jahrgangs, der als "Besondere Lernleistung" (Teil des Abiturs) das
Thema "Wriezener Freiraumlabor" gew&auml;hlt hatte. In seinem
PowerPoint-Vortrag ging es also prim&auml;r um das gr&uuml;ne
Klassenzimmer und den Schulgarten, aber auch um die weiteren Module des
Parks, n&auml;mlich den Sportparcours, das Fukuoka Demofeld, den
WLan-Hein und die gemeinschaftliche Nutzung des Lokschuppens.
Unterst&uuml;tzt wurde Benni Dummer von seinem Tutor und Lehrer
f&uuml;r Erdkunde und Biologie, Herrn Stephan Noth. Last but not
least berichtete Frau Weidemann, Lehrerin f&uuml;r
Naturwissenschaften und Leiterin der Biostation, &uuml;ber den
aktuellen Stand der Arbeiten am Schulgarten und &uuml;ber die
Pl&auml;ne, wie es dort weitergehen soll.</p>

      <p>In der anschlie&szlig;enden Podiumsdiskussion
debattierte man &uuml;ber die Weiterentwicklung des Parks und
dar&uuml;ber, wie man dabei sowohl den Interessen der zum Teil
&auml;lteren Anwohner/innen als auch der Schule und Jugendlichen
gerecht werden kann. Es wurde sehr deutlich, dass die Zukunft des Parks
stark vom Engagement und Einsatz aller Beteiligten abh&auml;ngt.
Wiederholt forderten die Referenten zur Mitarbeit auf.
Ausf&uuml;hrlich wurde schlie&szlig;lich auch zur Frage aus dem
Publikum Stellung genommen, wie man den Park vor Vandalismus
sch&uuml;tzen k&ouml;nne. </p>

      <p>Als Moderatorin der Veranstaltung hatte ich zur
Vorbereitung einiges &uuml;ber das Modellvorhaben selbst sowie die
verschiedenen Module des Parks gelesen und war doch erstaunt, wie viel
Neues ich an diesem Abend erfuhr. Am meisten hat mir das Engagement und
der Elan der vier Podiumsteilnehmer/innen imponiert. Man
sp&uuml;rte, dass der Park f&uuml;r sie zur
Herzensangelegenheit geworden ist. </p>

      <p><br>

Eva Walzik<br>

Stellv. GEV-Vorsitzende</p>

