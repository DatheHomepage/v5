<br> Bisher:


<br> Die Veranstaltungsreihe "Dathe im Dialog", die im Schuljahr 2006/2007 von der Elternschaft initiiert wurde, bietet an unserer Schule ein gemeinsames Diskussionsforum für alle interessierten Schüler/innen, Lehrer/innen und Eltern. Auch Gäste, wie etwa Freunde der Schule, sind willkommen.

<p>Bei den "Dathe im Dialog"-Abenden steht jeweils ein aktuelles Thema mit Bezug zur Schule im Mittelpunkt. Das entsprechende Thema wird so aufbereitet, dass eine lebhafte und anregende Diskussion unter den anwesenden Schüler/innen, Lehrer/innen und Eltern entstehen kann. Als Einstieg in den Abend kann z. B. ein externer Experte einen kleinen Vortrag halten, eine Podiumsdiskussion stattfinden oder ein Film gezeigt werden. </p>



<p>Wer Interesse an einem speziellen Thema hat, das auch für die Schulgemeinschaft - also für die Lehrer/innen, Eltern und Schüler/innen - von Interesse ist, kann sich gerne bei uns melden: <em>GEV-Vorsitz@dathe-gymnasium.cidsnet.de</em>.
    <br> Die endgültige Themenauswahl und Terminierung findet in enger Abstimmung mit der Schulleitung statt. Die Einladungen mit allen erforderlichen Informationen werden in der gesamten Schule per E-Mail und Plakaten rechtzeitig breit verteilt. </p>

