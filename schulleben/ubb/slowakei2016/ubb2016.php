<p>Umwelt baut Brücken- Poprad, Slowakei 

Vom 15.06. bis zum 23.06.2016 ist Herr Fiedler mal wieder in die Slowakei gefahren. Netterweise hat er uns mitgenommen ( Gruppenfoto von der Wanderung) 
Nach 16 Stunden Fahrt durch das schöne Europa, voller Aufregung und Neugier auf die bevorstehende Zeit, sind wir endlich in Poprad angekommen und wurden von unseren Gastfamilien herzlich empfangen.
In den folgenden Tagen erwartete uns ein vielschichtiges Programm, welches uns viele bleibende Momente schenkte.    
Unter den Stadtbesichtigungen (z.B. Kosice), Museumsbesuchen und natürlich auch viel Freizeit stach besonders die Wanderung im Gebirge der Hohen Tatra hervor.
Die atemberaubende Natur beeindruckte sogar uns Stadtkinder. 
Der Austausch von den beiden Kulturen hat uns neue Einblicke beschert und die gemachten Erfahrungen werden uns noch lange begleiten. 

PS: Es gibt Pizza für 1€ in der Slowakei

